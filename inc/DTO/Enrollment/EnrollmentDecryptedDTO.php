<?php

declare( strict_types=1 );

namespace Inc\DTO\Enrollment;

/**
 * Class EnrollmentDecryptedDTO
 *
 * Расшифрованный снимок данных зачисления.
 *
 * @package Inc\DTO
 *
 * ### Основные обязанности:
 *
 * 1. **Представление расшифрованного зачисления** — содержит ID зачисления и расшифрованный снимок данных.
 *
 * ### Архитектурная роль:
 *
 * Создаётся сервисами после расшифровки поля snapshot_enc из таблицы enrollments.
 * Используется для отображения данных о зачислении в административной панели
 * и для формирования документов (договор, приказ о зачислении).
 *
 * ### Примечания:
 *
 * - snapshot содержит полный слепок (snapshot) всех данных на момент зачисления:
 *   данные ученика, родителя, информация о договоре, приказе и т.д.
 * - Нужен для аудита и юридической значимости — если данные в основной таблице
 *   изменятся, снимок останется неизменным.
 */
readonly class EnrollmentDecryptedDTO {

	/**
	 * Конструктор DTO.
	 *
	 * @param int   $enrollmentId ID записи зачисления
	 * @param array $snapshot     Расшифрованные данные-слепок (ученик, родитель, договор, приказ)
	 */
	public function __construct(
		public int   $enrollmentId,
		public array $snapshot,
	) {}
}