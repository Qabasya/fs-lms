/**
 * @fileoverview Таблица заявок на вкладке "Заявки" (fs_lms_userlist).
 *
 * @module ApplicationsTable
 * @description Менеджер таблицы заявок, отвечающий за:
 *              - Копирование JOIN-ссылки в буфер обмена с визуальной обратной связью
 *              - Перемещение заявки в корзину с подтверждением
 *              - Восстановление заявки из корзиры
 *              - Полное удаление заявки с подтверждением
 *              - Единую обработку AJAX-запросов через обобщенный метод sendAction
 *
 *              Все операции используют делегирование событий через $('body').on(...),
 *              так как строки таблицы могут обновляться динамически (AJAX-фильтры, пагинация).
 *              Деструктивные действия (перемещение в корзину, удаление) требуют подтверждения
 *              через ConfirmModal для предотвращения случайных действий пользователя.
 *
 * @requires jQuery
 * @requires ConfirmModal - модальное окно подтверждения действий
 * @requires showNotice - утилита для отображения уведомлений в стиле WordPress
 */

import { ConfirmModal } from '../../modals/confirm-modal.js';
import { showNotice } from '../../modules/utils.js';

// Глобальные переменные WordPress, переданные через wp_localize_script.
// vars содержит общие настройки плагина (ajaxurl, общие actions).
// appVars содержит специфичные для модуля заявок данные (nonces для операций с заявками).
// Разделение на два объекта позволяет использовать разные nonce для разных групп действий,
// что повышает безопасность: если токен для одной операции скомпрометирован,
// злоумышленник не сможет выполнить действия из другой группы.
const vars = window.fs_lms_vars;
const appVars = window.fs_lms_applications_vars;

const $ = jQuery;

/**
 * Менеджер таблицы заявок.
 * Обрабатывает пользовательские действия над заявками: копирование ссылок,
 * перемещение в корзину, восстановление, полное удаление.
 */
export const ApplicationsTable = {

    /**
     * Инициализация менеджера таблицы заявок.
     * Проверяет наличие глобальных переменных и инициализирует зависимые компоненты.
     */
    init() {
        // Guard clause: если appVars не определен (скрипт загружен на странице без заявок),
        // прекращаем инициализацию, чтобы избежать ошибок при обращении к appVars.nonces.
        if (!appVars) {
            return;
        }

        // Инициализируем ConfirmModal, так как он используется для подтверждения
        // перемещения в корзину и удаления. Если не вызвать init(), модалка не будет
        // найдена в DOM и методы confirm() не сработают.
        ConfirmModal.init();
        this.bindEvents();
    },

    /**
     * Привязка обработчиков событий к элементам таблицы.
     * Использует делегирование событий через $('body').on(...) для поддержки
     * динамически добавляемых строк таблицы.
     *
     * ПОЧЕМУ ДЕЛЕГИРОВАНИЕ:
     * Таблица заявок может обновляться через AJAX (фильтры, сортировка, пагинация).
     * Если бы мы использовали $('.fs-lms-copy-join').on('click', ...), обработчики
     * навешивались бы только на существующие в момент вызова элементы. Новые строки,
     * добавленные после AJAX-обновления, не имели бы обработчиков.
     * Делегирование через $('body') решает эту проблему: обработчик срабатывает
     * для любого элемента .fs-lms-copy-join, даже если он добавлен после инициализации.
     */
    bindEvents() {
        // Копирование JOIN-ссылки в буфер обмена
        $('body').on('click', '.fs-lms-copy-join', (e) => {
            e.preventDefault();
            this.copyJoinLink(e.currentTarget);
        });

        // Перемещение заявки в корзину (с подтверждением)
        $('body').on('click', '.fs-lms-btn-trash', (e) => {
            e.preventDefault();
            this.confirmTrash(e.currentTarget);
        });

        // Восстановление заявки из корзины (без подтверждения — действие обратимо)
        $('body').on('click', '.fs-lms-btn-restore', (e) => {
            e.preventDefault();
            this.restore(e.currentTarget);
        });

        // Полное удаление заявки (с подтверждением — действие необратимо)
        $('body').on('click', '.fs-lms-btn-delete', (e) => {
            e.preventDefault();
            this.confirmDelete(e.currentTarget);
        });
    },

    /**
     * Копирование JOIN-ссылки в буфер обмена с визуальной обратной связью.
     *
     * ПАТТЕРН: Clipboard API с Promise.
     * navigator.clipboard.writeText() возвращает Promise, который разрешается
     * при успешном копировании и отклоняется при ошибке (например, если браузер
     * заблокировал доступ к буферу обмена или страница открыта не по HTTPS).
     * Мы используем .then() для выполнения действий ТОЛЬКО после успешного копирования.
     *
     * ПАТТЕРН: Временная смена текста кнопки.
     * Сохраняем оригинальный текст, меняем на "✓ Скопировано", через 2 секунды возвращаем.
     * Это дает пользователю мгновенную визуальную обратную связь без toast-уведомлений,
     * что менее навязчиво и соответствует современным UX-трендам.
     *
     * @param {HTMLElement} btn - DOM-элемент кнопки, по которой кликнули.
     *                           Должен иметь data-атрибут data-url с ссылкой для копирования.
     */
    copyJoinLink(btn) {
        // Clipboard API — современный стандарт для работы с буфером обмена.
        // В отличие от устаревшего document.execCommand('copy'), он асинхронный
        // и требует явных разрешений пользователя (или контекста HTTPS).
        navigator.clipboard.writeText(btn.dataset.url).then(() => {
            // Сохраняем оригинальный текст кнопки, чтобы вернуть его после анимации успеха.
            // Используем textContent вместо innerText, так как текст простой (без HTML-разметки)
            // и textContent работает быстрее.
            const originalText = btn.textContent;

            // Меняем текст на индикатор успеха. Символ ✓ (Unicode) используется вместо иконки,
            // чтобы избежать зависимости от шрифтов иконок.
            btn.textContent = 'Скопировано ✓';

            // Через 2 секунды возвращаем оригинальный текст.
            // Это достаточно долго, чтобы пользователь заметил изменение,
            // но достаточно коротко, чтобы не раздражать при многократном копировании.
            setTimeout(() => {
                btn.textContent = originalText;
            }, 2000);
        });
        // Примечание: мы не обрабатываем .catch() здесь, так как Clipboard API
        // в большинстве случаев работает корректно. Если копирование не удалось
        // (например, из-за политик браузера), пользователь просто не увидит галочку,
        // что является приемлемым fallback. Для критичных сценариев можно добавить
        // обработку ошибки с показом toast-уведомления.
    },

    /**
     * Показ модального окна подтверждения перед перемещением заявки в корзину.
     *
     * ПАТТЕРН: Двухэтапное деструктивное действие.
     * Вместо немедленного выполнения, мы сначала показываем подтверждение.
     * Это предотвращает случайные удаления/перемещения, которые могут произойти
     * из-за быстрого клика или неправильного позиционирования курсора.
     *
     * @param {HTMLElement} btn - DOM-элемент кнопки "В корзину".
     */
    confirmTrash(btn) {
        ConfirmModal.confirm({
            title: 'Подтвердите действие',
            message: 'Переместить заявку в корзину?',
            confirmText: 'В корзину',
            cancelText: 'Отмена',
            size: 'sm', // Маленький размер модалки — вопрос простой, не нужно много места
            isDanger: true, // Красная кнопка подтверждения — визуальный сигнал о деструктивном действии
        })
            .then(() => this.moveToTrash(btn)) // Если пользователь подтвердил — выполняем действие
            .catch(() => {}); // Если отменил — ничего не делаем (пустой catch подавляет ошибку отмены)
    },

    /**
     * Показ модального окна подтверждения перед полным удалением заявки.
     * Текст подтверждения более строгий, чем для корзины, так как действие необратимо.
     *
     * @param {HTMLElement} btn - DOM-элемент кнопки "Удалить".
     */
    confirmDelete(btn) {
        ConfirmModal.confirm({
            title: 'Подтвердите удаление',
            // Явно указываем на необратимость действия — это критически важно для UX.
            // Пользователь должен понимать, что после подтверждения данные будут потеряны навсегда.
            message: 'Удалить заявку навсегда? Это действие нельзя отменить.',
            confirmText: 'Удалить',
            cancelText: 'Отмена',
            size: 'sm',
            isDanger: true,
        })
            .then(() => this.delete(btn))
            .catch(() => {});
    },

    /**
     * Перемещение заявки в корзину.
     * Делегирует выполнение универсальному методу sendAction.
     *
     * @param {HTMLElement} btn - DOM-элемент кнопки.
     */
    moveToTrash(btn) {
        this.sendAction(
            btn,
            vars.ajax_actions.moveApplicationToTrash, // WordPress AJAX action name
            btn.dataset.id // ID заявки из data-атрибута
        );
    },

    /**
     * Восстановление заявки из корзины.
     * Обратите внимание: восстановление НЕ требует подтверждения, так как это обратимое действие.
     * Если пользователь случайно восстановил заявку, он может снова переместить её в корзину.
     *
     * @param {HTMLElement} btn - DOM-элемент кнопки.
     */
    restore(btn) {
        this.sendAction(
            btn,
            vars.ajax_actions.restoreApplicationFromTrash,
            btn.dataset.id
        );
    },

    /**
     * Полное удаление заявки.
     * Вызывается только после подтверждения через confirmDelete.
     *
     * @param {HTMLElement} btn - DOM-элемент кнопки.
     */
    delete(btn) {
        this.sendAction(
            btn,
            vars.ajax_actions.deleteApplication,
            btn.dataset.id
        );
    },

    /**
     * Универсальный метод для выполнения AJAX-действий над заявками.
     *
     * ПАТТЕРН: DRY (Don't Repeat Yourself).
     * Вместо того чтобы дублировать код AJAX-запроса в moveToTrash, restore и delete,
     * мы вынесли общую логику в один метод. Различия только в имени action и ID заявки.
     * Это упрощает поддержку: если нужно изменить обработку ошибок или добавить логирование,
     * мы правим только один метод, а не три.
     *
     * ПАТТЕРН: Блокировка кнопки через classList.
     * Используем btn.classList.add('disabled') вместо jQuery .prop('disabled', true),
     * так как CSS-класс 'disabled' может иметь дополнительные стили (например, opacity: 0.5),
     * а не только блокировку клика. Это дает более богатую визуальную обратную связь.
     *
     * ПАТТЕРН: Плавное удаление строки таблицы.
     * После успешного действия строка не исчезает мгновенно, а плавно скрывается через fadeOut.
     * Это дает пользователю время осознать, что произошло, и предотвращает "прыжок" интерфейса.
     *
     * @param {HTMLElement} btn - DOM-элемент кнопки, по которой кликнули.
     * @param {string} action - Имя WordPress AJAX action (например, 'moveApplicationToTrash').
     * @param {string|number} applicationId - ID заявки для выполнения действия.
     */
    sendAction(btn, action, applicationId) {
        // Блокируем кнопку через CSS-класс, чтобы предотвратить повторные клики
        // во время выполнения AJAX-запроса. Это защищает от дублирования действий,
        // если пользователь нетерпеливо кликает несколько раз.
        btn.classList.add('disabled');

        $.post(fs_lms_vars.ajaxurl, {
            action, // Сокращенная запись: эквивалентно action: action
            security: appVars.nonces.trash, // Nonce-токен для защиты от CSRF-атак
            application_id: applicationId,
        })
            .done((response) => {
                // WordPress AJAX всегда возвращает объект { success: boolean, data: any }.
                // Проверяем success, чтобы различать успешные и ошибочные ответы сервера.
                if (response.success) {
                    // УСПЕХ: плавно скрываем строку таблицы и удаляем её из DOM.
                    // $(btn).closest('tr') находит родительскую строку таблицы.
                    // fadeOut(300, ...) — анимация исчезновения за 300мс.
                    // Колбэк внутри fadeOut вызывается ПОСЛЕ завершения анимации,
                    // что гарантирует плавный переход без "прыжков" контента.
                    $(btn).closest('tr').fadeOut(300, function () {
                        $(this).remove();
                    });
                } else {
                    // ОШИБКА СЕРВЕРА: разблокируем кнопку и показываем уведомление.
                    btn.classList.remove('disabled');

                    // Fallback-цепочка для сообщения об ошибке:
                    // 1. response.data?.message — специфичное сообщение от сервера (если есть)
                    // 2. response.data — если сервер вернул просто строку с ошибкой
                    // 3. 'Произошла ошибка.' — дефолтное сообщение, если ничего не найдено
                    // Оператор ?. (optional chaining) предотвращает ошибку, если response.data равен undefined.
                    showNotice(
                        response.data?.message || response.data || 'Произошла ошибка.',
                        'error',
                        $( btn ).closest( '.wrap' ) // Вставляем уведомление в ближайший контейнер .wrap
                    );
                }
            })
            .fail(() => {
                // СЕТЕВАЯ ОШИБКА: потеря интернета, таймаут, ошибка 500 и т.д.
                // Разблокируем кнопку и показываем общее сообщение о проблеме с соединением.
                btn.classList.remove('disabled');
                showNotice( 'Ошибка соединения с сервером.', 'error', $( btn ).closest( '.wrap' ) );
            });
    },
};