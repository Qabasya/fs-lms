/**
 * @fileoverview Модуль управления таблицей студентов с массовыми операциями.
 *
 * @module StudentsTable
 * @description Менеджер таблицы списка студентов с поддержкой:
 *              - Массового выбора через чекбокс "Select All" с состоянием indeterminate
 *              - Массового отчисления студентов с умной очередью обработки
 *              - Массового экспорта PII-данных выбранных студентов
 *              - Реактивного обновления строк таблицы при отчислении через кастомные события
 *
 *              Архитектурная особенность: модуль НЕ управляет модалками напрямую,
 *              а РЕАГИРУЕТ на кастомные события от ExpelModalManager (fs:student:expelled,
 *              fs:student:expel-partial). Это реализация паттерна Pub/Sub, где таблица
 *              является "подписчиком", а менеджер отчисления — "издателем".
 *
 *              Сложная логика массовой обработки:
 *              - Студенты с одним зачислением (singleGroup) отправляются в модалку одним пакетом
 *              - Студенты с несколькими зачислениями (multiGroup) обрабатываются последовательно,
 *                так как для каждого нужно выбрать конкретное зачисление для отчисления
 *              - Используется очередь (_multiGroupQueue) для последовательного открытия модалок
 *
 * @requires jQuery
 * @requires ExpelModal - модальное окно отчисления студентов
 */

import { ExpelModal } from '../../modals/enrollment/expel-modal.js';

const $ = jQuery;

// Геттеры для глобальных переменных WordPress.
// Использование функций вместо прямого обращения позволяет избежать ошибок, 
// если объект fs_lms_vars еще не инициализирован на момент объявления модуля.
const NONCES   = () => fs_lms_applications_vars.nonces;
const ACTIONS  = () => fs_lms_vars.ajax_actions;
const AJAX_URL = () => fs_lms_vars.ajaxurl;

/**
 * Менеджер таблицы студентов.
 * Отвечает за UI-взаимодействия в таблице: выбор, массовые действия, реактивное обновление.
 *
 * @namespace StudentsTable
 */
export const StudentsTable = {

    /** @type {boolean} Флаг для предотвращения повторной инициализации */
    _initialized:       false,

    /**
     * Очередь студентов с несколькими зачислениями для последовательной обработки.
     * Каждый студент извлекается из очереди через shift() и открывается в модалке.
     * После закрытия модалки вызывается следующий студент из очереди.
     * @private
     * @type {Array<{id: string|number, name: string, enrollments: Array}>}
     */
    _multiGroupQueue:   [],

    /**
     * Студенты с одним зачислением, ожидающие обработки после завершения очереди multiGroup.
     * Они будут отправлены в массовую модалку одним пакетом после обработки всех multiGroup.
     * @private
     * @type {Array<{id: string|number, name: string, enrollments: Array}>}
     */
    _singleGroupPending: [],

    /**
     * Инициализация модуля.
     * Проверяет наличие таблицы студентов на странице и навешивает обработчики событий.
     *
     * @memberof StudentsTable
     * @instance
     * @returns {void}
     */
    init() {
        // Защита от повторной инициализации (паттерн Singleton)
        if ( this._initialized ) return;

        // Guard clause: если таблицы студентов нет на странице, не тратим ресурсы
        if ( ! $( '.fs-lms-students' ).length ) return;

        this._initialized = true;
        this._bindEvents();
    },

    /**
     * Привязка обработчиков событий.
     * Включает:
     * - Синхронизацию чекбоксов (Select All + indeterminate state)
     * - Обработку массовых действий (отчисление, экспорт)
     * - Реактивное обновление строк при отчислении через кастомные события
     *
     * @private
     * @memberof StudentsTable
     * @instance
     * @returns {void}
     */
    _bindEvents() {
        // ==========================================
        // 1. ЧЕКБОКС "SELECT ALL" → ВЫБОР ВСЕХ СТУДЕНТОВ
        // ==========================================
        // Когда пользователь ставит/снимает галочку в чекбоксе "Выбрать всех",
        // синхронизируем состояние всех индивидуальных чекбоксов студентов.
        // 
        // e.currentTarget.checked — булево значение состояния чекбокса "Select All".
        // Мы просто передаем его всем чекбоксам .js-student-cb через .prop('checked', ...).
        $( document ).on( 'change', '#js-select-all-students', ( e ) => {
            $( '.js-student-cb' ).prop( 'checked', e.currentTarget.checked );
        } );

        // ==========================================
        // 2. ИНДИВИДУАЛЬНЫЕ ЧЕКБОКСЫ → ОБНОВЛЕНИЕ "SELECT ALL"
        // ==========================================
        // Когда пользователь ставит/снимает галочку у конкретного студента,
        // нужно обновить состояние чекбокса "Select All".
        // 
        // ПАТТЕРН: ТРЕХСОСТОЯТЕЛЬНЫЙ ЧЕКБОКС (indeterminate state).
        // HTML-чекбокс может находиться в трех состояниях:
        // 1. checked = true    — все студенты выбраны (галочка установлена)
        // 2. checked = false, indeterminate = false — ни один не выбран (пустой)
        // 3. checked = false, indeterminate = true  — выбраны частично (черточка вместо галочки)
        // 
        // Состояние indeterminate — это визуальный индикатор "частичного выбора",
        // который широко используется в файловых менеджерах и email-клиентах.
        // Оно устанавливается через свойство DOM-элемента, а не через атрибут HTML.
        $( document ).on( 'change', '.js-student-cb', () => {
            const total   = $( '.js-student-cb' ).length;
            const checked = $( '.js-student-cb:checked' ).length;

            // Устанавливаем indeterminate, если выбрано больше 0, но меньше всех
            $( '#js-select-all-students' ).prop( 'indeterminate', checked > 0 && checked < total );

            // Устанавливаем checked в true только если выбраны ВСЕ студенты
            $( '#js-select-all-students' ).prop( 'checked', checked === total );
        } );

        // ==========================================
        // 3. КНОПКА "ПРИМЕНИТЬ" МАССОВЫХ ДЕЙСТВИЙ
        // ==========================================
        // Обработчик клика по кнопке применения массового действия.
        // Логика маршрутизации (export vs expel) находится в методе _applyBulk().
        $( document ).on( 'click', '#js-bulk-apply', () => this._applyBulk() );

        // ==========================================
        // 4. РЕАКТИВНОЕ ОБНОВЛЕНИЕ: ПОЛНОЕ ОТЧИСЛЕНИЕ
        // ==========================================
        // ПАТТЕРН: Pub/Sub через кастомные события jQuery.
        // Когда ExpelModalManager успешно отчисляет студента (полностью, без остатка зачислений),
        // он генерирует событие 'fs:student:expelled' с данными { studentId }.
        // 
        // Таблица подписана на это событие и реагирует: находит строку студента 
        // по data-атрибуту data-wp-user-id и плавно удаляет её из DOM через fadeOut.
        // 
        // ПРЕИМУЩЕСТВА ПАТТЕРНА:
        // - ExpelModalManager не знает о существовании StudentsTable (слабая связанность)
        // - Если в будущем появится другой компонент, которому нужно реагировать на отчисление,
        //   он просто подпишется на то же событие без изменения кода менеджера
        // - Легко тестировать: можно программно сгенерировать событие и проверить реакцию
        $( document ).on( 'fs:student:expelled', ( e, { studentId } ) => {
            // Находим строку таблицы по ID пользователя WordPress
            // и плавно удаляем её с анимацией fadeOut (300мс).
            // Колбэк внутри fadeOut вызывается после завершения анимации.
            $( `tr[data-wp-user-id="${ studentId }"]` ).fadeOut( 300, function () {
                $( this ).remove();
            } );
        } );

        // ==========================================
        // 5. РЕАКТИВНОЕ ОБНОВЛЕНИЕ: ЧАСТИЧНОЕ ОТЧИСЛЕНИЕ
        // ==========================================
        // Если у студента было несколько зачислений и отчислили только одно из них,
        // генерируется событие 'fs:student:expel-partial' с данными:
        // - studentId: ID студента
        // - remaining: массив оставшихся зачислений
        // 
        // Таблица должна обновить строку студента, показав только оставшиеся зачисления.
        // Это сложная операция, так как нужно обновить несколько колонок и data-атрибуты.
        $( document ).on( 'fs:student:expel-partial', ( e, { studentId, remaining } ) => {
            const $row = $( `tr[data-wp-user-id="${ studentId }"]` );
            if ( ! $row.length ) return; // Если строка не найдена, выходим

            // УТИЛИТА БЕЗОПАСНОГО ЭКРАНИРОВАНИЯ (XSS-защита):
            // Создаем временный div, устанавливаем текст через .text() (автоматически экранирует HTML),
            // а затем забираем безопасный HTML через .html().
            // Это гарантирует, что если в названии предмета есть символы '<' или '>',
            // они будут отображены как текст, а не исполнены как HTML-код.
            // 
            // Альтернатива: использовать утилиту escapeHtml из modules/utils.js.
            // Но этот jQuery-трюк короче и не требует импорта дополнительных модулей.
            const esc = ( str ) => $( '<div>' ).text( String( str || '' ) ).html();

            // Формируем HTML для каждой колонки.
            // remaining.map() преобразует массив зачислений в массив строк,
            // а join('<br>') объединяет их через HTML-перенос строки.
            // Это создает многострочный контент внутри одной ячейки таблицы.
            const subjectHtml  = remaining.map( r => esc( r.subject_name ) ).join( '<br>' );
            const groupHtml    = remaining.map( r => esc( r.group_title ) ).join( '<br>' );
            const scheduleHtml = remaining.map( r => esc( r.schedule || '—' ) ).join( '<br>' );
            const contractHtml = remaining.map( r => esc( r.contract_no || '—' ) ).join( '<br>' );

            // ОБНОВЛЕНИЕ КОЛОНОК ПО ИНДЕКСУ:
            // Используем .eq(index) для доступа к конкретной ячейке таблицы по её позиции.
            // Индексы колонок (0-based):
            // 0 = чекбокс выбора
            // 1 = ФИО студента
            // 2 = Предмет (обновляем)
            // 3 = Группа (обновляем)
            // 4 = Расписание (обновляем)
            // 5 = Договор (обновляем)
            // 6 = Действия (кнопки)
            // 
            // ВАЖНО: такой подход хрупкий — если изменится структура таблицы (добавится колонка),
            // индексы сломаются. Более надежный способ — использовать data-атрибуты для колонок,
            // но текущая реализация работает, так как структура таблицы стабильна.
            $row.find( 'td' ).eq( 2 ).html( subjectHtml );
            $row.find( 'td' ).eq( 3 ).html( groupHtml );
            $row.find( 'td' ).eq( 4 ).html( scheduleHtml );
            $row.find( 'td' ).eq( 5 ).html( contractHtml );

            // ОБНОВЛЕНИЕ DATA-АТРИБУТА КНОПКИ ОТЧИСЛЕНИЯ:
            // Кнопка отчисления (.js-expel-student) хранит список зачислений в data-атрибуте
            // data-expel-enrollments в виде JSON-строки. После частичного отчисления нужно
            // обновить этот атрибут, чтобы при следующем клике модалка показала только
            // оставшиеся зачисления, а не все (включая уже отчисленные).
            // 
            // Формируем новый массив зачислений с нужными полями и сериализуем в JSON.
            const newEnrollments = remaining.map( r => ( {
                record_id:    r.record_id,
                subject_name: r.subject_name,
                group_title:  r.group_title,
            } ) );

            // Обновляем атрибут через .attr() (не .data(), так как JSON должен быть в HTML-атрибуте).
            $row.find( '.js-expel-student' ).attr( 'data-expel-enrollments', JSON.stringify( newEnrollments ) );
        } );
    },

    /**
     * Обработка нажатия кнопки "Применить" массовых действий.
     * Маршрутизирует действие к соответствующему обработчику в зависимости от выбранного действия.
     *
     * ЛОГИКА МАССОВОГО ОТЧИСЛЕНИЯ:
     * Студенты разделяются на две группы:
     * 1. singleGroup — студенты с одним зачислением. Отчисляются одним пакетом через openBulk().
     * 2. multiGroup — студенты с несколькими зачислениями. Отчисляются последовательно,
     *    так как для каждого нужно выбрать конкретное зачисление в модалке.
     *
     * ПОЧЕМУ РАЗДЕЛЕНИЕ:
     * Если у студента несколько зачислений, модалка должна показать выпадающий список
     * для выбора конкретного зачисления. Если отчислять таких студентов пакетом,
     * пользователь не сможет выбрать, какое зачисление прекратить.
     * Поэтому multiGroup обрабатываются по одному, а singleGroup — пакетом.
     *
     * @private
     * @memberof StudentsTable
     * @instance
     * @returns {void}
     */
    _applyBulk() {
        // Считываем выбранное действие из выпадающего списка массовых действий
        const action = $( '#js-bulk-action' ).val();

        // Маршрутизация: если выбран экспорт — вызываем обработчик экспорта и выходим
        if ( action === 'export' ) {
            this._applyBulkExport();
            return;
        }

        // Если действие не 'expel' — выходим (других массовых действий пока нет)
        if ( action !== 'expel' ) return;

        // Находим все отмеченные чекбоксы студентов
        const $checked = $( '.js-student-cb:checked' );
        if ( ! $checked.length ) return; // Если никто не выбран, выходим

        // Массивы для разделения студентов по количеству зачислений
        const singleGroup = [];
        const multiGroup  = [];

        // Проходим по всем отмеченным студентам и классифицируем их
        $checked.each( ( _, el ) => {
            // Извлекаем список зачислений из data-атрибута кнопки отчисления.
            // Атрибут data-expel-enrollments содержит JSON-строку с массивом зачислений.
            // 
            // БЕЗОПАСНЫЙ ПАРСИНГ JSON:
            // Оборачиваем JSON.parse в try...catch, так как атрибут может содержать
            // невалидный JSON (например, если данные повреждены или не были инициализированы).
            // Это предотвращает падение всего скрипта при ошибке парсинга одного студента.
            let enrollments = [];
            try {
                const raw = $( el ).closest( 'tr' ).find( '.js-expel-student' ).attr( 'data-expel-enrollments' );
                if ( raw ) enrollments = JSON.parse( raw );
            } catch ( _ ) {
                // Игнорируем ошибку парсинга, оставляем пустой массив.
                // Студент будет классифицирован как singleGroup (enrollments.length === 0).
            }

            // Формируем объект студента с нужными данными
            const student = {
                id:          $( el ).val(), // ID студента из value чекбокса
                name:        $( el ).data( 'student-name' ) || '', // Имя из data-атрибута
                enrollments, // Массив зачислений
            };

            // Классификация: если зачислений больше одного — в multiGroup, иначе в singleGroup
            if ( enrollments.length > 1 ) {
                multiGroup.push( student );
            } else {
                singleGroup.push( student );
            }
        } );

        // ==========================================
        // ЗАПУСК ОБРАБОТКИ
        // ==========================================
        // Если есть студенты с несколькими зачислениями — запускаем последовательную обработку.
        // Студенты с одним зачислением сохраняются в _singleGroupPending и будут обработаны
        // после завершения очереди multiGroup.
        if ( multiGroup.length ) {
            this._multiGroupQueue    = [ ...multiGroup ]; // Копируем массив в очередь
            this._singleGroupPending = singleGroup; // Сохраняем singleGroup для последующей обработки
            this._processNextMultiGroup(); // Запускаем обработку первого студента из очереди
        }
        // Если multiGroup нет — сразу открываем массовую модалку для singleGroup
        else {
            ExpelModal.openBulk( singleGroup );
        }
    },

    /**
     * Массовый экспорт PII-данных выбранных студентов.
     * Для каждого выбранного студента отправляет AJAX-запрос на генерацию файла экспорта
     * и инициирует скачивание через динамическое создание элемента <a>.
     *
     * ПАТТЕРН: Программное скачивание файла через <a>.
     * Браузеры не позволяют напрямую скачивать файлы через AJAX (ограничения безопасности).
     * Вместо этого сервер генерирует файл и возвращает URL для скачивания.
     * Мы создаем невидимый элемент <a>, устанавливаем его href на URL файла,
     // программно кликаем по нему (что инициирует скачивание), а затем удаляем элемент.
     *
     * ВАЖНО: при массовом экспорте браузер может заблокировать множественные скачивания.
     * Современные браузеры ограничивают количество автоматических скачиваний с одной страницы
     * (обычно 3-5 файлов). Пользователю может потребоваться разрешить скачивания в настройках.
     *
     * @private
     * @memberof StudentsTable
     * @instance
     * @returns {void}
     */
    _applyBulkExport() {
        // Проходим по всем отмеченным студентам
        $( '.js-student-cb:checked' ).each( ( _, el ) => {
            // Извлекаем ID персоны из data-атрибута кнопки экспорта в строке таблицы
            const personId = parseInt(
                $( el ).closest( 'tr' ).find( '.js-export-person' ).data( 'personId' ), 10
            );

            // Если ID не найден или не является числом — пропускаем этого студента
            if ( ! personId ) return;

            // Отправляем AJAX-запрос на генерацию файла экспорта
            $.post( AJAX_URL(), {
                action:    ACTIONS().exportPii,
                person_id: personId,
                security:  NONCES().exportPii, // Nonce для защиты от CSRF
            } ).done( ( r ) => {
                // Если сервер вернул URL для скачивания — инициируем скачивание
                if ( r.success && r.data.download_url ) {
                    // Создаем невидимый элемент <a>
                    const a = document.createElement( 'a' );
                    a.href = r.data.download_url;

                    // Добавляем элемент в DOM (некоторые браузеры требуют, 
                    // чтобы элемент был в DOM для программного клика)
                    document.body.appendChild( a );

                    // Программный клик инициирует скачивание файла
                    a.click();

                    // Удаляем элемент из DOM после скачивания
                    document.body.removeChild( a );
                }
            } );
        } );
    },

    /**
     * Обработка следующего студента из очереди multiGroup.
     * Реализует паттерн последовательной обработки через рекурсивный вызов.
     *
     * АЛГОРИТМ:
     * 1. Если очередь multiGroup пуста:
     *    - Проверяем, есть ли отложенные singleGroup
     *    - Если есть — открываем массовую модалку для них
     *    - Выходим
     * 2. Если очередь не пуста:
     *    - Извлекаем первого студента из очереди через shift()
     *    - Устанавливаем колбэк onAfterClose, который вызовет _processNextMultiGroup()
     *      после закрытия модалки (это обеспечит обработку следующего студента)
     *    - Открываем модалку для текущего студента
     *
     * ПАТТЕРН: Асинхронная очередь через колбэки.
     * Вместо того чтобы открывать все модалки одновременно (что невозможно физически),
     * мы открываем одну, ждем её закрытия, затем открываем следующую.
     * Это создает эффект "мастера" (wizard), где пользователь проходит через каждого студента.
     *
     * @private
     * @memberof StudentsTable
     * @instance
     * @returns {void}
     */
    _processNextMultiGroup() {
        // ==========================================
        // ПРОВЕРКА ЗАВЕРШЕНИЯ ОЧЕРЕДИ
        // ==========================================
        // Если очередь multiGroup пуста — проверяем, есть ли отложенные singleGroup
        if ( ! this._multiGroupQueue.length ) {
            // Если есть отложенные студенты с одним зачислением — открываем для них массовую модалку
            if ( this._singleGroupPending.length ) {
                const pending            = this._singleGroupPending;
                this._singleGroupPending = []; // Очищаем, чтобы не обработать повторно
                ExpelModal.openBulk( pending );
            }
            return; // Выходим, обработка завершена
        }

        // ==========================================
        // ОБРАБОТКА СЛЕДУЮЩЕГО СТУДЕНТА
        // ==========================================
        // Извлекаем первого студента из очереди.
        // shift() удаляет первый элемент из массива и возвращает его.
        // Это гарантирует, что каждый студент будет обработан ровно один раз.
        const student = this._multiGroupQueue.shift();

        // Устанавливаем колбэк, который вызовется после закрытия модалки.
        // ExpelModal.setAfterClose() регистрирует функцию, которая выполнится
        // в методе close() модалки после завершения анимации закрытия.
        // 
        // ВАЖНО: мы передаем ссылку на метод _processNextMultiGroup, 
        // но не вызываем его сразу. Модалка вызовет его в нужный момент.
        // Это создает цепочку: модалка закрылась → вызвался колбэк → обработался следующий студент.
        ExpelModal.setAfterClose( () => this._processNextMultiGroup() );

        // Открываем модалку для текущего студента.
        // Передаем ID, имя и список зачислений.
        // Модалка покажет выпадающий список для выбора конкретного зачисления,
        // так как enrollments.length > 1 (иначе студент не попал бы в multiGroup).
        ExpelModal.open( student.id, student.name, student.enrollments );
    },
};