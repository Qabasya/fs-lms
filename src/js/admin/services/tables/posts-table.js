/**
 * @fileoverview Модуль управления таблицей постов (задач) с AJAX-фильтрацией и пагинацией.
 *
 * @module PostsTable
 * @description Обеспечивает динамическую загрузку таблицы постов без перезагрузки страницы:
 *              - Фильтрация по статусам (All/Published/Draft/Trash) через AJAX
 *              - Пагинация без перезагрузки страницы
 *              - Поиск с автоматическим сбросом на первую страницу
 *
 *              Архитектурная особенность: модуль реализует паттерн "Single Page Application" (SPA)
 *              для таблицы WordPress. Вместо стандартных переходов по ссылкам и отправки форм,
 *              все действия перехватываются и выполняются через AJAX. Сервер возвращает готовый HTML
 *              таблицы, который заменяет содержимое контейнера. Это создает ощущение мгновенного отклика.
 *
 *              Ключевые паттерны:
 *              - Делегирование событий через $(document).on() для поддержки динамически обновляемого контента
 *              - URLSearchParams для парсинга параметров из ссылок фильтров и пагинации
 *              - Переинициализация WordPress inlineEditPost после замены HTML (иначе Quick Edit перестанет работать)
 *              - Визуальный индикатор загрузки через изменение opacity контейнера
 *
 * @requires jQuery - глобальная зависимость WordPress
 * @requires ../_types.js - глобальные типы данных
 */

import '../../_types.js';

const $ = jQuery;

/**
 * Объект для управления таблицей постов с AJAX-загрузкой.
 * Реализует SPA-подобное поведение для стандартной таблицы WordPress.
 *
 * ПРИНЦИП РАБОТЫ:
 * 1. Пользователь кликает по ссылке фильтра/пагинации или отправляет форму поиска
 * 2. Модуль перехватывает событие (e.preventDefault())
 * 3. Извлекает параметры из URL ссылки или формы
 * 4. Отправляет AJAX-запрос на сервер с этими параметрами
 * 5. Сервер возвращает готовый HTML таблицы
 * 6. Модуль заменяет содержимое контейнера новым HTML
 * 7. Переинициализирует WordPress inlineEditPost для работы Quick Edit
 *
 * @namespace PostsTable
 */
export const PostsTable = {

    /**
     * Инициализирует модуль таблицы постов.
     * Проверяет наличие контейнера таблицы и навешивает обработчики событий
     * для фильтров, пагинации и поиска.
     *
     * ВАЖНО: Этот метод должен вызываться один раз после загрузки DOM.
     * Повторный вызов приведет к дублированию обработчиков событий.
     *
     * @memberof PostsTable
     * @instance
     * @returns {void}
     *
     * @example
     * // Инициализация после загрузки DOM
     * jQuery(document).ready(() => {
     *     PostsTable.init();
     * });
     */
    init() {
        // Guard clause: проверяем наличие контейнера таблицы на странице.
        // Если контейнер отсутствует (например, скрипт загружен на странице без таблицы),
        // прекращаем инициализацию, чтобы избежать ошибок при навешивании обработчиков.
        // Это стандартный паттерн для модулей, которые загружаются глобально,
        // но используются только на определенных страницах.
        if (!$('.fs-posts-table-container').length) {
            return;
        }

        // ==========================================
        // 1. ОБРАБОТЧИК ФИЛЬТРОВ СТАТУСОВ
        // ==========================================
        // Ссылки фильтрации (All / Published / Draft / Trash) в WordPress имеют класс .subsubsub.
        // Каждая ссылка содержит URL с параметром post_status, например:
        // ?post_status=publish&paged=1
        //
        // ПАТТЕРН: Делегирование событий.
        // Мы навешиваем обработчик на document, а не на сами ссылки.
        // Это критически важно, так как после AJAX-обновления таблицы HTML заменяется целиком,
        // и все ссылки создаются заново. Если бы мы использовали $('.subsubsub a').on('click', ...),
        // обработчики потерялись бы после первого обновления таблицы.
        // Делегирование через $(document).on() гарантирует, что обработчик сработает
        // для любых ссылок .subsubsub a, даже если они были добавлены в DOM после инициализации.
        $(document).on('click', '.fs-posts-table-container .subsubsub a', (e) => {
            e.preventDefault(); // Отменяем стандартный переход по ссылке (иначе страница перезагрузится)
            const $link = $(e.currentTarget);

            // Находим ближайший контейнер таблицы относительно кликнутой ссылки.
            // Это нужно, так как на странице может быть несколько таблиц (например, в разных вкладках),
            // и мы должны обновлять только ту таблицу, внутри которой находится кликнутая ссылка.
            // Передаем href ссылки в метод _load, который извлечет из него параметры.
            this._load($link.closest('.fs-posts-table-container'), $link.attr('href'));
        });

        // ==========================================
        // 2. ОБРАБОТЧИК ПАГИНАЦИИ
        // ==========================================
        // Ссылки пагинации (номера страниц, "вперед", "назад") имеют класс .tablenav-pages a.
        // URL содержит параметр paged, например: ?paged=2&post_status=publish
        // Логика аналогична фильтрам: перехватываем клик, извлекаем параметры, отправляем AJAX.
        $(document).on('click', '.fs-posts-table-container .tablenav-pages a', (e) => {
            e.preventDefault();
            const $link = $(e.currentTarget);
            this._load($link.closest('.fs-posts-table-container'), $link.attr('href'));
        });

        // ==========================================
        // 3. ОБРАБОТЧИК ПОИСКА
        // ==========================================
        // Форма поиска имеет ID #posts-filter (стандартный ID в WordPress).
        // При отправке формы мы:
        // 1. Извлекаем значение поискового запроса из поля [name="s"]
        // 2. Сбрасываем пагинацию на первую страницу (paged: 1)
        // 3. Сбрасываем фильтр статусов (post_status: '')
        //
        // ПОЧЕМУ СБРАСЫВАЕМ:
        // Когда пользователь ищет "математика", он ожидает увидеть ВСЕ результаты,
        // а не только те, что в статусе "Draft". Поэтому сбрасываем фильтр статусов.
        // Аналогично, если пользователь был на 5-й странице и начал новый поиск,
        // логично показать первую страницу результатов, а не пытаться загрузить 5-ю страницу
        // новых результатов (которой может не существовать).
        $(document).on('submit', '.fs-posts-table-container #posts-filter', (e) => {
            e.preventDefault(); // Отменяем стандартную отправку формы (иначе страница перезагрузится)
            const $form = $(e.currentTarget);
            const $container = $form.closest('.fs-posts-table-container');

            // Извлекаем значение поискового запроса.
            // Оператор || '' гарантирует, что если поле пустое, мы передадим пустую строку,
            // а не undefined, что упрощает обработку на сервере.
            const s = $form.find('[name="s"]').val() || '';

            // Выполняем загрузку с переопределением параметров:
            // - s: поисковый запрос из формы
            // - paged: 1 (сброс на первую страницу)
            // - post_status: '' (сброс фильтра статусов)
            // Третий параметр overrides в методе _load имеет приоритет над параметрами из URL.
            this._load($container, null, { s, paged: 1, post_status: '' });
        });
    },

    /**
     * Загружает таблицу постов через AJAX.
     * Приватный метод, вызываемый при фильтрации, пагинации или поиске.
     *
     * АЛГОРИТМ РАБОТЫ:
     * 1. Парсит параметры из URL ссылки (если передана) через URLSearchParams
     * 2. Объединяет параметры из URL с переопределениями (overrides имеют приоритет)
     * 3. Добавляет обязательные параметры: action, security, subject_key, tab, page_slug
     * 4. Показывает индикатор загрузки (уменьшает opacity контейнера)
     * 5. Отправляет AJAX-запрос на сервер
     * 6. При успехе заменяет HTML контейнера ответом сервера
     * 7. Переинициализирует WordPress inlineEditPost для работы Quick Edit
     * 8. Восстанавливает opacity контейнера
     *
     * @memberof PostsTable
     * @instance
     * @private
     *
     * @param {jQuery} $container - jQuery-объект контейнера таблицы (.fs-posts-table-container).
     *                              Контейнер хранит контекст в data-атрибутах: data-subject, data-tab, data-page.
     * @param {string|null} url - URL из ссылки (для фильтров и пагинации), содержащий параметры запроса.
     *                            Если null, параметры берутся только из overrides.
     * @param {Object} [overrides={}] - Объект с параметрами, переопределяющими значения из URL.
     *                                  Используется для сброса пагинации/фильтров при поиске.
     * @param {string} [overrides.post_status] - Статус постов для фильтрации (publish, draft, trash и т.д.).
     * @param {number} [overrides.paged] - Номер страницы пагинации.
     * @param {string} [overrides.s] - Поисковый запрос.
     *
     * @returns {void}
     *
     * @fires jQuery.ajax - AJAX-запрос к серверу для получения HTML таблицы
     */
    _load($container, url, overrides = {}) {
        // ==========================================
        // ПАРСИНГ ПАРАМЕТРОВ ИЗ URL
        // ==========================================
        // URLSearchParams — это встроенный Web API для работы с query string (часть URL после ?).
        // Например, для URL "?post_status=publish&paged=2":
        // - params.get('post_status') вернет 'publish'
        // - params.get('paged') вернет '2'
        // - params.get('s') вернет null (если параметра нет)
        //
        // ПРЕИМУЩЕСТВА URLSearchParams:
        // - Автоматическое декодирование URL-encoded символов (например, %20 → пробел)
        // - Не нужно вручную парсить строку через split('&') и split('=')
        // - Работает с любыми спецсимволами в значениях параметров
        //
        // Если url передан, создаем URLSearchParams на основе query string из этого URL.
        // new URL(url, window.location.href) создает абсолютный URL из относительного,
        // используя текущий URL страницы как базу. Это нужно, так как ссылки могут быть относительными.
        // Если url равен null, создаем пустой URLSearchParams (параметры будут только из overrides).
        const params = url
            ? new URLSearchParams(new URL(url, window.location.href).search)
            : new URLSearchParams();

        // ==========================================
        // ФОРМИРОВАНИЕ ДАННЫХ ДЛЯ ЗАПРОСА
        // ==========================================
        // Объединяем параметры из трех источников в порядке приоритета:
        // 1. overrides (самый высокий приоритет) — используется для сброса пагинации/фильтров
        // 2. params (из URL ссылки) — основные параметры фильтрации/пагинации
        // 3. Значения по умолчанию — если параметр не найден ни в overrides, ни в params
        //
        // ОПЕРАТОР ?? (Nullish Coalescing):
        // Возвращает правое значение, только если левое равно null или undefined.
        // В отличие от ||, оператор ?? НЕ считает 0, '' и false "пустыми" значениями.
        // Например: 0 ?? 1 вернет 0, а 0 || 1 вернет 1.
        // Это важно для параметра paged: если пользователь на первой странице, paged = 1,
        // и мы не хотим, чтобы оператор || заменил его на дефолтное значение.
        const data = {
            // Обязательные параметры WordPress AJAX:
            action: fs_lms_vars.ajax_actions.getPostsTable, // Имя AJAX-действия на сервере
            security: fs_lms_vars.nonces.subject, // Nonce-токен для защиты от CSRF-атак

            // Контекстные параметры из data-атрибутов контейнера:
            // Они определяют, какую именно таблицу загружать (предмет, вкладка, страница).
            subject_key: $container.data('subject'), // Ключ предмета (например, 'mathematics')
            tab: $container.data('tab'), // Вкладка (например, 'tasks')
            page_slug: $container.data('page'), // Слаг страницы (например, 'fs_lms_tasks')

            // Параметры фильтрации/пагинации/поиска:
            // Приоритет: overrides → params → дефолтное значение
            post_status: overrides.post_status ?? (params.get('post_status') || ''),
            paged: overrides.paged ?? (params.get('paged') || 1),
            s: overrides.s ?? (params.get('s') || ''),
        };

        // ==========================================
        // ВИЗУАЛЬНЫЙ ИНДИКАТОР ЗАГРУЗКИ
        // ==========================================
        // Уменьшаем прозрачность контейнера до 0.5, чтобы показать пользователю,
        // что идет загрузка данных. Это ненавязчивый способ индикации, который не блокирует интерфейс.
        // CSS-свойство opacity поддерживается всеми современными браузерами.
        // После завершения запроса (в success и error) мы восстановим opacity к исходному значению.
        $container.css('opacity', '0.5');

        // ==========================================
        // AJAX-ЗАПРОС К СЕРВЕРУ
        // ==========================================
        // Используем $.ajax вместо $.post, так как нам нужна более гибкая настройка:
        // - Явное указание type: 'POST' (хотя $.post делает то же самое)
        // - Возможность добавить дополнительные опции в будущем (например, timeout, dataType)
        // - Более явный синтаксис для сложных запросов
        $.ajax({
            url: fs_lms_vars.ajaxurl, // URL обработчика AJAX WordPress (обычно /wp-admin/admin-ajax.php)
            type: 'POST', // HTTP метод запроса
            data, // Данные для отправки (объект автоматически сериализуется в URL-encoded формат)

            /**
             * Обработчик успешного ответа сервера.
             * Вызывается, когда HTTP-запрос завершился с кодом 2xx (200 OK, 201 Created и т.д.).
             *
             * ВАЖНО: Это не означает, что бизнес-операция успешна!
             * Сервер может вернуть { success: false, data: 'Ошибка' } с HTTP 200.
             * Поэтому мы проверяем response.success внутри обработчика.
             *
             * @param {Object} response - Ответ сервера в формате WordPress AJAX:
             *                            { success: boolean, data: any }
             */
            success(response) {
                // Восстанавливаем прозрачность контейнера.
                // Передаем пустую строку '', что означает "удалить inline-стиль".
                // Это вернет opacity к значению, заданному в CSS (обычно 1).
                $container.css('opacity', '');

                // Проверяем флаг успешности бизнес-операции.
                // WordPress AJAX всегда возвращает объект { success: boolean, data: any }.
                if (response.success) {
                    // ЗАМЕНА HTML КОНТЕЙНЕРА:
                    // Полностью заменяем содержимое контейнера новым HTML от сервера.
                    // Это включает саму таблицу, фильтры, пагинацию и все остальные элементы.
                    // jQuery .html() автоматически удаляет старые элементы и вставляет новые.
                    $container.html(response.data.html);

                    // ==========================================
                    // ПЕРЕИНИЦИАЛИЗАЦИЯ WORDPRESS INLINE EDIT POST
                    // ==========================================
                    // КРИТИЧЕСКИ ВАЖНЫЙ ШАГ!
                    // WordPress имеет встроенный функционал "Quick Edit" (быстрое редактирование),
                    // который позволяет редактировать посты прямо в таблице без открытия полной формы.
                    // Этот функционал реализован через глобальный объект window.inlineEditPost.
                    //
                    // ПРОБЛЕМА:
                    // Когда inlineEditPost инициализируется при загрузке страницы, он навешивает
                    // обработчики событий на элементы таблицы (кнопки "Quick Edit", поля формы и т.д.).
                    // После AJAX-обновления таблицы HTML заменяется целиком, и все эти обработчики
                    // теряются, так как они были привязаны к старым DOM-элементам.
                    //
                    // РЕШЕНИЕ:
                    // Вызываем window.inlineEditPost.init() повторно, чтобы он заново навесил
                    // обработчики на новые элементы таблицы. Без этого Quick Edit перестанет работать
                    // после первого AJAX-обновления таблицы.
                    //
                    // ПРОВЕРКА СУЩЕСТВОВАНИЯ:
                    // Проверяем window.inlineEditPost, так как этот объект может отсутствовать,
                    // если на странице не используется функционал Quick Edit (например, для кастомных
                    // таблиц, где он не нужен).
                    if (window.inlineEditPost) {
                        window.inlineEditPost.init();
                    }
                }
                // Если response.success === false, мы ничего не делаем.
                // Сервер мог вернуть ошибку валидации или другую бизнес-ошибку.
                // В данном модуле мы не показываем уведомления об ошибках, так как это таблица постов,
                // и ошибки обычно означают, что данных нет или параметры некорректны.
                // Пользователь просто увидит пустую таблицу или таблицу с сообщением "Ничего не найдено".
            },

            /**
             * Обработчик ошибки HTTP-запроса.
             * Вызывается, когда HTTP-запрос завершился с кодом 4xx или 5xx
             * (например, 404 Not Found, 500 Internal Server Error) или при сетевых проблемах
             * (потеря интернета, таймаут).
             *
             * ВАЖНО: Этот обработчик НЕ вызывается, если сервер вернул HTTP 200,
             * но в ответе { success: false }. Это бизнес-ошибка, а не HTTP-ошибка.
             */
            error() {
                // Восстанавливаем прозрачность контейнера при ошибке.
                // Это критически важно: если не восстановить opacity, таблица останется полупрозрачной
                // навсегда, что выглядит как баг и блокирует взаимодействие с интерфейсом.
                $container.css('opacity', '');

                // Намеренно не показываем уведомление об ошибке здесь.
                // Причина: сетевые ошибки могут быть временными, и пользователь может повторить действие.
                // Если показать alert или toast при каждой ошибке, это будет раздражать.
                // Вместо этого таблица просто останется в предыдущем состоянии (до попытки загрузки),
                // что является приемлемым fallback.
                //
                // Для критичных сценариев можно добавить логирование через console.error()
                // или отправку ошибки в систему мониторинга (например, Sentry).
            },
        });
    },
};