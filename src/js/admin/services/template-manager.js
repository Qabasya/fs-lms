/**
 * @fileoverview Модуль управления задачами (TemplateManager) для плагина FS-LMS.
 *
 * @module TemplateManager
 * @description Обеспечивает функционал обновления шаблона (boilerplate) для конкретной задачи
 *              через AJAX с визуальной обратной связью через уведомления.
 *
 *              Архитектурная особенность: модуль реализует "inline editing" —
 *              пользователь меняет шаблон прямо в таблице через dropdown,
 *              без открытия модального окна или перехода на отдельную страницу.
 *              Изменение сохраняется автоматически при смене значения (событие 'change').
 *
 *              Принцип работы:
 *              1. Пользователь выбирает новый шаблон в dropdown для конкретной задачи
 *              2. Событие 'change' перехватывается модулем
 *              3. Данные (ID задачи, выбранный шаблон, контекст предмета) отправляются на сервер
 *              4. Сервер обновляет привязку шаблона к задаче
 *              5. Пользователь получает уведомление об успехе/ошибке в контексте таблицы
 *
 * @requires jQuery - глобальная зависимость WordPress
 * @requires ../_types.js - глобальные типы данных
 * @requires ../modules/utils.js - Утилиты (showNotice для отображения уведомлений)
 */

import '../_types.js';
import { showNotice } from '../modules/utils.js';

const $ = jQuery;

/**
 * Объект для управления задачами.
 * Отвечает за обновление привязки шаблонов (boilerplates) к задачам (терминам таксономии).
 *
 * КОНТЕКСТ WORDPRESS:
 * В данном плагине "задача" (task) — это термин (term) кастомной таксономии.
 * Каждый термин может быть привязан к шаблону (boilerplate) — готовому набору контента,
 * который будет использоваться при создании нового поста этого типа.
 * TemplateManager позволяет администратору менять эту привязку прямо из таблицы задач,
 * без необходимости открывать полную форму редактирования термина.
 *
 * @namespace TemplateManager
 * @typedef {Object} TemplateManager
 */
export const TemplateManager = {

    /**
     * Инициализирует модуль управления задачами.
     * Проверяет наличие таблицы задач на странице и запускает инициализацию менеджера шаблонов.
     *
     * ПАТТЕРН: Guard clause с проверкой наличия DOM-элемента.
     * Модуль загружается глобально, но работает только на страницах с таблицей задач.
     * Проверка $('.js-task-manager-table').length предотвращает ненужную инициализацию
     * и навешивание обработчиков на страницах, где таблица отсутствует.
     *
     * @memberof TemplateManager
     * @instance
     * @returns {void}
     *
     * @example
     * // Инициализация после загрузки DOM
     * jQuery(document).ready(() => {
     *     TemplateManager.init();
     * });
     */
    init() {
        // Guard clause: если таблицы задач нет на странице, не инициализируем модуль.
        // Это экономит ресурсы и предотвращает ошибки при поиске несуществующих элементов.
        if ($('.js-task-manager-table').length) {
            this.initTemplateManager();
        }
    },

    /**
     * Инициализирует менеджер шаблонов для таблицы задач.
     * Навешивает обработчик изменения выпадающего списка шаблонов с использованием делегирования событий.
     *
     * ПАТТЕРН: Делегирование событий через таблицу.
     * Обработчик навешивается на контейнер таблицы $('.js-task-manager-table'),
     * а не на сами dropdown-ы. Это критически важно, так как:
     * 1. Таблица может обновляться через AJAX (фильтрация, пагинация),
     *    и новые dropdown-ы будут созданы заново.
     * 2. Делегирование гарантирует, что обработчик сработает для ЛЮБОГО dropdown
     *    с классом .js-change-term-template, даже если он добавлен после инициализации.
     * 3. Это эффективнее, чем навешивание на $(document), так как ограничивает
     *    область проверки событий только таблицей.
     *
     * ПАТТЕРН: Inline editing через событие 'change'.
     * Вместо кнопки "Сохранить" мы используем событие 'change' на <select>.
     * Это означает, что изменение сохраняется АВТОМАТИЧЕСКИ, как только пользователь
     * выбрал новый вариант. Это ускоряет рабочий процесс, но требует надежной
     * обработки ошибок (пользователь может случайно выбрать не тот шаблон).
     *
     * @memberof TemplateManager
     * @instance
     * @returns {void}
     * @listens change.js-task-manager-table .js-change-term-template
     */
    initTemplateManager() {
        // ДЕЛЕГИРОВАНИЕ СОБЫТИЙ:
        // Навешиваем обработчик на контейнер таблицы, но реагируем только на клики
        // внутри элементов с классом .js-change-term-template.
        //
        // ВАЖНО: используем function(), а не стрелочную функцию,
        // так как внутри нам нужен 'this' как ссылка на DOM-элемент <select>,
        // который вызвал событие. Стрелочная функция захватила бы 'this'
        // из внешнего контекста (объект TemplateManager), что сломало бы логику.
        $('.js-task-manager-table').on('change', '.js-change-term-template', function (e) {
            // ОСТАНОВКА РАСПРОСТРАНЕНИЯ СОБЫТИЯ:
            // stopImmediatePropagation() делает две вещи:
            // 1. Предотвращает всплытие события до родительских элементов (как stopPropagation)
            // 2. Предотвращает вызов ДРУГИХ обработчиков этого же события на текущем элементе
            //
            // ЗАЧЕМ ЭТО НУЖНО:
            // На странице могут быть другие модули, которые тоже слушают событие 'change'
            // на dropdown-ах (например, модуль валидации или аналитики).
            // Мы не хотим, чтобы они срабатывали при нашем AJAX-обновлении шаблона,
            // так как это может вызвать нежелательные побочные эффекты
            // (например, повторную отправку данных на сервер или конфликты состояния).
            e.stopImmediatePropagation();

            // ==========================================
            // СБОР КОНТЕКСТА ИЗ DOM
            // ==========================================
            // 'this' внутри обработчика — это DOM-элемент <select>, который вызвал событие.
            // Оборачиваем его в jQuery для удобства работы.
            const $select = $(this);

            // Находим родительские элементы для извлечения контекстных данных.
            // Каждый из этих элементов хранит важную информацию в data-атрибутах:
            // - $row (строка таблицы) — содержит ID задачи (term-id) и её название
            // - $table (таблица) — содержит ключ предмета (subject)
            // - $wrapper (обертка) — контейнер для показа уведомлений в правильном месте
            const $row = $select.closest('tr');
            const $table = $select.closest('.js-task-manager-table');
            const $wrapper = $select.closest('.task-manager-wrapper');

            // ==========================================
            // ФОРМИРОВАНИЕ ДАННЫХ ДЛЯ ЗАПРОСА
            // ==========================================
            // Собираем все необходимые данные из DOM-элементов и data-атрибутов.
            // Это пример паттерна "извлечение контекста из разметки":
            // вместо передачи данных через параметры функции, мы читаем их из HTML.
            const requestData = {
                // Обязательные параметры WordPress AJAX:
                action:   fs_lms_vars.ajax_actions.updateTermTemplate, // Имя серверного action
                security: fs_lms_vars.nonces.subject, // Nonce-токен для защиты от CSRF

                // Контекстные данные из data-атрибутов:
                // term_id — ID термина (задачи), которую обновляем.
                // Берется из data-term-id строки таблицы.
                term_id:  $row.data('term-id'),

                // template — выбранное значение в dropdown (slug или ID шаблона).
                // $select.val() возвращает value выбранного <option>.
                template: $select.val(),

                // key — ключ предмета (например, 'mathematics' или 'physics').
                // Нужен серверу для понимания, в контексте какого предмета работает задача.
                // Берется из data-subject таблицы.
                key:      $table.data('subject'),

                // name — человекочитаемое название задачи.
                // Может использоваться сервером для логирования или отображения в уведомлениях.
                // Берется из data-task-name строки таблицы.
                name:     $row.data('task-name'),
            };

            // ==========================================
            // AJAX-ЗАПРОС К СЕРВЕРУ
            // ==========================================
            // Используем $.ajax вместо $.post для более явного синтаксиса
            // и возможности легкой настройки дополнительных опций в будущем.
            $.ajax({
                url:  fs_lms_vars.ajaxurl, // URL обработчика AJAX WordPress
                type: 'POST', // HTTP метод
                data: requestData, // Данные запроса (автоматически сериализуются)

                /**
                 * Обработчик успешного HTTP-ответа (2xx).
                 * ВАЖНО: это не означает, что бизнес-операция успешна!
                 * Проверяем response.success для различения бизнес-успеха и бизнес-ошибки.
                 *
                 * @param {UpdateTermTemplateResponse} response - Ответ сервера в формате WordPress AJAX
                 */
                success(response) {
                    if (response.success) {
                        // БИЗНЕС-УСПЕХ: показываем уведомление об успехе.
                        //
                        // КОНТЕКСТНЫЙ КОНТЕЙНЕР ($wrapper):
                        // Уведомление вставляется не в глобальный контейнер страницы,
                        // а в $wrapper — ближайшую обертку таблицы задач.
                        // Это улучшает UX: пользователь видит уведомление рядом с местом действия,
                        // а не где-то вверху страницы. Особенно важно, если на странице
                        // несколько таблиц или секций.
                        showNotice('Шаблон задания успешно сохранён!', 'success', $wrapper);
                    } else {
                        // БИЗНЕС-ОШИБКА: сервер вернул { success: false, data: '...' }.
                        // Извлекаем сообщение об ошибке с fallback на дефолтное.
                        // Оператор || реализует цепочку fallback: если response.data пустое,
                        // используем 'Неизвестная ошибка'.
                        const msg = response.data || 'Неизвестная ошибка';
                        showNotice(msg, 'error', $wrapper);

                        // ПРИМЕЧАНИЕ: мы НЕ откатываем значение dropdown к предыдущему.
                        // Это сознательное решение: пользователь видит ошибку и может
                        // выбрать другой шаблон вручную. Автоматический откат мог бы
                        // дезориентировать пользователя ("почему значение изменилось само?").
                        //
                        // Альтернативный подход: сохранять предыдущее значение в data-атрибуте
                        // перед изменением и восстанавливать его при ошибке.
                        // Но это усложнило бы код без существенного выигрыша в UX.
                    }
                },

                /**
                 * Обработчик HTTP-ошибки (4xx, 5xx) или сетевой проблемы.
                 * Вызывается, когда запрос не дошел до сервера или сервер вернул ошибку.
                 */
                error() {
                    // Показываем общее сообщение о сетевой ошибке.
                    // Намеренно не показываем технические детали (статус, текст ошибки),
                    // так как они непонятны конечному пользователю.
                    // Упоминание консоли помогает разработчикам или продвинутым пользователям.
                    showNotice('Системная ошибка AJAX. Проверьте консоль.', 'error', $wrapper);
                },
            });
        });
    }
};