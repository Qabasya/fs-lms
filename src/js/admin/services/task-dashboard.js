/**
 * @fileoverview Модуль фильтрации и сортировки таблицы заданий.
 *
 * @module TaskFilter
 * @description Обеспечивает клиентское взаимодействие с таблицей заданий на странице предмета:
 *              - Фильтрация заданий по "номеру" (типу задания / term) через AJAX-загрузку
 *              - Клиентская сортировка таблицы по клику на заголовки колонок (th.sortable)
 *
 *              Архитектурная особенность:
 *              - Фильтрация работает через AJAX, так как список заданий для конкретного типа
 *                может быть большим и подгружается динамически.
 *              - Сортировка работает полностью на клиенте (без AJAX), так как сортируются
 *                только уже загруженные в DOM строки текущей выборки.
 *                Используется data-атрибут data-val для корректной сортировки
 *                (чтобы сортировать по числам или исходным строкам, а не по отформатированному тексту).
 *
 * @requires jQuery
 * @requires ../_types.js - глобальные типы данных
 */

import '../_types.js';

const $ = jQuery;

/**
 * Модуль управления фильтрацией и сортировкой таблицы заданий.
 *
 * @namespace TaskFilter
 */
export const TaskFilter = {

    /**
     * Инициализация модуля.
     * Запускает инициализацию фильтра по номеру и клиентской сортировки таблицы.
     *
     * @memberof TaskFilter
     * @instance
     * @returns {void}
     */
    init() {
        this._initNumberFilter();
        this._initTableSort();
    },

    /**
     * Инициализация фильтрации таблицы заданий по "номеру" (типу задания).
     * При изменении значения в выпадающем списке (#fs-task-number-filter)
     * отправляет AJAX-запрос на сервер для получения отфильтрованных заданий
     * и заменяет содержимое контейнера таблицы.
     *
     * @private
     * @memberof TaskFilter
     * @instance
     * @returns {void}
     */
    _initNumberFilter() {
        // Проверяем наличие обертки дашборда заданий.
        // Если её нет на странице, модулю нечего делать, выходим.
        const $wrapper = $('.task-dashboard-wrapper');
        if (!$wrapper.length) return;

        // Контейнер, в который будет загружаться HTML таблицы заданий
        const $container = $('#fs-task-table-container');

        // Используем делегирование событий через $wrapper, 
        // так как сам select может перерисовываться или находиться внутри динамического контента.
        $wrapper.on('change', '#fs-task-number-filter', (e) => {
            const termId = $(e.currentTarget).val();

            // Если выбран пустой вариант (например, "-- Выберите номер --"),
            // просто очищаем контейнер с таблицей и выходим.
            if (!termId) {
                $container.empty();
                return;
            }

            // Показываем индикатор загрузки внутри контейнера
            $container.html('<p>Загрузка заданий...</p>');

            // Отправляем AJAX-запрос на сервер
            $.post(
                fs_lms_vars.ajaxurl,
                {
                    action:      fs_lms_vars.ajax_actions.getTasksByNumber,
                    security:    fs_lms_vars.nonces.subject, // Nonce для безопасности
                    subject_key: fs_lms_task_data.subject_key, // Ключ текущего предмета
                    term_id:     termId, // ID выбранного типа задания (term)
                },
                (response) => {
                    // Обработчик успешного HTTP-ответа
                    if (response.success) {
                        // Сервер возвращает готовый HTML таблицы, просто вставляем его
                        $container.html(response.data.html);
                    } else {
                        // Если сервер вернул business-ошибку (success: false)
                        $container.html('<p>Ошибка загрузки.</p>');
                    }
                }
            );
        });
    },

    /**
     * Инициализация клиентской сортировки таблицы заданий.
     * Навешивает обработчик клика на заголовки колонок с классом .sortable.
     * Сортировка происходит полностью на стороне клиента (без AJAX-запросов)
     * путем перестановки DOM-элементов строк (<tr>) внутри <tbody>.
     *
     * ПАТТЕРН: Сортировка по data-атрибуту.
     * Для корректной сортировки (особенно чисел и дат) используется не видимый текст ячейки,
     * а скрытый data-атрибут data-val, который содержит "сырое" значение для сравнения.
     * Это позволяет избежать проблем, когда в ячейке отображается "100 руб.",
     * а сортировать нужно по числу 100.
     *
     * @private
     * @memberof TaskFilter
     * @instance
     * @returns {void}
     */
    _initTableSort() {
        // Используем делегирование событий на контейнере таблицы,
        // так как сама таблица может перерисовываться после AJAX-фильтрации (в _initNumberFilter).
        // Если навесить обработчик напрямую на th, он потеряется после первой же AJAX-загрузки.
        $('#fs-task-table-container').on('click', 'th.sortable', (e) => {
            const $th    = $(e.currentTarget); // Заголовок колонки, по которому кликнули
            const $table = $th.closest('table'); // Ближайшая таблица
            const col    = $th.index(); // Индекс колонки (0-based) для сортировки
            const isAsc  = $th.hasClass('sort-asc'); // Текущее направление: true, если уже сортировано по возрастанию

            // Сбрасываем классы сортировки у всех заголовков текущей таблицы
            $table.find('thead th').removeClass('sort-asc sort-desc');

            // Переключаем направление сортировки:
            // Если было по возрастанию (sort-asc), ставим по убыванию (sort-desc), и наоборот.
            $th.addClass(isAsc ? 'sort-desc' : 'sort-asc');

            const $tbody = $table.find('tbody');

            // Получаем массив строк таблицы для сортировки.
            // ВАЖНО: фильтруем строки, чтобы исключить строки-разделители или строки с colspan 
            // (например, если в таблице есть строки-заголовки групп или "итого" внизу).
            // У таких строк есть ячейки с атрибутом colspan, мы их исключаем из сортировки.
            const $rows  = $tbody.find('tr').filter((_, tr) => !$(tr).find('[colspan]').length).toArray();

            // Сортировка массива DOM-элементов с помощью встроенного Array.prototype.sort()
            $rows.sort((a, b) => {
                // Извлекаем значения для сравнения из data-атрибута data-val ячейки нужной колонки.
                // Оператор ?? подставляет пустую строку, если data-val отсутствует.
                const aVal = $(a).find('td').eq(col).data('val') ?? '';
                const bVal = $(b).find('td').eq(col).data('val') ?? '';

                // ПАТТЕРН: Пустые значения всегда в конец списка.
                // Независимо от направления сортировки (возрастание/убывание), 
                // ячейки без данных должны оставаться внизу таблицы.
                if (!aVal && bVal) return 1;  // a пусто, b нет -> a идет после b
                if (aVal && !bVal) return -1; // a не пусто, b пусто -> a идет перед b
                if (!aVal && !bVal) return 0; // оба пусты -> порядок не важен

                // Сравниваем значения с помощью встроенного метода String.prototype.localeCompare().
                // ПАРАМЕТРЫ localeCompare:
                // - 'ru' — используем русскую локаль для корректного сравнения кириллицы
                // - { numeric: true } — включаем числовой режим. 
                //   Это позволяет корректно сортировать строки с числами: "2" будет перед "10", 
                //   а не после (как было бы при обычной лексикографической сортировке).
                // 
                // Если текущая сортировка была по возрастанию (isAsc === true), 
                // мы меняем местами a и b при сравнении, чтобы получить убывание.
                return isAsc
                    ? String(bVal).localeCompare(String(aVal), 'ru', { numeric: true })
                    : String(aVal).localeCompare(String(bVal), 'ru', { numeric: true });
            });

            // Вставляем отсортированные строки обратно в tbody.
            // jQuery .append() с массивом элементов умно перемещает их в DOM, 
            // а не дублирует (если элемент уже в DOM, он просто меняет позицию).
            $tbody.append($rows);
        });
    },
};