/**
 * @fileoverview Сервис для безвозвратного (hard) удаления студента из системы.
 *
 * @module HardDeleteStudentService
 * @description Менеджер для операции полного удаления студента.
 *              Отвечает за:
 *              - Двухэтапное подтверждение критически важного действия через ConfirmModal
 *              - Блокировку кнопки на время выполнения AJAX-запроса
 *              - Удаление строки из таблицы с плавной анимацией (если действие из списка)
 *              - Перезагрузку страницы (если действие со страницы деталей студента)
 *              - Отображение ошибок в контекстном контейнере
 *
 *              Архитектурное отличие от других менеджеров:
 *              Это именно "сервис", а не "менеджер модалки". Он не управляет UI-компонентом,
 *              а выполняет единственную бизнес-операцию — полное удаление записи.
 *              Термин "hard delete" подчеркивает, что данные удаляются из БД физически,
 *              в отличие от "soft delete" (перемещение в корзину/архив), которое применяется
 *              в других модулях (например, ApplicationsTable).
 *
 * @requires jQuery
 * @requires ConfirmModal - модальное окно подтверждения действий
 * @requires toggleButton, apiError, showNotice - утилиты для UX и обработки ошибок
 */

import '../_types.js';
import { ConfirmModal } from '../modals/confirm-modal.js';
import { toggleButton, apiError, showNotice } from '../modules/utils.js';

const $ = jQuery;

/**
 * Сервис безвозвратного удаления студента.
 * Выполняет физическое удаление записи студента из базы данных вместе со всеми связанными данными.
 */
export const HardDeleteStudentService = {

    /**
     * Инициализация сервиса.
     * Точка входа, вызывается при загрузке страницы.
     * Инициализирует ConfirmModal и навешивает обработчик на кнопки удаления.
     */
    init() {
        // Инициализируем ConfirmModal, так как он используется для подтверждения удаления.
        // Если не вызвать init(), модалка не будет найдена в DOM и метод confirm() не сработает.
        ConfirmModal.init();

        // ДЕЛЕГИРОВАНИЕ СОБЫТИЙ:
        // Навешиваем обработчик на document, так как кнопки удаления могут быть:
        // 1. В таблице списка студентов (могут обновляться через AJAX-фильтры)
        // 2. На странице деталей студента (могут быть в разных контекстах)
        // Прямая привязка $('.js-hard-delete-student').on(...) не сработала бы для динамически добавленных кнопок.
        $(document).on('click', '.js-hard-delete-student', (e) => this._handleDelete(e));
    },

    /**
     * Обработчик клика по кнопке безвозвратного удаления.
     * Показывает модальное окно подтверждения с предупреждением о необратимости действия.
     *
     * ПАТТЕРН: Двухэтапное подтверждение для деструктивных действий.
     * Вместо немедленного выполнения удаления, мы сначала показываем пользователю
     * модальное окно с явным предупреждением. Это предотвращает случайные удаления,
     * которые могут произойти из-за быстрого клика или неправильного позиционирования курсора.
     *
     * @private
     * @param {jQuery.Event} e - Событие клика.
     */
    _handleDelete(e) {
        e.preventDefault();
        const $btn     = $(e.currentTarget);
        const personId = $btn.data('person-id'); // ID студента из data-атрибута кнопки
        const $row     = $btn.closest('tr');     // Родительская строка таблицы (если есть)

        // Показываем модальное окно подтверждения.
        // КЛЮЧЕВЫЕ ПАРАМЕТРЫ:
        // - isDanger: true — делает кнопку подтверждения красной (визуальный сигнал об опасности)
        // - size: 'sm' — небольшой размер модалки, так как вопрос простой
        // - message содержит явное предупреждение о необратимости — это критически важно для UX
        ConfirmModal.confirm({
            title:       'Безвозвратное удаление',
            message:     'Ученик и все связанные данные (зачисления, документы, учётная запись) будут удалены безвозвратно. Это действие нельзя отменить.',
            confirmText: 'Удалить',
            cancelText:  'Отмена',
            size:        'sm',
            isDanger:    true,
        })
            // Если пользователь подтвердил — выполняем удаление
            .then(() => this._doDelete(personId, $btn, $row))
            // Если пользователь отменил — ничего не делаем.
            // Пустой catch намеренно подавляет ошибку отмены, так как это штатная ситуация,
            // а не ошибка, требующая уведомления пользователя.
            .catch(() => {});
    },

    /**
     * Выполнение AJAX-запроса на безвозвратное удаление студента.
     * Обрабатывает два сценария UI:
     * 1. Удаление из таблицы списка — строка плавно исчезает через fadeOut
     * 2. Удаление со страницы деталей — страница перезагружается
     *
     * @private
     * @param {string|number} personId - ID студента для удаления.
     * @param {jQuery} $btn - jQuery-объект кнопки удаления (для блокировки на время запроса).
     * @param {jQuery} $row - jQuery-объект строки таблицы (может быть пустым, если действие со страницы деталей).
     */
    _doDelete(personId, $btn, $row) {
        // Блокируем кнопку и меняем текст на индикатор загрузки.
        // Это защищает от двойного клика во время AJAX-запроса.
        // toggleButton сохраняет оригинальный текст в data-атрибут и восстанавливает его при разблокировке.
        toggleButton($btn, true, '...');

        $.post(fs_lms_vars.ajaxurl, {
            action:    fs_lms_vars.ajax_actions.hardDeleteStudent,
            // ВАЖНО: используется отдельный nonce для операции hard delete.
            // Это принцип минимальных привилегий: даже если токен для обычной операции скомпрометирован,
            // злоумышленник не сможет выполнить критическое удаление.
            // Каждый опасный action должен иметь свой уникальный nonce.
            security:  fs_lms_vars.nonces.hardDeleteStudent,
            person_id: personId,
        })
            .done((res) => {
                if (res.success) {
                    // ДВА СЦЕНАРИЯ UI:

                    // Сценарий 1: Удаление из таблицы списка.
                    // Проверяем $row.length — если строка существует в DOM, значит мы в таблице.
                    // Плавно скрываем строку через fadeOut (400мс) и удаляем из DOM после завершения анимации.
                    // Это дает пользователю визуальную обратную связь о выполненном действии.
                    if ($row.length) {
                        $row.fadeOut(400, () => $row.remove());
                    }
                        // Сценарий 2: Удаление со страницы деталей студента.
                        // Строки таблицы нет (мы на странице профиля), поэтому перезагружаем страницу целиком,
                    // чтобы показать сообщение "Студент не найден" или перенаправить на список.
                    else {
                        location.reload();
                    }
                } else {
                    // ОШИБКА СЕРВЕРА: разблокируем кнопку и показываем уведомление.
                    toggleButton($btn, false);

                    // КОНТЕКСТНЫЙ КОНТЕЙНЕР ДЛЯ УВЕДОМЛЕНИЯ:
                    // Используем селектор с запятой '.wrap, .fs-person-detail', чтобы найти ближайший
                    // подходящий контейнер для вставки уведомления.
                    // - .wrap — стандартный контейнер WordPress admin для списков
                    // - .fs-person-detail — контейнер страницы деталей студента
                    // jQuery .closest() с таким селектором найдет первый подходящий родительский элемент.
                    // Это позволяет показывать уведомление в правильном месте независимо от контекста.
                    showNotice(res.data?.message || 'Ошибка удаления.', 'error', $btn.closest('.wrap, .fs-person-detail'));
                }
            })
            .fail(() => {
                // СЕТЕВАЯ ОШИБКА: потеря интернета, таймаут, ошибка 500 и т.д.
                // Разблокируем кнопку и логируем ошибку через централизованный обработчик.
                toggleButton($btn, false);

                // apiError логирует ошибку в консоль для разработчиков и может показать toast-уведомление.
                // Это предпочтительнее прямого вызова console.error(), так как обеспечивает единообразие
                // обработки ошибок во всем приложении.
                apiError('Failed to hard delete student');
            });
    },
};