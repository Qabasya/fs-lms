/**
 * @fileoverview Модуль-страж (Guard) обязательных таксономий при публикации поста.
 *
 * @module RequiredTaxGuard
 * @description Перехватывает нажатие кнопки "Опубликовать" в редакторе поста WordPress
 *              и проверяет, заполнены ли все таксономии, помеченные как обязательные.
 *              Если есть незаполненные таксономии — отменяет публикацию и показывает
 *              уведомление с перечислением недостающих полей.
 *
 *              КОНТЕКСТ WORDPRESS:
 *              В WordPress таксономии — это способы группировки постов (категории, метки,
 *              а также кастомные таксономии, созданные плагином). Стандартная проверка
 *              WordPress требует заполнения только стандартных полей (заголовок, контент).
 *              Кастомные таксономии не валидируются нативно, поэтому этот модуль добавляет
 *              клиентскую валидацию для тех таксономий, которые администратор пометил
 *              как обязательные на странице настроек предмета.
 *
 *              АРХИТЕКТУРНЫЙ ПАТТЕРН "GUARD" (СТРАЖ):
 *              Вместо того чтобы модифицировать стандартную логику публикации WordPress
 *              (что было бы хрупко и могло сломаться при обновлениях ядра), мы "охраняем"
 *              кнопку публикации: перехватываем клик, проверяем условия, и если они не выполнены —
 *              отменяем действие через e.preventDefault(). Это неинвазивный подход,
 *              который работает поверх стандартного поведения WordPress.
 *
 * @requires jQuery - глобальная зависимость WordPress
 * @requires ../_types.js - глобальные типы данных
 * @requires showNotice - утилита для отображения уведомлений в стиле WordPress
 */

import '../_types.js';
import { showNotice } from '../modules/utils.js';

const $ = jQuery;

/**
 * Модуль-страж обязательных таксономий.
 * Перехватывает публикацию поста и проверяет заполненность обязательных таксономий.
 *
 * ПРИНЦИП РАБОТЫ:
 * 1. При инициализации считывает список обязательных таксономий из глобальной переменной
 * 2. Нормализует данные (поддержка разных форматов: строка, объект с slug/name/label)
 * 3. Перехватывает клик по кнопке "Опубликовать" (#publish)
 * 4. Проверяет каждую обязательную таксономию на наличие значения
 * 5. Если есть незаполненные — отменяет публикацию и показывает уведомление
 * 6. Если все заполнены — позволяет стандартной логике WordPress продолжить работу
 *
 * @namespace RequiredTaxGuard
 */
export const RequiredTaxGuard = {

    /**
     * Инициализация модуля-стража.
     * Считывает список обязательных таксономий и навешивает обработчик на кнопку публикации.
     *
     * @memberof RequiredTaxGuard
     * @instance
     * @returns {void}
     */
    init() {
        // ==========================================
        // 1. ПОЛУЧЕНИЕ СПИСКА ОБЯЗАТЕЛЬНЫХ ТАКСОНОМИЙ
        // ==========================================
        // Считываем список из глобальной переменной fs_lms_task_data, 
        // которая передается из PHP через wp_localize_script.
        // 
        // БЕЗОПАСНАЯ ПРОВЕРКА СУЩЕСТВОВАНИЯ:
        // typeof fs_lms_task_data !== 'undefined' — стандартный способ проверки 
        // существования глобальной переменной без риска ReferenceError.
        // Если переменная не определена, используем пустой массив как fallback.
        const rawRequired = typeof fs_lms_task_data !== 'undefined'
            ? (fs_lms_task_data.required_taxonomies || [])
            : [];

        // ==========================================
        // 2. УТИЛИТА ФОРМАТИРОВАНИЯ SLUG В ЧИТАЕМОЕ ИМЯ
        // ==========================================
        // Стрелочная функция для преобразования slug таксономии в человекочитаемое название.
        // 
        // АЛГОРИТМ:
        // 1. replace(/[-_]/g, ' ') — заменяет все дефисы и подчеркивания на пробелы
        //    Пример: 'task_type' → 'task type', 'lesson-category' → 'lesson category'
        // 2. replace(/\b\w/g, c => c.toUpperCase()) — капитализирует первую букву каждого слова
        //    \b — граница слова, \w — любой буквенно-цифровой символ
        //    Пример: 'task type' → 'Task Type', 'lesson category' → 'Lesson Category'
        // 
        // ЗАЧЕМ ЭТО НУЖНО:
        // Если таксономия не имеет явного имени (name или label), мы используем slug 
        // как fallback. Но slug в формате 'task_type' выглядит некрасиво в уведомлениях.
        // Эта функция превращает его в 'Task Type', что понятнее пользователю.
        const formatSlug = (str) => str.replace(/[-_]/g, ' ').replace(/\b\w/g, c => c.toUpperCase());

        // ==========================================
        // 3. НОРМАЛИЗАЦИЯ ДАННЫХ ТАКСОНОМИЙ
        // ==========================================
        // Преобразуем "сырой" массив в унифицированный формат { slug, name }.
        // 
        // ПОЧЕМУ НОРМАЛИЗАЦИЯ НУЖНА:
        // Сервер может возвращать обязательные таксономии в разных форматах:
        // - Просто строка slug: 'task_type'
        // - Объект с полными данными: { slug: 'task_type', name: 'Тип задачи' }
        // - Объект с альтернативным полем: { slug: 'task_type', label: 'Тип задачи' }
        // 
        // Чтобы не писать условия в каждом месте использования, мы один раз 
        // приводим все данные к единому формату. Это принцип "Normalize early, use late".
        const required = rawRequired.map(t => {
            // Проверяем, является ли элемент объектом (а не строкой)
            const isObj = typeof t === 'object' && t !== null;

            // Извлекаем slug: если это объект — берем t.slug, иначе сам элемент является slug
            const slug = isObj ? t.slug : t;

            // Извлекаем читаемое имя с приоритетом полей:
            // 1. t.name — основное имя (если есть)
            // 2. t.label — альтернативное имя (если name нет)
            // 3. formatSlug(slug) — генерируем имя из slug (если ни name, ни label нет)
            // 
            // Оператор || реализует цепочку fallback-значений: 
            // первое truthy-значение будет использовано.
            const name = isObj ? (t.name || t.label || formatSlug(slug)) : formatSlug(slug);

            return { slug, name };
        });

        // ==========================================
        // 4. GUARD CLAUSES (РАННИЙ ВЫХОД)
        // ==========================================
        // Если список обязательных таксономий пуст — модулю нечего делать, выходим.
        // Если кнопки публикации нет на странице (например, пользователь не имеет прав 
        // на публикацию или находится в режиме автосохранения) — тоже выходим.
        // 
        // ПАТТЕРН: Ранний выход (Early Return / Guard Clause).
        // Вместо вложенных if-else, мы сразу выходим из функции при невыполнении условий.
        // Это делает код плоским и читаемым.
        if (!required.length || !$('#publish').length) return;

        // ==========================================
        // 5. ПЕРЕХВАТ КНОПКИ ПУБЛИКАЦИИ
        // ==========================================
        // Навешиваем обработчик клика на кнопку "Опубликовать" (#publish).
        // 
        // НЕЙМСПЕЙСИНГ СОБЫТИЙ ('.fs-required'):
        // Мы добавляем суффикс '.fs-required' к имени события 'click'.
        // Это позволяет:
        // 1. Точечно удалить именно этот обработчик через $('#publish').off('click.fs-required'),
        //    не затрагивая другие обработчики клика на этой кнопке.
        // 2. Избежать конфликтов, если другие плагины или модули тоже навешивают обработчики 
        //    на кнопку публикации.
        // 3. Легче отлаживать — в DevTools видно, какие обработчики от какого модуля.
        $('#publish').on('click.fs-required', (e) => {
            // ==========================================
            // 6. ПРОВЕРКА ЗАПОЛНЕННОСТИ ТАКСОНОМИЙ
            // ==========================================
            // Фильтруем массив required, оставляя только те таксономии, 
            // которые НЕ имеют значения (не заполнены пользователем).
            // 
            // this._hasValue(t.slug) возвращает true, если таксономия заполнена, 
            // и false, если пуста. Оператор ! инвертирует значение.
            // 
            // ВАЖНО: используем стрелочную функцию, чтобы 'this' внутри filter 
            // ссылался на объект RequiredTaxGuard, а не на DOM-элемент.
            const missing = required.filter(t => !this._hasValue(t.slug));

            // Если все обязательные таксономии заполнены — выходим без preventDefault().
            // Это позволяет стандартной логике WordPress продолжить выполнение 
            // (отправить форму публикации на сервер).
            if (!missing.length) return;

            // ==========================================
            // 7. ОТМЕНА ПУБЛИКАЦИИ
            // ==========================================
            // Если есть незаполненные таксономии — отменяем стандартное действие браузера.
            // e.preventDefault() предотвращает отправку формы публикации.
            // Пользователь остается на странице и видит уведомление об ошибке.
            e.preventDefault();

            // ==========================================
            // 8. ВОССТАНОВЛЕНИЕ СОСТОЯНИЯ КНОПКИ
            // ==========================================
            // WordPress при клике на "Опубликовать" автоматически:
            // - Добавляет класс 'disabled' к кнопке (блокирует повторные клики)
            // - Показывает спиннер (класс 'is-active' на элементе .spinner)
            // 
            // Поскольку мы отменили публикацию, нужно вернуть кнопку в исходное состояние,
            // иначе пользователь увидит заблокированную кнопку с крутящимся спиннером 
            // и подумает, что что-то зависло.
            $('#publish').removeClass('disabled');
            $('.spinner').removeClass('is-active');

            // ==========================================
            // 9. ПОКАЗ УВЕДОМЛЕНИЯ ОБ ОШИБКЕ
            // ==========================================
            // Находим контейнер .wrap — стандартный контейнер WordPress admin для контента страницы.
            // Уведомление будет вставлено в начало этого контейнера.
            const $container = $('.wrap');

            // Формируем строку с именами недостающих таксономий через запятую.
            // Array.prototype.map() извлекает имена, join(', ') объединяет их в строку.
            // Пример: ['Тип задачи', 'Сложность'] → 'Тип задачи, Сложность'
            const missingNames = missing.map(t => t.name).join(', ');

            // Показываем уведомление в стиле WordPress Admin Notice.
            // showNotice — утилита из modules/utils.js, которая создает стандартное 
            // уведомление WordPress с классами notice notice-error.
            showNotice(
                `Заполните все обязательные таксономии: ${missingNames}`,
                'error',
                $container
            );

            // ==========================================
            // 10. АВТОПРОКРУТКА К ВЕРХУ СТРАНИЦЫ
            // ==========================================
            // Уведомление вставляется в начало контейнера .wrap, но пользователь может 
            // находиться внизу страницы (где форма публикации). Чтобы он увидел уведомление,
            // плавно прокручиваем страницу к верху.
            // 
            // $('html, body') — выбираем оба элемента, так как разные браузеры 
            // используют разные элементы для прокрутки (html для Firefox, body для Chrome).
            // .animate({ scrollTop: 0 }, 'fast') — плавная анимация прокрутки.
            $('html, body').animate({ scrollTop: 0 }, 'fast');
        });
    },

    /**
     * Проверяет, имеет ли таксономия заполненное значение.
     * Приватный метод, вызываемый для каждой обязательной таксономии.
     *
     * СПЕЦИФИКА WORDPRESS:
     * Таксономии в WordPress могут отображаться в двух видах:
     * 1. Выпадающий список (select) — для иерархических таксономий (как категории)
     *    HTML: <select name="tax_input[task_type][]">...</select>
     * 2. Чекбоксы (input[type="checkbox"]) — для неиерархических таксономий (как метки)
     *    HTML: <input type="checkbox" name="tax_input[task_type][]" value="123">
     *
     * В обоих случаях имя поля имеет формат 'tax_input[slug][]', где:
     * - tax_input — стандартный префикс WordPress для полей таксономий
     * - slug — слаг таксономии (например, 'task_type')
     * - [] — квадратные скобки указывают, что значение может быть массивом
     *        (пользователь может выбрать несколько категорий)
     *
     * @memberof RequiredTaxGuard
     * @instance
     * @private
     *
     * @param {string} slug - Слаг таксономии для проверки (например, 'task_type').
     * @returns {boolean} true, если таксономия имеет хотя бы одно выбранное значение;
     *                    false, если все поля таксономии пусты.
     */
    _hasValue(slug) {
        // ==========================================
        // ПРОВЕРКА SELECT (ВЫПАДАЮЩИЙ СПИСОК)
        // ==========================================
        // Ищем select-элемент с именем 'tax_input[slug][]'.
        // В шаблонной строке используем экранирование: [ и ] — специальные символы в CSS-селекторах,
        // поэтому их нужно экранировать обратным слэшем: \[ и \].
        const $select = $(`select[name="tax_input[${slug}][]"]`);

        if ($select.length) {
            // Если select найден, проверяем его значение.
            // $select.val() возвращает:
            // - null или '' (пустая строка), если ничего не выбрано
            // - строку или массив строк, если есть выбор
            // 
            // Оператор !! (двойное отрицание) преобразует значение в boolean:
            // - !!null → false
            // - !!'' → false
            // - !!'123' → true
            // - !!['123', '456'] → true
            return !!$select.val();
        }

        // ==========================================
        // ПРОВЕРКА CHECKBOX (ЧЕКБОКСЫ)
        // ==========================================
        // Если select не найден, предполагаем, что таксономия отображается как чекбоксы.
        // Ищем все чекбоксы с именем 'tax_input[slug][]', у которых есть атрибут checked.
        // 
        // ДОПОЛНИТЕЛЬНАЯ ФИЛЬТРАЦИЯ:
        // .filter((_, el) => !!el.value) — оставляем только те чекбоксы, у которых 
        // есть непустое значение. Это защита от edge case, когда чекбокс отмечен, 
        // но его value пустое (что теоретически возможно при некорректной HTML-разметке).
        // 
        // _.length > 0 — если найден хотя бы один отмеченный чекбокс с значением, 
        // таксономия считается заполненной.
        return $(`input[name="tax_input[${slug}][]"]:checked`)
            .filter((_, el) => !!el.value)
            .length > 0;
    },
};