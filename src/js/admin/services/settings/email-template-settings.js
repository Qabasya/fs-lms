/**
 * @fileoverview Модуль управления настройками email-шаблонов для плагина FS-LMS.
 *
 * @module EmailTemplateSettings
 * @description Менеджер для страницы настроек email-шаблонов.
 *              Отвечает за:
 *              - Сохранение пользовательских шаблонов email-уведомлений
 *              - Сброс шаблонов к значениям по умолчанию
 *              - Визуальную индикацию статуса шаблона (переопределён / по умолчанию)
 *              - Отображение контекстных уведомлений внутри каждой карточки шаблона
 *
 *              Архитектурная особенность: страница содержит несколько независимых карточек
 *              шаблонов, каждая со своими полями и кнопками. Модуль использует паттерн
 *              "scope isolation" — все операции выполняются в контексте конкретной карточки ($card),
 *              что предотвращает конфликты между параллельными действиями пользователя.
 *
 * @requires jQuery
 */

import '../../_types.js';

const $ = jQuery;

/**
 * Менеджер настроек email-шаблонов.
 * Управляет UI-взаимодействиями на странице настроек email-уведомлений.
 */
export const EmailTemplateSettings = {

	/**
	 * Инициализация модуля.
	 * Проверяет наличие целевого контейнера перед навешиванием событий.
	 */
	init() {
		// Guard clause: если контейнера с шаблонами нет на текущей странице,
		// не тратим ресурсы на инициализацию. Это стандартный паттерн для модулей,
		// которые загружаются глобально, но используются только на определенных страницах.
		if ( ! $( '#js-email-templates' ).length ) {
			return;
		}
		this.bindEvents();
	},

	/**
	 * Привязка обработчиков событий.
	 * Использует делегирование событий для элементов внутри контейнера шаблонов.
	 */
	bindEvents() {
		// ДЕЛЕГИРОВАНИЕ СОБЫТИЙ:
		// Навешиваем обработчики на контейнер #js-email-templates, а не на document.
		// Это более эффективно, чем делегирование через $('body') или $(document),
		// так как браузеру нужно проверять меньше элементов при всплытии события.
		// Также это ограничивает область действия обработчиков только нашей страницей.

		// Обработчик сохранения шаблона
		$( '#js-email-templates' ).on( 'click', '.js-save-email-template', ( e ) => {
			// ПАТТЕРН: Изоляция контекста через closest().
			// Находим ближайшую карточку шаблона относительно нажатой кнопки.
			// Это позволяет работать только с элементами ВНУТРИ этой конкретной карточки,
			// не затрагивая другие карточки на странице.
			// Такой подход критически важен, когда на странице несколько одинаковых блоков.
			this._handleSave( $( e.currentTarget ).closest( '.fs-email-template-card' ) );
		} );

		// Обработчик сброса шаблона к значениям по умолчанию
		$( '#js-email-templates' ).on( 'click', '.js-reset-email-template', ( e ) => {
			this._handleReset( $( e.currentTarget ).closest( '.fs-email-template-card' ) );
		} );
	},

	/**
	 * Обработка сохранения пользовательского шаблона.
	 * Собирает данные из полей карточки и отправляет AJAX-запрос.
	 *
	 * @private
	 * @param {jQuery} $card - jQuery-объект карточки шаблона, в которой нажали "Сохранить".
	 */
	_handleSave( $card ) {
		// Извлекаем данные из карточки.
		// type хранится в data-атрибуте карточки (например, data-type="welcome_email").
		// Это позволяет серверу понять, какой именно шаблон обновлять.
		const type    = $card.data( 'type' );

		// Собираем значения полей. .trim() удаляет случайные пробелы по краям,
		// что важно для корректного отображения в email-клиентах.
		const subject = $card.find( '.js-email-subject' ).val().trim();
		const body    = $card.find( '.js-email-body' ).val().trim();

		// Находим элементы управления ВНУТРИ этой карточки.
		// Благодаря передаче $card мы гарантируем, что работаем с нужной кнопкой и уведомлением,
		// даже если на странице 10 одинаковых карточек шаблонов.
		const $btn    = $card.find( '.js-save-email-template' );
		const $notice = $card.find( '.js-template-notice' );

		// Блокируем кнопку и показываем индикатор загрузки.
		// Это защищает от двойного клика во время AJAX-запроса.
		$btn.prop( 'disabled', true ).text( 'Сохранение…' );
		$notice.hide(); // Скрываем предыдущее уведомление, если оно было

		$.post( fs_lms_vars.ajaxurl, {
			action:   fs_lms_vars.ajax_actions.saveEmailTemplate,
			security: fs_lms_vars.nonces.manager, // Nonce для защиты от CSRF-атак
			type,
			subject,
			body,
		} )
			.done( ( res ) => {
				if ( res.success ) {
					// Обновляем визуальный статус шаблона на "Переопределён"
					this._setStatus( $card, true );

					// Показываем уведомление об успехе внутри карточки
					this._showNotice( $notice, res.data.message || 'Сохранено', 'success' );

					// Разблокируем кнопку сброса — теперь шаблон можно вернуть к дефолтному
					$card.find( '.js-reset-email-template' ).prop( 'disabled', false );
				} else {
					// Показываем ошибку от сервера (например, валидация не прошла)
					this._showNotice( $notice, res.data || 'Ошибка сохранения', 'error' );
				}
			} )
			.fail( () => {
				// Обработка сетевых ошибок (потеря интернета, ошибка 500 и т.д.)
				this._showNotice( $notice, 'Ошибка сервера', 'error' );
			} )
			.always( () => {
				// ГАРАНТИРОВАННАЯ РАЗБЛОКИРОВКА:
				// .always() выполняется ВСЕГДА, независимо от успеха или ошибки запроса.
				// Это критически важно: если бы мы разблокировали кнопку только в .done(),
				// то при сетевой ошибке кнопка осталась бы заблокированной навсегда,
				// и пользователь не смог бы повторить попытку без перезагрузки страницы.
				$btn.prop( 'disabled', false ).text( 'Сохранить' );
			} );
	},

	/**
	 * Обработка сброса шаблона к значениям по умолчанию.
	 * Отправляет AJAX-запрос и очищает поля карточки.
	 *
	 * @private
	 * @param {jQuery} $card - jQuery-объект карточки шаблона, в которой нажали "Сбросить".
	 */
	_handleReset( $card ) {
		const type    = $card.data( 'type' );
		const $btn    = $card.find( '.js-reset-email-template' );
		const $notice = $card.find( '.js-template-notice' );

		$btn.prop( 'disabled', true ).text( 'Сброс…' );
		$notice.hide();

		$.post( fs_lms_vars.ajaxurl, {
			action:   fs_lms_vars.ajax_actions.resetEmailTemplate,
			security: fs_lms_vars.nonces.manager,
			type,
			// Не передаем subject и body — сервер сам подставит значения по умолчанию из кода
		} )
			.done( ( res ) => {
				if ( res.success ) {
					// Обновляем статус на "По умолчанию"
					this._setStatus( $card, false );

					// Очищаем поля — теперь в них нет пользовательских значений
					$card.find( '.js-email-subject' ).val( '' );
					$card.find( '.js-email-body' ).val( '' );

					// Блокируем кнопку сброса — сбрасывать больше нечего, шаблон уже дефолтный
					$btn.prop( 'disabled', true );

					this._showNotice( $notice, res.data.message || 'Сброшено', 'success' );
				} else {
					this._showNotice( $notice, res.data || 'Ошибка', 'error' );
					// При ошибке разблокируем кнопку, чтобы пользователь мог повторить попытку
					$btn.prop( 'disabled', false );
				}
			} )
			.fail( () => {
				this._showNotice( $notice, 'Ошибка сервера', 'error' );
				$btn.prop( 'disabled', false );
			} )
			.always( () => {
				// Восстанавливаем текст кнопки.
				// ВАЖНО: мы не разблокируем кнопку здесь, так как логика блокировки
				// зависит от результата операции (успех/ошибка) и обрабатывается в .done()/.fail().
				// .always() используется ТОЛЬКО для восстановления текста.
				$btn.text( 'Сбросить к умолчанию' );
			} );
	},

	/**
	 * Обновляет визуальный статус шаблона (переопределён / по умолчанию).
	 * Переключает CSS-классы и текст лейбла.
	 *
	 * ПАТТЕРН: Атомарное переключение классов.
	 * Сначала удаляем ВСЕ возможные классы статусов, затем добавляем нужный.
	 * Это гарантирует, что не будет ситуации, когда у элемента одновременно
	 * два конфликтующих класса (например, --custom и --default).
	 *
	 * @private
	 * @param {jQuery} $card - jQuery-объект карточки шаблона.
	 * @param {boolean} isCustom - true, если шаблон переопределён пользователем; false, если по умолчанию.
	 */
	_setStatus( $card, isCustom ) {
		const $label = $card.find( '[data-status-label]' );

		// Цепочка методов jQuery для атомарного обновления состояния:
		// 1. .text() — устанавливаем новый текст лейбла
		// 2. .removeClass() — удаляем ВСЕ классы статусов (и кастомный, и дефолтный)
		// 3. .addClass() — добавляем нужный класс в зависимости от состояния
		$label
			.text( isCustom ? 'Переопределён' : 'По умолчанию' )
			.removeClass( 'fs-email-template-card__status--custom fs-email-template-card__status--default' )
			.addClass( isCustom
				? 'fs-email-template-card__status--custom'
				: 'fs-email-template-card__status--default'
			);
	},

	/**
	 * Показывает уведомление внутри карточки шаблона с автозакрытием.
	 *
	 * ПАТТЕРН: Контекстное уведомление с автозакрытием.
	 * Вместо глобального toast-уведомления, которое появляется в углу экрана,
	 * мы показываем сообщение ВНУТРИ конкретной карточки, рядом с кнопкой действия.
	 * Это улучшает UX: пользователь сразу видит, к какому шаблону относится сообщение.
	 *
	 * Автозакрытие через setTimeout + fadeOut создает ненавязчивый интерфейс:
	 * уведомление исчезает само через 3 секунды, но если пользователь хочет прочитать его дольше,
	 * он может это сделать (в отличие от alert(), который блокирует интерфейс).
	 *
	 * @private
	 * @param {jQuery} $el - jQuery-объект элемента для отображения уведомления.
	 * @param {string} text - Текст уведомления.
	 * @param {'success'|'error'} type - Тип уведомления (определяет цвет).
	 */
	_showNotice( $el, text, type ) {
		// Цвет задаётся CSS-классами is-success / is-error (см. _email-templates.scss).
		$el.text( text )
			.removeClass( 'is-success is-error' )
			.addClass( type === 'success' ? 'is-success' : 'is-error' )
			.show();

		// АВТОЗАКРЫТИЕ С АНИМАЦИЕЙ:
		// Через 3 секунды запускаем плавное исчезновение (fadeOut за 400мс).
		// Это дает пользователю время прочитать сообщение, но не загромождает интерфейс.
		setTimeout( () => $el.fadeOut( 400 ), 3000 );
	},
};