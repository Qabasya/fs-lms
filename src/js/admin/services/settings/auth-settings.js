/**
 * @fileoverview Модуль управления настройками OAuth-аутентификации (вкладка "Настройки" → "Авторизация").
 *
 * @module AuthSettings
 * @description Менеджер формы настроек OAuth-провайдеров (Google, VK, GitHub и т.д.).
 *              Отвечает за:
 *              - Динамическое управление видимостью полей credentials для каждого провайдера
 *              - Синхронизацию атрибута required с состоянием toggle-переключателя
 *              - Перехват стандартной отправки формы и замена на AJAX-submit
 *              - Визуальную обратную связь при сохранении настроек
 *              - Слушание кастомного события fs-toggle-changed для межкомпонентного взаимодействия
 *
 *              Архитектурная особенность: модуль не создает toggle-переключатели сам,
 *              а реагирует на кастомное событие 'fs-toggle-changed', которое генерируется
 *              другим компонентом (вероятно, универсальным toggle-компонентом).
 *              Это реализация слабосвязанной архитектуры через паттерн Pub/Sub.
 *
 * @requires jQuery
 * @requires showNotice - утилита для отображения уведомлений в стиле WordPress
 * @requires HelpModal - модальное окно справки (импортируется, но не используется напрямую в этом модуле)
 */

const $ = jQuery;

import { showNotice } from '../../modules/utils.js';
import { HelpModal } from '../../modals/help-modal.js';

/**
 * Менеджер формы настроек OAuth-аутентификации.
 * Управляет динамическим поведением формы: показывает/скрывает поля credentials
 * в зависимости от состояния toggle-переключателей провайдеров.
 */
export const AuthSettings = {

    /**
     * Инициализация менеджера настроек авторизации.
     * Точка входа, вызывается при загрузке страницы настроек.
     *
     * АЛГОРИТМ:
     * 1. Навешиваем обработчики событий (клики, submit формы)
     * 2. Синхронизируем состояние required для всех полей с текущим состоянием toggle
     *    Это критически важно: если провайдер был включен ранее и сохранен в БД,
     *    его поля должны быть required сразу при загрузке страницы,
     *    иначе форма пройдет валидацию даже с пустыми credentials.
     */
    init() {
        this.bindEvents();
        this._syncAllRequired();
    },

    /**
     * Привязка обработчиков событий.
     *
     * ИСПОЛЬЗУЕТСЯ ДВА ТИПА ОБРАБОТЧИКОВ:
     * 1. Кастомное событие 'fs-toggle-changed' — для реакции на переключение провайдеров
     * 2. Стандартное событие 'submit' — для перехвата отправки формы
     *
     * ПАТТЕРН: Кастомные события для межкомпонентного взаимодействия.
     * Вместо того чтобы AuthSettings напрямую управлял toggle-переключателями,
     * он слушает событие 'fs-toggle-changed', которое генерируется другим компонентом.
     * Это снижает связанность кода: AuthSettings не знает, КАК устроен toggle,
     * он только знает, ЧТО произошло (какой элемент изменился и какое новое значение).
     */
    bindEvents() {
        // СЛУШАНИЕ КАСТОМНОГО СОБЫТИЯ:
        // Событие 'fs-toggle-changed' генерируется универсальным toggle-компонентом.
        // Вторым параметром передается объект data с информацией об изменении:
        // - data.element — jQuery-объект переключателя, который изменился
        // - data.value — новое состояние (true/false или 1/0)
        //
        // ПРОВЕРКА ПРИНАДЛЕЖНОСТИ:
        // Мы проверяем, что переключатель находится внутри .js-provider-toggle,
        // так как на странице могут быть и другие toggle-переключатели 
        // (например, для включения/отключения всего модуля авторизации).
        // Нам нужно реагировать ТОЛЬКО на переключение конкретных провайдеров.
        $(document).on('fs-toggle-changed', (e, data) => {
            if (data.element.closest('.js-provider-toggle').length) {
                this.toggleFields(data.element, data.value);
            }
        });

        // ПЕРЕХВАТ ОТПРАВКИ ФОРМЫ:
        // Вместо стандартной отправки (которая перезагрузит страницу),
        // мы перехватываем submit и отправляем данные через AJAX.
        // Это позволяет показать уведомление об успехе без перезагрузки.
        //
        // ВАЖНО: используем function(e), а не стрелочную функцию,
        // так как внутри нам нужен 'this' как ссылка на DOM-элемент формы.
        // Стрелочная функция захватила бы 'this' из внешнего контекста (AuthSettings),
        // а не из DOM-элемента, что сломало бы логику.
        $('#tab-2 form').on('submit', function(e) {
            e.preventDefault(); // Отменяем стандартную отправку формы

            // HTML5 ВАЛИДАЦИЯ ФОРМЫ:
            // reportValidity() проверяет все поля формы на валидность 
            // (required, type, pattern, min/max и т.д.) и показывает браузерные подсказки.
            // Возвращает true, если все поля валидны, иначе false.
            // Это нативный механизм валидации, который работает без JavaScript.
            if (!this.reportValidity()) {
                return; // Если валидация не прошла, прерываем отправку
            }

            const $form = $(this);
            const $submitButton = $form.find('#submit');

            // БЛОКИРОВКА КНОПКИ:
            // Блокируем кнопку и меняем текст на индикатор загрузки.
            // Это защищает от двойного клика во время AJAX-запроса.
            $submitButton.prop('disabled', true).val('Сохранение...');

            // AJAX-ОТПРАВКА ФОРМЫ:
            // Используем $.ajax вместо $.post, так как нам нужны дополнительные опции:
            // - url из атрибута action формы (гибкость)
            // - type: 'POST' (явное указание метода)
            // - complete callback для гарантированной разблокировки кнопки
            $.ajax({
                url: $form.attr('action'), // URL из атрибута action формы (обычно options.php в WordPress)
                type: 'POST',
                // serialize() автоматически собирает все поля формы в URL-encoded строку.
                // Это избавляет от необходимости вручную собирать каждое поле.
                // WordPress обработает эти данные как стандартную отправку формы.
                data: $form.serialize(),

                // ОБРАБОТКА УСПЕХА:
                // Показываем уведомление об успехе с автозакрытием через 2 секунды.
                // Уведомление вставляется внутрь формы, чтобы быть рядом с местом действия.
                success: function() {
                    showNotice('Настройки успешно сохранены!', 'success', $form, {
                        autoDismiss: true,
                        autoDismissDelay: 2000
                    });
                },

                // ОБРАБОТКА ОШИБКИ:
                // Показываем уведомление об ошибке БЕЗ автозакрытия.
                // Ошибки требуют внимания пользователя, поэтому он должен сам закрыть уведомление.
                error: function() {
                    showNotice('Произошла ошибка при сохранении настроек.', 'error', $form, {
                        autoDismiss: false
                    });
                },

                // ГАРАНТИРОВАННАЯ РАЗБЛОКИРОВКА:
                // complete вызывается ВСЕГДА, независимо от успеха или ошибки.
                // Это гарантирует, что кнопка разблокируется даже при сетевых сбоях.
                // Если бы мы разблокировали только в success/error, при таймауте 
                // кнопка осталась бы заблокированной навсегда.
                complete: function() {
                    $submitButton.prop('disabled', false).val('Сохранить изменения');
                }
            });
        });
    },

    /**
     * Динамическое управление видимостью и валидацией полей credentials провайдера.
     *
     * АЛГОРИТМ:
     * 1. Определяем, какой провайдер был переключен (через fallback-цепочку источников)
     * 2. Находим все строки полей этого провайдера по CSS-классу .auth-fields-${provider}
     * 3. В зависимости от состояния toggle:
     *    - Если включен: показываем поля и делаем их required
     *    - Если выключен: скрываем поля и снимаем required
     *
     * ПАТТЕРН: Fallback-цепочка для определения провайдера.
     * Провайдер может быть указан в разных местах (data-атрибуты, ID элемента),
     * поэтому мы пытаемся извлечь его из нескольких источников в порядке приоритета.
     * Это делает функцию устойчивой к разным вариантам разметки HTML.
     *
     * @param {jQuery} $checkbox - jQuery-объект переключателя, который изменился.
     * @param {boolean} isChecked - Новое состояние переключателя (true = включен, false = выключен).
     */
    toggleFields($checkbox, isChecked) {
        // FALLBACK-ЦЕПОЧКА ДЛЯ ОПРЕДЕЛЕНИЯ ПРОВАЙДЕРА:
        // Пытаемся получить имя провайдера из разных источников в следующем порядке:

        // 1. data-provider на самом переключателе
        let provider = $checkbox.data('provider') ||
            // 2. data-provider на родительском контейнере .js-provider-toggle
            $checkbox.closest('.js-provider-toggle').data('provider');

        // 3. Если не найдено, пытаемся извлечь из ID переключателя.
        //    Например, ID "google_toggle" → provider = "google".
        //    Метод replace('_toggle', '') удаляет суффикс '_toggle' из ID.
        if (!provider && $checkbox.attr('id')) {
            provider = $checkbox.attr('id').replace('_toggle', '');
        }

        // Если провайдер так и не определен, выходим — не можем определить, какие поля управлять.
        if (!provider) return;

        // ПОИСК СТРОК ПОЛЕЙ:
        // Ищем все строки таблицы/контейнера с классом .auth-fields-${provider}.
        // Например, для provider="google" найдем .auth-fields-google.
        // Это соглашение об именовании связывает toggle-переключатель с его полями.
        const $targetRows = $(`.auth-fields-${provider}`);

        // Находим все текстовые поля и поля паролей внутри этих строк.
        // Именно эти поля нужно делать required/не-required.
        const $inputs = $targetRows.find('input[type="text"], input[type="password"]');

        if (isChecked) {
            // ПРОВАЙДЕР ВКЛЮЧЕН:
            // 1. Показываем строки полей (удаляем класс hidden)
            $targetRows.removeClass('hidden');
            // 2. Делаем все поля обязательными для заполнения.
            //    Теперь HTML5 валидация не пропустит форму, если эти поля пусты.
            $inputs.prop('required', true);
        } else {
            // ПРОВАЙДЕР ВЫКЛЮЧЕН:
            // 1. Скрываем строки полей (добавляем класс hidden)
            $targetRows.addClass('hidden');
            // 2. Снимаем атрибут required, чтобы форма могла быть отправлена 
            //    даже если поля пусты (они все равно не будут использоваться).
            // 3. Удаляем класс form-invalid, если он был добавлен предыдущей неудачной валидацией.
            //    Это очищает визуальное состояние ошибки.
            $inputs.prop('required', false).removeClass('form-invalid');
        }
    },

    /**
     * Синхронизация атрибута required для всех полей с текущим состоянием toggle.
     *
     * ЗАЧЕМ ЭТО НУЖНО:
     * При загрузке страницы настройки уже могут быть сохранены в БД:
     * некоторые провайдеры включены, другие выключены.
     * HTML-разметка может не отражать это состояние корректно:
     * например, поля включенного провайдера могут не иметь атрибута required.
     *
     * Этот метод проходит по всем карточкам провайдеров и синхронизирует
     * атрибут required с фактической видимостью карточки:
     * - Если карточка видна (провайдер включен) → поля required
     * - Если карточка скрыта (провайдер выключен) → поля не required
     *
     * Это гарантирует, что HTML5 валидация будет работать корректно
     * с самого начала, без необходимости ждать взаимодействия пользователя.
     *
     * @private
     */
    _syncAllRequired() {
        // Проходим по всем телам карточек провайдеров.
        // Каждая карточка (auth-fields-*) содержит поля credentials одного провайдера.
        $('.fs-lms-auth-providers .fs-card__body').each((_, body) => {
            const $body = $(body);

            // Определяем состояние провайдера по наличию класса hidden.
            // Если класс hidden отсутствует — провайдер включен (карточка видима).
            // Если класс hidden присутствует — провайдер выключен (карточка скрыта).
            const isEnabled = !$body.hasClass('hidden');

            // Устанавливаем атрибут required для всех полей ввода в карточке 
            // в соответствии с состоянием провайдера.
            $body.find('input[type="text"], input[type="password"]').prop('required', isEnabled);
        });
    }
};