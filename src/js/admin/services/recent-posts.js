/**
 * @fileoverview Модуль загрузки блока "Недавний контент" (задачи или статьи).
 *
 * @module RecentContent
 * @description Обеспечивает динамическую загрузку списка недавних задач или статей
 *              на страницу предмета через AJAX.
 *
 *              Архитектурная особенность: один модуль обслуживает два типа контента
 *              (задачи и статьи), определяя тип по ID контейнера в DOM.
 *              Это позволяет избежать дублирования кода для двух практически идентичных блоков.
 *
 *              Принцип работы:
 *              1. При инициализации ищет контейнер на странице
 *              2. Определяет тип контента по ID контейнера
 *              3. Отправляет AJAX-запрос на сервер с ключом предмета
 *              4. Заменяет содержимое контейнера HTML-ответом от сервера
 *              5. Обрабатывает ошибки (бизнес-ошибки и сетевые сбои)
 *
 * @requires jQuery - глобальная зависимость WordPress
 * @requires ../_types.js - глобальные типы данных
 */

import '../_types.js';

const $ = jQuery;

/**
 * Модуль загрузки и отображения блока "Недавний контент".
 * Используется на странице предмета для показа последних добавленных задач или статей.
 *
 * @namespace RecentContent
 */
export const RecentContent = {

    /**
     * Инициализация модуля и загрузка контента.
     *
     * АЛГОРИТМ РАБОТЫ:
     * 1. Поиск контейнера на странице (для задач или статей)
     * 2. Определение типа контента по ID контейнера
     * 3. Выбор соответствующего AJAX-действия
     * 4. Проверка наличия необходимых данных (subject_key)
     * 5. Показ индикатора загрузки
     * 6. Отправка AJAX-запроса
     * 7. Обработка ответа (успех/ошибка)
     *
     * ВАЖНО: В отличие от других модулей, этот не имеет отдельного метода bindEvents(),
     * так как вся логика выполняется один раз при инициализации — загрузка контента.
     * Нет интерактивных элементов, требующих обработчиков событий.
     *
     * @memberof RecentContent
     * @instance
     * @returns {void}
     */
    init() {
        // ==========================================
        // 1. ПОИСК КОНТЕЙНЕРА
        // ==========================================
        // Используем селектор с запятой для поиска ЛЮБОГО из двух возможных контейнеров.
        // jQuery вернет первый найденный элемент.
        // На странице предмета может быть либо блок задач, либо блок статей, но не оба сразу.
        const $container = $('#fs-recent-tasks-container, #fs-recent-articles-container');

        // Guard clause: если ни одного контейнера нет на странице,
        // прекращаем выполнение. Это стандартный паттерн для модулей,
        // которые загружаются глобально, но используются только на определенных страницах.
        if (!$container.length) return;

        // ==========================================
        // 2. ОПРЕДЕЛЕНИЕ ТИПА КОНТЕНТА
        // ==========================================
        // ПАТТЕРН: Полиморфизм через ID контейнера.
        // Вместо того чтобы создавать два отдельных модуля (RecentTasks и RecentArticles),
        // мы используем один модуль, который определяет тип контента по ID контейнера.
        // Это устраняет дублирование кода: логика загрузки, отображения и обработки ошибок
        // идентична для обоих типов, различаются только AJAX-действия и заголовки.
        //
        // Проверяем, равен ли ID контейнера 'fs-recent-articles-container'.
        // Если да — это статьи, иначе — задачи.
        const isArticle = $container.attr('id') === 'fs-recent-articles-container';
        const type = isArticle ? 'articles' : 'tasks';

        // ==========================================
        // 3. ВЫБОР AJAX-ДЕЙСТВИЯ
        // ==========================================
        // В зависимости от типа контента выбираем соответствующее WordPress AJAX-действие.
        // Эти действия зарегистрированы на сервере через add_action('wp_ajax_...').
        // Ternary operator (условный оператор) делает код компактным и читаемым.
        const ajaxAction = isArticle
            ? fs_lms_vars.ajax_actions.getRecentArticles
            : fs_lms_vars.ajax_actions.getRecentTasks;

        // ==========================================
        // 4. ПРОВЕРКА НЕОБХОДИМЫХ ДАННЫХ
        // ==========================================
        // Проверяем наличие глобального объекта fs_lms_task_data, который передается
        // через wp_localize_script в PHP. Этот объект содержит контекст текущей страницы
        // (ключ предмета, nonce и т.д.).
        //
        // typeof fs_lms_task_data === 'undefined' — безопасный способ проверки существования
        // глобальной переменной. Если написать просто `if (!fs_lms_task_data)`,
        // при отсутствии объекта выбросится ReferenceError.
        //
        // Также проверяем наличие subject_key — без него мы не сможем загрузить контент
        // для конкретного предмета.
        if (typeof fs_lms_task_data === 'undefined' || !fs_lms_task_data.subject_key) {
            // Используем console.warn для предупреждений (желтый цвет в DevTools),
            // а не console.error (красный), так как это не критическая ошибка,
            // а скорее отсутствие данных для работы модуля.
            // Динамическое имя модуля в логе помогает быстро найти источник проблемы.
            console.warn(`[Recent${type === 'articles' ? 'Articles' : 'Tasks'}] fs_lms_task_data не найден`);
            return;
        }

        // Извлекаем ключ предмета для передачи на сервер
        const subjectKey = fs_lms_task_data.subject_key;

        // ==========================================
        // 5. ВИЗУАЛЬНЫЙ ИНДИКАТОР ЗАГРУЗКИ
        // ==========================================
        // Добавляем CSS-класс 'is-loading' (может использоваться для показа спиннера)
        // и уменьшаем прозрачность контейнера до 0.5 для визуальной индикации загрузки.
        // Цепочка методов jQuery позволяет выполнить оба действия в одной строке.
        $container.addClass('is-loading').css('opacity', '0.5');

        // ==========================================
        // 6. AJAX-ЗАПРОС К СЕРВЕРУ
        // ==========================================
        // Используем $.post() для отправки POST-запроса.
        // Третий параметр — callback-функция, которая вызывается при успешном HTTP-ответе (2xx).
        // ВАЖНО: этот callback вызывается даже если response.success === false.
        // Это бизнес-ошибка, а не HTTP-ошибка, поэтому она обрабатывается внутри callback.
        $.post(fs_lms_vars.ajaxurl, {
            action:      ajaxAction, // Имя WordPress AJAX-действия (например, 'getRecentTasks')
            security:    fs_lms_vars.nonces.subject, // Nonce-токен для защиты от CSRF-атак
            subject_key: subjectKey, // Ключ предмета для загрузки контента
        }, (response) => {
            // ==========================================
            // 7. ОБРАБОТКА ОТВЕТА СЕРВЕРА
            // ==========================================

            // СНИМАЕМ ИНДИКАТОР ЗАГРУЗКИ:
            // Удаляем CSS-класс 'is-loading' и возвращаем прозрачность к 1 (полная видимость).
            // Это делаем в начале обработчика, чтобы индикатор исчез сразу после получения ответа,
            // а не после обработки данных.
            $container.removeClass('is-loading').css('opacity', '1');

            // ПРОВЕРКА БИЗНЕС-УСПЕХА:
            // WordPress AJAX всегда возвращает объект { success: boolean, data: any }.
            // Проверяем флаг success, чтобы различать успешные и ошибочные ответы.
            if (response.success) {
                // УСПЕХ: заменяем содержимое контейнера HTML-ответом от сервера.
                // Сервер возвращает готовый HTML-код списка задач/статей,
                // что упрощает клиентскую логику — не нужно рендерить данные через JS.
                $container.html(response.data.html);
            } else {
                // БИЗНЕС-ОШИБКА: сервер вернул { success: false, data: '...' }.
                // Это может быть ошибка валидации, отсутствие данных или другая логическая ошибка.

                // Извлекаем сообщение об ошибке с fallback-цепочкой:
                // 1. response.data?.message — специфичное сообщение от сервера (если есть)
                // 2. 'Не удалось загрузить данные.' — дефолтное сообщение
                // Оператор ?. (optional chaining) предотвращает ошибку, если response.data равен undefined.
                const msg = response.data?.message || 'Не удалось загрузить данные.';

                // Показываем ошибку в стиле WordPress Admin Notice.
                // Классы 'notice notice-error inline' обеспечивают стандартное оформление WordPress.
                $container.html(`<div class="notice notice-error inline"><p>${msg}</p></div>`);
            }
        }).fail((jqXHR, textStatus) => {
            // ==========================================
            // 8. ОБРАБОТКА СЕТЕВЫХ ОШИБОК
            // ==========================================
            // Метод .fail() вызывается, когда HTTP-запрос завершился с кодом 4xx/5xx
            // или при сетевых проблемах (потеря интернета, таймаут).
            //
            // ВАЖНО: это НЕ вызывается при response.success === false.
            // Это HTTP-ошибка, а не бизнес-ошибка.

            // Снимаем индикатор загрузки
            $container.removeClass('is-loading').css('opacity', '1');

            // Логируем ошибку в консоль для разработчиков.
            // jqXHR — объект jQuery с деталями HTTP-запроса (статус, заголовки, тело ответа).
            // textStatus — строковое описание статуса ('timeout', 'error', 'abort', 'parsererror').
            // jqXHR.responseText — тело ответа сервера (может содержать HTML ошибки 500).
            console.error(`[Recent${type}] AJAX Error:`, textStatus, jqXHR.responseText);

            // Показываем пользователю общее сообщение об ошибке.
            // Намеренно не показываем технические детали (статус, текст ошибки),
            // так как они непонятны конечному пользователю.
            // Упоминание консоли (F12) помогает разработчикам или продвинутым пользователям
            // получить больше информации для отладки.
            $container.html('<p>Ошибка сервера. Проверьте консоль (F12).</p>');
        });
    },
};