/**
 * @module modal-base
 * @description Базовые утилиты для управления модальными окнами.
 *              Предоставляет низкоуровневые функции открытия/закрытия модалок с анимацией,
 *              управления скроллом страницы и обработки клавиши Escape.
 *
 *              Все специализированные модальные окна (ConfirmModal, AlertModal, SubjectModal и т.д.)
 *              используют эти утилиты как фундамент, добавляя свою бизнес-логику поверх.
 *
 *              Ключевые особенности:
 *              - Автоматическая компенсация ширины скроллбара для предотвращения "прыжков" контента
 *              - Корректная работа CSS-анимаций через принудительный reflow
 *              - Изоляция обработчиков ESC через неймспейсинг (несколько модалок не конфликтуют)
 *              - Поддержка callback-функций для выполнения действий после завершения анимации
 *
 * @requires jQuery
 */

const $ = jQuery;

/**
 * Открывает модальное окно с плавной CSS-анимацией.
 *
 * АЛГОРИТМ РАБОТЫ:
 * 1. Вычисляем ширину скроллбара и сохраняем её в CSS-переменную --scrollbar-width.
 *    Это нужно, потому что при добавлении класса 'modal-open' к <html> мы устанавливаем
 *    overflow: hidden, что скрывает скроллбар. Без компенсации ширины весь контент страницы
 *    сместится вправо на ширину скроллбара (обычно 17px), что выглядит как "прыжок".
 *    CSS-переменная используется в стилях для добавления padding-right к body,
 *    компенсируя это смещение.
 *
 * 2. Добавляем класс 'modal-open' к <html>, что блокирует скролл страницы.
 *
 * 3. Удаляем класс 'hidden' (display: none) с модалки, делая её частью DOM-потока,
 *    но ещё невидимой (opacity: 0).
 *
 * 4. Выполняем принудительный reflow через чтение offsetHeight.
 *    Это критически важный шаг: без него браузер оптимизирует отрисовку и применит
 *    классы 'hidden' и 'active' в одном кадре, из-за чего CSS-transition не сработает
 *    (не будет промежуточного состояния для анимации).
 *
 * 5. Добавляем класс 'active', который триггерит CSS-transition (opacity 0→1, transform и т.д.).
 *
 * @param {jQuery} $modal - jQuery-объект модального окна (должен иметь классы 'hidden' и 'active' в CSS).
 */
export function openModal($modal) {
    // ШАГ 1: Компенсация скроллбара.
    // window.innerWidth — полная ширина окна браузера (включая скроллбар).
    // document.documentElement.clientWidth — ширина видимой области контента (без скроллбара).
    // Разница между ними — это ширина скроллбара (обычно 17px на Windows, 0 на macOS с overlay-скроллом).
    const scrollBarWidth = window.innerWidth - document.documentElement.clientWidth;

    // Сохраняем значение в CSS-переменную, чтобы её могли использовать стили модалки.
    // Например, в CSS может быть: body.modal-open { padding-right: var(--scrollbar-width); }
    document.documentElement.style.setProperty('--scrollbar-width', scrollBarWidth + 'px');

    // ШАГ 2: Блокировка скролла страницы.
    // Класс 'modal-open' обычно устанавливает html { overflow: hidden; },
    // предотвращая прокрутку страницы, когда открыта модалка.
    $('html').addClass('modal-open');

    // ШАГ 3: Убираем 'hidden' (display: none), чтобы модалка стала частью DOM-потока.
    // Но она ещё невидима, так как opacity по умолчанию 0 (или задано в CSS для состояния без 'active').
    $modal.removeClass('hidden');

    // ШАГ 4: Принудительный reflow (Force Reflow).
    // Чтение свойства offsetHeight заставляет браузер немедленно пересчитать layout.
    // Это создает необходимую "паузу" между удалением 'hidden' и добавлением 'active',
    // позволяя браузеру увидеть два разных состояния элемента и применить CSS-transition.
    // void используется, чтобы явно указать, что возвращаемое значение не используется
    // (защита от предупреждений линтеров типа ESLint).
    if ($modal[0]) void $modal[0].offsetHeight;

    // ШАГ 5: Запуск анимации появления.
    // Класс 'active' обычно устанавливает opacity: 1 и, возможно, transform (scale, translateY),
    // что в сочетании с CSS transition создает плавное появление модалки.
    $modal.addClass('active');
}

/**
 * Закрывает модальное окно с плавной CSS-анимацией и выполняет callback после её завершения.
 *
 * АЛГОРИТМ РАБОТЫ:
 * 1. Удаляем класс 'active', что запускает обратную CSS-анимацию (opacity 1→0).
 *
 * 2. Ждем завершения анимации через setTimeout.
 *    ВАЖНО: Длительность setTimeout (200мс) ДОЛЖНА совпадать с длительностью CSS-transition,
 *    указанной в стилях модалки (например, transition: opacity 200ms ease).
 *    Если setTimeout будет меньше, модалка скроется до завершения анимации (резкое исчезновение).
 *    Если больше — пользователь увидит задержку между окончанием анимации и скрытием.
 *
 * 3. По истечении setTimeout:
 *    - Добавляем 'hidden' (display: none), полностью удаляя модалку из потока
 *    - Убираем 'modal-open' с <html>, разблокируя скролл страницы
 *    - Удаляем CSS-переменную --scrollbar-width, очищая глобальное состояние
 *    - Вызываем callback, если он передан (например, для сброса состояния модалки)
 *
 * @param {jQuery} $modal - jQuery-объект модального окна.
 * @param {Function} [callback] - Функция, вызываемая после завершения анимации закрытия.
 *                                 Используется для очистки состояния модалки (сброс формы, удаление данных).
 */
export function closeModal($modal, callback) {
    // ШАГ 1: Запуск анимации исчезновения.
    // Удаление класса 'active' возвращает opacity к 0, что триггерит CSS-transition.
    $modal.removeClass('active');

    // ШАГ 2-3: Ожидание завершения анимации и полная очистка.
    // Таймаут 200мс должен совпадать с transition-duration в CSS.
    setTimeout(() => {
        // Полностью скрываем модалку через display: none
        $modal.addClass('hidden');

        // Разблокируем скролл страницы
        $('html').removeClass('modal-open');

        // Очищаем CSS-переменную, чтобы она не влияла на другие элементы страницы
        document.documentElement.style.removeProperty('--scrollbar-width');

        // Вызываем callback, если он передан и является функцией.
        // Проверка typeof нужна для защиты от случайной передачи не-функции.
        if (typeof callback === 'function') callback();
    }, 200);
}

/**
 * Привязывает обработчик нажатия клавиши Escape с изоляцией через неймспейс.
 *
 * ПАТТЕРН НЕЙМСПЕЙСИНГА:
 * Каждая модалка использует уникальный неймспейс (например, 'confirm', 'task_creation', 'subject').
 * Это позволяет:
 * 1. Иметь несколько обработчиков ESC одновременно (для разных модалок, если они могут быть открыты вместе).
 * 2. Точечно отвязывать обработчик конкретной модалки через unbindEsc(ns), не затрагивая другие.
 * 3. Избежать конфликтов, если одна модалка открывается поверх другой.
 *
 * ПРИМЕР ИСПОЛЬЗОВАНИЯ:
 * bindEsc('confirm', () => ConfirmModal.close());
 * bindEsc('task_creation', () => TaskModal.close());
 * // Теперь нажатие ESC вызовет оба обработчика, если обе модалки открыты.
 * // Но unbindEsc('confirm') отвяжет только обработчик ConfirmModal.
 *
 * @param {string} ns - Уникальный неймспейс события (например, 'confirm', 'task_creation', 'subject').
 *                      Должен совпадать с неймспейсом, переданным в unbindEsc() при закрытии.
 * @param {Function} fn - Функция, вызываемая при нажатии клавиши Escape.
 *                        Обычно это () => this.close() или аналогичный метод закрытия модалки.
 */
export function bindEsc(ns, fn) {
    // Неймспейс добавляется к имени события через точку: 'keydown.modal_confirm'.
    // Это стандартный механизм jQuery для группировки обработчиков событий.
    // Обработчики с разными неймспейсами не конфликтуют друг с другом.
    $(document).on('keydown.modal_' + ns, (e) => {
        // Проверяем, что нажата именно клавиша Escape.
        // e.key — современное свойство, возвращающее строковое представление клавиши ('Escape', 'Enter', 'a' и т.д.).
        // В старых браузерах использовался e.keyCode === 27, но e.key более читаем и стандартизирован.
        if (e.key === 'Escape') fn();
    });
}

/**
 * Отвязывает обработчик Escape по указанному неймспейсу.
 *
 * ВАЖНО: Этот метод ДОЛЖЕН вызываться при закрытии модалки (в функции close()).
 * Если забыть отвязать обработчик, нажатие ESC на странице будет продолжать вызывать
 * функцию закрытия уже невидимой модалки, что может привести к:
 * - Ошибкам JavaScript (если модалка уже удалена из DOM)
 * - Неожиданному поведению (например, закрытию другой модалки, открытой поверх)
 * - Утечкам памяти (обработчики накапливаются при каждом открытии модалки)
 *
 * @param {string} ns - Неймспейс, переданный в bindEsc() при открытии модалки.
 *                      Должен точно совпадать, иначе обработчик не будет удален.
 */
export function unbindEsc(ns) {
    // Метод .off() с указанием неймспеса удаляет ТОЛЬКО обработчики с этим неймспесом.
    // Другие обработчики keydown (например, от других модалок или компонентов) остаются нетронутыми.
    // Это безопаснее, чем .off('keydown'), который удалил бы ВСЕ обработчики keydown на документе.
    $(document).off('keydown.modal_' + ns);
}