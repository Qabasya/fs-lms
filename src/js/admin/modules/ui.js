/**
 * @module UI
 * @description Модуль централизованной инициализации всех UI-компонентов приложения.
 *              Автоматически находит все файлы модальных окон в директории '../modals'
 *              и вызывает их метод init() без необходимости вручную импортировать каждый компонент.
 *
 *              Ключевые особенности:
 *              - Динамический импорт через Webpack require.context (автоматическое обнаружение компонентов)
 *              - Поддержка разных стилей экспорта (named export, default export)
 *              - Безопасная инициализация с обработкой ошибок (один упавший компонент не ломает остальные)
 *              - Проверка готовности DOM перед запуском
 *              - Проверка наличия jQuery (все компоненты зависят от него)
 *              - Механизм повторной инициализации для динамически подгруженного контента
 *
 * @requires jQuery - все UI-компоненты используют jQuery для DOM-манипуляций
 */

/**
 * Модуль централизованной инициализации UI-компонентов.
 * @namespace UI
 */
export const UI = {
    /**
     * Находит и инициализирует все компоненты из директории '../modals'.
     *
     * КАК ЭТО РАБОТАЕТ (Webpack Magic):
     * Вместо того чтобы вручную импортировать каждый компонент:
     *   import { SubjectModal } from '../modals/subject-modal.js';
     *   import { GroupModal } from '../modals/group-modal.js';
     *   // ... и так 20+ раз
     *
     * Мы используем Webpack-специфичный API require.context, который:
     * 1. Сканирует указанную директорию на этапе сборки (build time)
     * 2. Создает "виртуальный" модуль, содержащий все файлы, соответствующие фильтру
     * 3. Позволяет динамически загружать эти файлы по имени через функцию requireComponent(fileName)
     *
     * ПРЕИМУЩЕСТВА:
     * - При добавлении нового файла модалки в директорию '../modals' он автоматически
     *   будет инициализирован без правки этого файла
     * - Устраняется риск забыть импортировать новый компонент
     * - Код остается чистым и не разрастается с каждым новым компонентом
     *
     * НЕДОСТАТКИ:
     * - Это не стандартный ES-модуль, а Webpack-специфика. При переходе на Vite/Rollup
     *   потребуется использовать import.meta.glob() или аналог
     * - IDE может не подсказывать типы компонентов (они загружаются динамически)
     *
     * @returns {void}
     */
    init() {
        // require.context: Webpack-specific API для динамического импорта.
        // Параметры:
        //   1. '../modals'     — относительный путь к директории с компонентами
        //   2. false           — флаг рекурсии (false = не заглядывать в поддиректории)
        //   3. /\.js$/         — регулярное выражение для фильтрации файлов (только .js)
        //
        // Результат — функция requireComponent, которая принимает имя файла
        // и возвращает объект с экспортами этого файла.
        const requireComponent = require.context('../modals', false, /\.js$/);

        // requireComponent.keys() возвращает массив всех найденных путей, например:
        // ['./subject-modal.js', './group-modal.js', './confirm-modal.js', ...]
        requireComponent.keys().forEach(fileName => {
            // Загружаем модуль по имени файла.
            // componentConfig — это объект со всеми экспортами файла, например:
            // { SubjectModal: {...}, default: {...} }
            const componentConfig = requireComponent(fileName);

            // ПОДДЕРЖКА РАЗНЫХ СТИЛЕЙ ЭКСПОРТА:
            // Разные разработчики могут экспортировать компоненты по-разному:
            //   export const SubjectModal = {...}      — named export
            //   export default { ... }                 — default export
            //   module.exports = {...}                 — CommonJS (редко, но возможно)
            //
            // Мы пытаемся получить компонент в следующем порядке приоритета:
            // 1. componentConfig.Modal — ожидаем, что экспорт называется 'Modal' (по соглашению)
            // 2. componentConfig.default — стандартный default export из ES-модулей
            // 3. componentConfig[Object.keys(...)[0]] — берем ПЕРВЫЙ экспорт из файла (fallback)
            //
            // Это делает систему устойчивой к разным стилям написания кода.
            const component = componentConfig.Modal ||
                componentConfig.default ||
                componentConfig[Object.keys(componentConfig)[0]];

            // ПРОВЕРКА НАЛИЧИЯ МЕТОДА init():
            // Не все файлы в директории могут быть компонентами (например, _types.js или utils.js).
            // Мы инициализируем только те модули, у которых есть метод init().
            // Это позволяет держать в одной директории и вспомогательные файлы.
            if (component && typeof component.init === 'function') {
                try {
                    component.init();
                } catch (err) {
                    // БЕЗОПАСНАЯ ИНИЦИАЛИЗАЦИЯ (Fail-Safe):
                    // Если один компонент упал с ошибкой (например, из-за отсутствующего DOM-элемента),
                    // мы ловим ошибку, логируем её и продолжаем инициализацию остальных компонентов.
                    // Без try/catch ошибка в одном компоненте прервала бы весь цикл forEach,
                    // и остальные компоненты не были бы инициализированы.
                    console.error(`[UI] Failed to initialize component "${fileName}":`, err);
                }
            }
        });
    },
};

/**
 * Точка входа для инициализации UI.
 * Проверяет окружение (наличие jQuery) и вызывает UI.init() после готовности DOM.
 *
 * ПОЧЕМУ ПРОВЕРКА DOM-READY ВАЖНА:
 * Все UI-компоненты ищут свои DOM-элементы через $('#id') в методе init().
 * Если вызвать init() до того, как HTML отрисован, все селекторы вернут пустые jQuery-объекты,
 * и компоненты не смогут работать. Поэтому мы ждем события DOMContentLoaded.
 *
 * АЛГОРИТМ:
 * 1. Проверяем, загружен ли jQuery. Если нет — логируем ошибку и выходим.
 *    Все компоненты зависят от jQuery, поэтому без него инициализация бессмысленна.
 * 2. Проверяем состояние готовности DOM через document.readyState:
 *    - 'loading'    — HTML еще парсится, ждем jQuery.ready()
 *    - 'interactive' — HTML распарсен, но ресурсы (картинки) еще грузятся. Можно инициализировать.
 *    - 'complete'   — всё загружено. Можно инициализировать.
 * 3. Если DOM готов — запускаем инициализацию сразу. Иначе — через jQuery(document).ready().
 *
 * @returns {void}
 */
export function initUI() {
    // ПРОВЕРКА НАЛИЧИЯ JQUERY:
    // typeof jQuery === 'undefined' — безопасный способ проверки существования глобальной переменной.
    // Если написать просто `if (!jQuery)`, при отсутствии jQuery выбросится ReferenceError.
    // typeof всегда возвращает строку и никогда не бросает ошибку, даже для необъявленных переменных.
    if (typeof jQuery === 'undefined') {
        console.error('[UI] jQuery not loaded. Components skipped.');
        return;
    }

    // ПРОВЕРКА ГОТОВНОСТИ DOM:
    // document.readyState может быть 'loading', 'interactive' или 'complete'.
    // Если состояние НЕ 'loading', значит DOM уже готов к манипуляциям.
    const isDOMReady = document.readyState !== 'loading';

    if (isDOMReady) {
        // DOM уже готов (скрипт подключен в конце <body> или с атрибутом defer) —
        // запускаем инициализацию немедленно, не дожидаясь дополнительных событий.
        UI.init();
    } else {
        // DOM еще грузится (скрипт в <head> без defer) — ждем события DOMContentLoaded
        // через обертку jQuery.ready(). Это гарантирует, что все HTML-элементы доступны.
        jQuery(document).ready(() => UI.init());
    }
}

/**
 * Повторная инициализация компонентов после динамической подгрузки контента.
 *
 * КОГДА ИСПОЛЬЗОВАТЬ:
 * Если часть страницы обновляется через AJAX (например, таблица с пагинацией,
 * модальные окна, подгружаемые по клику), новые DOM-элементы могут содержать
 * кнопки с классами типа .js-add-subject, которые еще не имеют обработчиков событий.
 *
 * Однако ВАЖНО ПОНИМАТЬ:
 * В текущей архитектуре большинство компонентов используют ДЕЛЕГИРОВАНИЕ СОБЫТИЙ
 * через $(document).on('click', '.selector', handler). Это значит, что обработчики
 * уже навешаны на document и автоматически работают для любых новых элементов.
 * Поэтому полная реинициализация обычно НЕ нужна — делегирование уже решает проблему.
 *
 * reinitUI() может понадобиться, если:
 * - Компонент инициализирует себя только при наличии определенного DOM-элемента
 *   (например, если модалка подгружается динамически через AJAX)
 * - Нужно перезапустить компонент с новыми настройками
 *
 * ТЕКУЩАЯ РЕАЛИЗАЦИЯ:
 * Метод просто вызывает UI.init() повторно. Благодаря проверке _initialized в каждом компоненте,
 * уже инициализированные компоненты пропустят повторную инициализацию (паттерн Singleton).
 * Только те компоненты, которые еще не были инициализированы (например, подгруженные через AJAX),
 * выполнят свой init().
 *
 * @param {HTMLElement|jQuery|string} [context] - Контекст для точечной реинициализации.
 *        В текущей реализации НЕ используется. Зарезервирован для будущей оптимизации,
 *        чтобы реинициализировать только компоненты внутри определенного DOM-поддерева,
 *        а не все компоненты на странице.
 * @returns {void}
 */
export function reinitUI(context) {
    // Проверка наличия jQuery — аналогично initUI()
    if (typeof jQuery === 'undefined') {
        console.error('[UI] jQuery not loaded.');
        return;
    }

    // Если передан контекст, предупреждаем, что эта функциональность еще не реализована.
    // Это полезно для разработчиков: они видят в консоли, что их запрос не обработан,
    // и понимают, что вызывается полная реинициализация.
    if (context) {
        console.warn('[UI] Context-based reinit not implemented. Full reinit called.');
    }

    // ПОЛНАЯ РЕИНИЦИАЛИЗАЦИЯ:
    // Вызываем UI.init() повторно. Благодаря паттерну Singleton (_initialized флаг)
    // в каждом компоненте, уже инициализированные компоненты проигнорируют повторный вызов.
    // Это безопасно, но может быть неэффективно при большом количестве компонентов.
    //
    // ВНИМАНИЕ: если какой-то компонент НЕ использует паттерн Singleton,
    // его обработчики событий могут продублироваться. Всегда проверяйте,
    // что ваши компоненты защищены от повторной инициализации.
    UI.init();
}