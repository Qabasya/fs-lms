/**
 * @module utils
 * @description Набор общих утилит для плагина FS-LMS.
 *              Содержит функции для:
 *              - Безопасности (экранирование HTML, защита от XSS)
 *              - Управления состоянием кнопок (блокировка при AJAX-запросах)
 *              - Отображения уведомлений (в стиле WordPress Admin Notice и toast-сообщений)
 *              - Обработки ошибок API (логирование + показ пользователю)
 *              - Транслитерации и генерации slug из кириллицы
 *              - Анимации удаления строк таблиц
 *
 *              Все функции спроектированы как переиспользуемые и не зависят
 *              от конкретных компонентов, что позволяет использовать их в любом модуле плагина.
 *
 * @requires jQuery
 * @requires toast - модуль для показа toast-уведомлений
 */

import { showToast } from './toast.js';

const $ = window.jQuery || jQuery;

/**
 * Экранирует строку для безопасной вставки в HTML.
 *
 * ЗАЧЕМ ЭТО НУЖНО (Защита от XSS):
 * Если пользователь введет в поле название "Учебный период <script>alert('XSS')</script>",
 * и мы вставим это напрямую в HTML через .html() или шаблонные строки,
 * браузер выполнит JavaScript-код, что приведет к краже данных или поломке интерфейса.
 *
 * escapeHtml() заменяет специальные символы на их HTML-сущности:
 * - '<' → '&lt;' (отображается как текст, а не как начало тега)
 * - '>' → '&gt;'
 * - '&' → '&amp;'
 * - '"' → '&quot;'
 * - "'" → '&#039;'
 *
 * ПРАВИЛО: Всегда используйте escapeHtml() перед вставкой пользовательских данных
 * в HTML через .html() или шаблонные строки. Если используете .text() — экранирование
 * не нужно, jQuery делает это автоматически.
 *
 * @param {string} str - Исходная строка (может содержать опасные символы)
 * @returns {string} Экранированная строка, безопасная для вставки в HTML
 */
export function escapeHtml(str) {
    // Карта замен: ключ — опасный символ, значение — безопасная HTML-сущность
    const map = {
        '&': '&amp;',   // Амперсанд заменяется ПЕРВЫМ, чтобы не экранировать '&' в других сущностях
        '<': '&lt;',    // Меньше чем (начало HTML-тега)
        '>': '&gt;',    // Больше чем (конец HTML-тега)
        '"': '&quot;',  // Двойная кавычка (используется в атрибутах HTML)
        "'": '&#039;',  // Одинарная кавычка (альтернатива для атрибутов)
    };

    // String(str) гарантирует, что даже если передано число или null,
    // мы получим строку и не упадем с ошибкой.
    // Регулярное выражение /[&<>"']/g находит все вхождения опасных символов (флаг g = global).
    // Функция замены (m) => map[m] подставляет для каждого найденного символа его безопасный аналог.
    return String(str).replace(/[&<>"']/g, (m) => map[m]);
}

/**
 * Переключает кнопку в режим загрузки или возвращает в обычное состояние.
 *
 * ПАТТЕРН: Сохранение оригинального текста через .data().
 * Когда мы блокируем кнопку и меняем её текст на "..." или "Сохранение...",
 * нам нужно потом восстановить исходный текст ("Создать", "Удалить" и т.д.).
 * Вместо того чтобы хардкодить этот текст в коде, мы сохраняем его в data-атрибут
 * при первом вызове toggleButton(true, ...), а при toggleButton(false) — восстанавливаем.
 *
 * Это делает функцию универсальной: она работает с любой кнопкой,
 * не зная заранее, какой у неё был текст.
 *
 * ВАЖНО: используем .prop('disabled', true), а не .attr('disabled', true).
 * .prop() работает с реальным свойством DOM-элемента (текущее состояние),
 * .attr() работает с HTML-атрибутом (изначальное состояние в разметке).
 * Для булевых атрибутов (disabled, checked, selected) всегда используйте .prop().
 *
 * @param {jQuery} $btn - jQuery-объект кнопки
 * @param {boolean} isLoading - true: блокировка и текст загрузки, false: восстановление
 * @param {string} [loadingText='...'] - Текст для режима загрузки (по умолчанию многоточие)
 */
export function toggleButton($btn, isLoading, loadingText = '...') {
    // ЗАЩИТА ОТ НЕКОРРЕКТНОГО ВЫЗОВА:
    // Проверяем, что передан именно jQuery-объект (у него есть свойство .jquery).
    // Если передать обычный DOM-элемент или null, функция упадет с ошибкой.
    // Эта проверка дает понятное сообщение в консоли вместо cryptic "Cannot read property of undefined".
    if (!$btn || !$btn.jquery) {
        console.error('[Utils.toggleButton] Invalid jQuery object', $btn);
        return;
    }

    if (isLoading) {
        // ВХОД В РЕЖИМ ЗАГРУЗКИ:
        // 1. Сохраняем текущий HTML-кнопки в data-атрибут 'original-text'.
        //    Используем .html(), а не .text(), чтобы сохранить возможную HTML-разметку внутри кнопки
        //    (например, иконки <span class="dashicons"></span>).
        // 2. Блокируем кнопку через .prop('disabled', true) — защита от двойного клика.
        // 3. Меняем текст на индикатор загрузки (по умолчанию '...').
        $btn.data('original-text', $btn.html())
            .prop('disabled', true)
            .text(loadingText);
    } else {
        // ВЫХОД ИЗ РЕЖИМА ЗАГРУЗКИ:
        // 1. Разблокируем кнопку.
        // 2. Восстанавливаем оригинальный HTML из data-атрибута.
        // Если .data('original-text') не существует (например, toggleButton(false) вызван
        // без предварительного toggleButton(true)), .html() получит undefined и очистит кнопку.
        // Это edge case, но в норме функция всегда вызывается парами.
        $btn.prop('disabled', false)
            .html($btn.data('original-text'));
    }
}

/**
 * Логирует ошибку API и показывает уведомление пользователю.
 *
 * ПАТТЕРН: Централизованная обработка ошибок.
 * Вместо того чтобы в каждом AJAX-обработчике писать:
 *   console.error(err);
 *   showToast('Ошибка сервера', 'error');
 *
 * Мы используем единую функцию apiError(), которая:
 * 1. Всегда логирует ошибку в консоль (для разработчиков)
 * 2. Показывает уведомление пользователю (если не передан silent: true)
 * 3. Позволяет кастомизировать показ через onNotify (например, показать ошибку в модалке, а не toast)
 *
 * @param {Error|Object|string} error - Объект ошибки, ответ сервера или текст ошибки
 * @param {Object} [options] - Настройки
 * @param {boolean} [options.silent=false] - Не показывать уведомление пользователю (только лог в консоль)
 * @param {Function} [options.onNotify] - Кастомная функция для показа ошибки (вместо toast).
 *        Получает параметры (message, type). Используется, когда нужно показать ошибку
 *        в специфичном месте (например, внутри модального окна через showModalError).
 */
export function apiError(error, options = {}) {
    const { silent = false, onNotify = null } = options;

    // Логируем ошибку в консоль для разработчиков.
    // console.error() выделяет сообщение красным цветом в DevTools,
    // что упрощает поиск проблем при отладке.
    console.error('FS-LMS API Error:', error);

    if (!silent) {
        // Используем ОДНО и то же сообщение для пользователя, независимо от типа ошибки.
        // Это намеренное решение: не показываем технические детали (статусы, коды) конечному пользователю,
        // так как они непонятны и могут вызвать панику. Разработчики видят детали в консоли.
        const message = 'Произошла ошибка при связи с сервером.';

        if (typeof onNotify === 'function') {
            // Если передана кастомная функция показа — используем её.
            // Это позволяет, например, показать ошибку внутри модалки через showModalError.
            onNotify(message, 'error');
        } else {
            // По умолчанию используем toast-уведомление (ненавязчивое, исчезает само).
            showToast(message, 'error');
        }
    }
}

/**
 * Расширенное переключение кнопки с поддержкой success-состояния и колбэков.
 *
 * ОТЛИЧИЯ ОТ toggleButton():
 * 1. Поддержка successText — временный текст после успешного завершения
 *    (например, "✓ Сохранено" перед возвратом к "Сохранить").
 * 2. Колбэк onComplete, вызываемый после завершения всех анимаций.
 * 3. Настраиваемая длительность показа success-текста.
 *
 * СЦЕНАРИИ ИСПОЛЬЗОВАНИЯ:
 * - Кнопка "Сохранить" → при клике "Сохранение..." → после успеха "✓ Сохранено" (2 сек) → "Сохранить"
 * - Кнопка "Удалить" → при клике "Удаление..." → после успеха вызов onComplete для закрытия модалки
 *
 * @param {jQuery} $btn - jQuery-объект кнопки
 * @param {boolean} isLoading - Флаг режима загрузки
 * @param {Object} [options] - Настройки
 * @param {string} [options.loadingText='...'] - Текст для режима загрузки
 * @param {string} [options.successText] - Временный текст после успеха (например, '✓ Готово')
 * @param {number} [options.successDuration=2000] - Длительность показа success-текста в миллисекундах
 * @param {Function} [options.onComplete] - Колбэк после завершения (после возврата к оригинальному тексту)
 */
export function toggleButtonExtended($btn, isLoading, options = {}) {
    const {
        loadingText = '...',
        successText = null,
        successDuration = 2000,
        onComplete = null
    } = options;

    if (isLoading) {
        // Вход в режим загрузки — аналогично toggleButton()
        $btn.data('original-text', $btn.html())
            .prop('disabled', true)
            .text(loadingText);
    } else {
        // Выход из режима загрузки
        $btn.prop('disabled', false)
            .html($btn.data('original-text'));

        if (successText) {
            // ЕСЛИ УКАЗАН SUCCESS-ТЕКСТ:
            // 1. Сохраняем текущий (восстановленный оригинальный) текст
            const originalText = $btn.html();
            // 2. Меняем текст на success-вариант (например, "✓ Сохранено")
            $btn.text(successText);
            // 3. Через successDuration мс возвращаем оригинальный текст и вызываем колбэк
            setTimeout(() => {
                $btn.html(originalText);
                if (typeof onComplete === 'function') onComplete();
            }, successDuration);
        } else if (typeof onComplete === 'function') {
            // Если success-текст не указан, вызываем колбэк сразу
            onComplete();
        }
    }
}

/**
 * Расширенная обработка ошибки API с извлечением человекочитаемого сообщения.
 *
 * ОТЛИЧИЯ ОТ apiError():
 * 1. Пытается извлечь конкретное сообщение об ошибке из разных форматов ответа:
 *    - Строка → используется как есть
 *    - Error объект → error.message
 *    - jQuery AJAX ответ → error.responseJSON.message или error.statusText
 * 2. Логирует дополнительные детали в console.debug (доступны только в режиме разработки).
 * 3. Поддерживает колбэк onError для передачи сообщения в вызывающий код.
 * 4. Возвращает человекочитаемое сообщение, чтобы вызывающий код мог использовать его.
 *
 * @param {Error|Object|string} error - Объект ошибки (может быть Error, jQuery jqXHR, строка или объект)
 * @param {Object} [options] - Настройки
 * @param {boolean} [options.silent=false] - Не показывать уведомление пользователю
 * @param {Function} [options.onError] - Колбэк с текстом ошибки (для передачи в другие компоненты)
 * @param {Function} [options.onNotify] - Кастомная функция для показа ошибки
 * @returns {string} Человекочитаемое сообщение об ошибке
 */
export function apiErrorEnhanced(error, options = {}) {
    const { silent = false, onError = null, onNotify = null } = options;
    let userMessage = 'Произошла ошибка при связи с сервером.';

    // ИЗВЛЕЧЕНИЕ СООБЩЕНИЯ ИЗ РАЗНЫХ ФОРМАТОВ:
    // AJAX-ответы от WordPress могут приходить в разных форматах,
    // поэтому мы пытаемся достать сообщение из всех возможных мест.
    if (typeof error === 'string') {
        // Если передана просто строка — используем её как есть
        userMessage = error;
    } else if (error && typeof error === 'object') {
        // Цепочка fallback-значений через ||:
        // 1. error.message — стандартное свойство Error объекта
        // 2. error.responseJSON?.message — тело ответа WordPress (если сервер вернул JSON с message)
        // 3. error.statusText — HTTP-статус (например, 'Not Found', 'Internal Server Error')
        // 4. userMessage — дефолтное сообщение, если ничего не найдено
        userMessage = error.message ||
            error.responseJSON?.message ||
            error.statusText ||
            userMessage;

        // ЛОГИРОВАНИЕ ДЕТАЛЕЙ ДЛЯ РАЗРАБОТЧИКОВ:
        // console.debug() показывает сообщения только когда в DevTools включен уровень "Verbose".
        // Это полезно для детальной отладки, но не засоряет обычную консоль.
        if (error.responseJSON?.data) {
            console.debug('[API Error Details]:', error.responseJSON.data);
        }
        if (error.code) {
            console.debug('[API Error Code]:', error.code);
        }
    }

    console.error('[FS-LMS API Error]:', error);
    console.debug('[User Message]:', userMessage);

    if (!silent) {
        if (typeof onNotify === 'function') {
            onNotify(userMessage, 'error');
        } else {
            showToast(userMessage, 'error');
        }
    }

    // Вызываем onError-колбэк, если передан.
    // Это позволяет вызывающему коду получить сообщение и, например,
    // показать его в специфичном месте (в модалке, в определенном блоке страницы).
    if (typeof onError === 'function') {
        onError(userMessage);
    }

    return userMessage;
}

/**
 * Показывает уведомление в стиле WordPress Admin Notice.
 *
 * ОТЛИЧИЯ ОТ showToast():
 * - showNotice() — встраивается в контент страницы (как стандартные уведомления WordPress),
 *   используется для важных сообщений, связанных с конкретным действием на странице.
 * - showToast() — всплывает в углу экрана, используется для быстрых, ненавязчивых уведомлений.
 *
 * ОСОБЕННОСТИ:
 * - Автоматически удаляет предыдущие уведомления (чтобы не накапливались)
 * - Поддерживает 4 типа: success, error, warning, info (с разными цветами и заголовками)
 * - Для success по умолчанию включено автозакрытие
 * - Кнопка закрытия с анимацией (fade → slide → remove)
 * - Экранирование сообщения для защиты от XSS
 *
 * @param {string} message - Текст сообщения
 * @param {'success'|'error'|'warning'|'info'} [type='info'] - Тип уведомления (определяет цвет и заголовок)
 * @param {jQuery|string|null} [$container=null] - Контейнер для вставки (по умолчанию $('body')).
 *        Можно передать селектор ('.wrap') или jQuery-объект для вставки уведомления
 *        в конкретное место страницы (например, внутри таблицы).
 * @param {Object} [options] - Дополнительные настройки
 * @param {boolean} [options.autoDismiss=true] - Автозакрытие для типа 'success'
 * @param {number} [options.autoDismissDelay=1000] - Задержка автозакрытия в миллисекундах
 * @param {boolean} [options.escape=true] - Экранировать сообщение для защиты от XSS.
 *        Отключайте только если message уже содержит безопасный HTML, который нужно отрендерить.
 * @returns {jQuery} Созданный элемент уведомления (можно использовать для отслеживания событий)
 */
export function showNotice(message, type = 'info', $container = null, options = {}) {
    const { autoDismiss = true, autoDismissDelay = 1000, escape = true } = options;

    // НОРМАЛИЗАЦИЯ КОНТЕЙНЕРА:
    // Пользователь может передать null, строку-селектор, DOM-элемент или jQuery-объект.
    // Мы приводим всё к jQuery-объекту для единообразия.
    if (!$container) {
        $container = $('body');
    } else if (!($container instanceof $)) {
        // Если передан не jQuery-объект, оборачиваем его в $()
        $container = $($container);
    }
    if (!$container.length) {
        // Если контейнер не найден в DOM, fallback на body
        $container = $('body');
    }

    // УДАЛЕНИЕ ПРЕДЫДУЩИХ УВЕДОМЛЕНИЙ:
    // Чтобы уведомления не накапливались на странице, удаляем все существующие .fs-notice.
    // Это особенно важно при быстром последовательном вызове showNotice() (например, при валидации).
    $container.find('.fs-notice').remove();

    // Карта заголовков для разных типов уведомлений.
    // Заголовки делают уведомление более информативным: пользователь сразу видит,
    // успех это или ошибка, даже не читая весь текст.
    const labels = {
        success: 'Готово!',
        error: 'Ошибка:',
        warning: 'Внимание:',
        info: 'Информация:',
    };
    const title = labels[type] || labels.info;

    // Экранируем сообщение, если не отключено явно.
    // Это защищает от XSS, если в message есть пользовательский ввод.
    const safeMessage = escape ? escapeHtml(message) : message;

    // СОЗДАНИЕ HTML УВЕДОМЛЕНИЯ:
    // Используем стандартные классы WordPress (notice, notice-${type}, is-dismissible),
    // чтобы уведомление выглядело нативно в админке WordPress.
    // style="margin: 10px 0;" добавляет отступы, так как уведомления обычно вставляются
    // в контейнеры без стандартных margin WordPress.
    const $notice = $(`
        <div class="notice notice-${type} is-dismissible fs-notice" style="margin: 10px 0;">
            <p><strong>${title}</strong> ${safeMessage}</p>
            <button type="button" class="notice-dismiss">
                <span class="screen-reader-text">Закрыть</span>
            </button>
        </div>
    `);

    // ОБРАБОТЧИК КНОПКИ ЗАКРЫТИЯ:
    // Используем делегирование событий (.on('click', '.notice-dismiss', ...)),
    // хотя в данном случае это не обязательно (кнопка существует с момента создания).
    // Это просто привычный паттерн для работы с динамическими элементами.
    // Анимация закрытия: fadeTo (уменьшение прозрачности) → slideUp (сворачивание высоты) → remove.
    // Это более плавная анимация, чем просто fadeOut, так как уведомление "сворачивается",
    // а не просто исчезает, что предотвращает "прыжок" остального контента.
    $notice.on('click', '.notice-dismiss', function () {
        $notice.fadeTo(100, 0, function () {
            $notice.slideUp(100, function () {
                $(this).remove();
            });
        });
    });

    // ВСТАВКА В КОНТЕЙНЕР:
    // Используем .prepend(), чтобы уведомление появилось вверху контейнера,
    // а не внизу. Это соответствует UX-паттерну: важные сообщения — на видном месте.
    $container.prepend($notice);

    // АВТОЗАКРЫТИЕ ДЛЯ SUCCESS:
    // Успешные уведомления обычно не требуют внимания пользователя,
    // поэтому автоматически закрываем их через autoDismissDelay мс.
    // Для error/warning автозакрытие отключено по умолчанию — пользователь должен
    // сам закрыть их, чтобы убедиться, что он прочитал сообщение.
    if (type === 'success' && autoDismiss) {
        setTimeout(() => {
            // Проверяем, что уведомление всё ещё видимо (пользователь мог закрыть его вручную).
            if ($notice.is(':visible')) {
                // Программно кликаем по кнопке закрытия, чтобы сработала анимация.
                $notice.find('.notice-dismiss').trigger('click');
            }
        }, autoDismissDelay);
    }

    return $notice;
}

/**
 * Показывает сообщение об ошибке внутри модального окна (в .fs-lms-modal-body).
 *
 * ПАТТЕРН: Встроенная ошибка вместо toast.
 * Для модальных окон toast-уведомления не всегда подходят — они могут перекрыться самой модалкой.
 * Вместо этого мы вставляем ошибку прямо в тело модалки, чтобы пользователь сразу видел,
 * что именно пошло не так в контексте его действия.
 *
 * ОСОБЕННОСТИ:
 * - Если элемент .fs-modal-error уже существует — обновляем его текст, а не создаем новый.
 *   Это предотвращает накопление ошибок при повторных попытках.
 * - Используем .show(), если элемент был скрыт предыдущим вызовом clearModalError().
 *
 * @param {string} message - Текст ошибки
 * @param {jQuery} $modal - jQuery-объект модального окна
 */
export function showModalError( message, $modal ) {
    const $body = $modal.find( '.fs-lms-modal-body' );
    let $err = $body.find( '.fs-modal-error' );

    // Если элемент ошибки еще не существует — создаем его и добавляем в начало тела модалки.
    // .prepend() используется, чтобы ошибка была на видном месте, над формой.
    if ( ! $err.length ) {
        $err = $( '<p class="fs-modal-error"></p>' );
        $body.prepend( $err );
    }

    // Устанавливаем текст и показываем элемент (на случай, если он был скрыт через clearModalError).
    // .text() автоматически экранирует HTML, что защищает от XSS.
    $err.text( message ).show();
}

/**
 * Скрывает сообщение об ошибке внутри модального окна.
 *
 * КОГДА ИСПОЛЬЗОВАТЬ:
 * - Перед новой попыткой сохранения (чтобы очистить старые ошибки)
 * - При изменении пользователем данных формы (ошибка больше не актуальна)
 *
 * ВАЖНО: мы используем .hide().text(''), а не .remove().
 * Это сохраняет элемент в DOM, чтобы при следующем вызове showModalError()
 * не пришлось создавать его заново. Это эффективнее и предотвращает "прыжки" интерфейса.
 *
 * @param {jQuery} $modal - jQuery-объект модального окна
 */
export function clearModalError( $modal ) {
    // .hide() устанавливает display: none, .text('') очищает содержимое.
    // Элемент остается в DOM, готовый к повторному использованию.
    $modal.find( '.fs-modal-error' ).hide().text( '' );
}

/**
 * Транслитерирует строку (кириллица → латиница) и превращает её в slug.
 *
 * ЗАЧЕМ ЭТО НУЖНО:
 * В WordPress многие сущности (таксономии, типы постов, ключи предметов) используют slug —
 * строку из латинских букв, цифр и подчеркиваний. Когда пользователь вводит название
 * на русском языке (например, "Обработка персональных данных"), нам нужно автоматически
 * сгенерировать из него slug ("obrabotka_personalnyh_dannyh").
 *
 * АЛГОРИТМ:
 * 1. Приводим к нижнему регистру
 * 2. Заменяем кириллические буквы на латинские аналоги по карте транслитерации
 * 3. Заменяем все не-латинские/не-цифровые символы на подчеркивания
 * 4. Удаляем подчеркивания в начале и конце строки
 * 5. Ограничиваем длину 64 символами (стандартное ограничение для slug в WordPress)
 *
 * @param {string} str - Исходная строка (может содержать кириллицу, пробелы, спецсимволы)
 * @returns {string} slug из строчных латинских букв, цифр и подчёркиваний
 *
 * @example
 * toSlug('Обработка ПД')      // → 'obrabotka_pd'
 * toSlug('Hello World!')      // → 'hello_world'
 * toSlug('Математика 5 класс') // → 'matematika_5_klass'
 * toSlug('---Тест---')         // → 'test' (лишние подчеркивания по краям удалены)
 */
export function toSlug( str ) {
    // Карта транслитерации: кириллическая буква → латинская транслитерация.
    // Используется стандартная система транслитерации, принятая в WordPress и веб-разработке.
    // Обратите внимание на многосимвольные замены: 'щ' → 'shch', 'ж' → 'zh'.
    // Это обеспечивает читаемость slug (сравните: 'shch' vs 'sch' vs 'sc').
    const map = {
        а:'a',б:'b',в:'v',г:'g',д:'d',е:'e',ё:'yo',ж:'zh',з:'z',и:'i',
        й:'y',к:'k',л:'l',м:'m',н:'n',о:'o',п:'p',р:'r',с:'s',т:'t',
        у:'u',ф:'f',х:'h',ц:'ts',ч:'ch',ш:'sh',щ:'shch',ъ:'',ы:'y',
        ь:'',э:'e',ю:'yu',я:'ya',
    };

    return String( str )
        .toLowerCase() // ШАГ 1: приводим к нижнему регистру
        // ШАГ 2: заменяем кириллические буквы.
        // /[а-яё]/g находит все буквы русского алфавита (включая ё).
        // Функция замены (c) => map[c] ?? c подставляет транслитерацию из карты.
        // Оператор ?? c — fallback: если буквы нет в карте (что невозможно для [а-яё],
        // но это защита на будущее), оставляем её как есть.
        .replace( /[а-яё]/g, ( c ) => map[ c ] ?? c )
        // ШАГ 3: заменяем все "плохие" символы на подчеркивания.
        // /[^a-z0-9]+/g находит последовательности символов, НЕ являющихся латиницей или цифрами.
        // Флаг + означает "один или более", чтобы "Hello   World" превратился в "hello_world",
        // а не в "hello___world".
        .replace( /[^a-z0-9]+/g, '_' )
        // ШАГ 4: удаляем подчеркивания в начале и конце.
        // /^_+|_+$/g находит подчеркивания в начале (^) ИЛИ в конце ($) строки.
        // Это нужно, так как после шага 3 строка "---Тест---" превратилась бы в "___test___".
        .replace( /^_+|_+$/g, '' )
        // ШАГ 5: ограничиваем длину 64 символами.
        // Это стандартное ограничение для slug в WordPress (поле post_name в БД имеет тип varchar(200),
        // но для ключей таксономий и других сущностей обычно используют 64 символа).
        .slice( 0, 64 );
}

/**
 * Анимирует удаление строки таблицы: подсвечивает красным и плавно скрывает.
 *
 * UX-ПАТТЕРН: Визуальная обратная связь при деструктивных действиях.
 * Когда пользователь удаляет строку, резкое исчезновение может дезориентировать.
 * Эта функция:
 * 1. Подсвечивает строку красным (сигнал: "это удаляется")
 * 2. Плавно скрывает её через fadeOut (400мс)
 * 3. Удаляет из DOM после завершения анимации
 * 4. Вызывает колбэк onRemoved, чтобы вызывающий код мог, например,
 *    перезагрузить таблицу или показать "Записей не найдено"
 *
 * @param {jQuery} $row - jQuery-объект строки таблицы (обычно <tr>)
 * @param {Function} [onRemoved] - Колбэк после удаления из DOM.
 *        Используется для обновления интерфейса: проверки пустой таблицы,
 *        обновления счетчиков, показа альтернативного контента.
 */
export function fadeDeleteRow($row, onRemoved) {
    // Устанавливаем красный фон через .css().
    // .fadeOut(400, ...) — анимация исчезновения за 400мс.
    // Колбэк внутри fadeOut вызывается ПОСЛЕ завершения анимации,
    // что гарантирует плавный переход без "прыжков".
    $row.css('background', '#ff8d8d').fadeOut(400, function () {
        // Удаляем элемент из DOM после завершения анимации.
        // Если не вызвать .remove(), элемент останется в DOM (просто невидимый),
        // что приведет к утечкам памяти при массовом удалении.
        $(this).remove();

        // Вызываем колбэк, если он передан и является функцией.
        // Проверка typeof нужна для защиты от случайной передачи не-функции.
        if (typeof onRemoved === 'function') onRemoved();
    });
}