/**
 * @module toast
 * @description Модуль для отображения всплывающих уведомлений (toast-сообщений).
 *              Реализует систему ненавязчивых уведомлений, которые появляются в углу экрана,
 *              автоматически исчезают через заданное время и могут быть закрыты вручную.
 *
 *              Ключевые особенности:
 *              - Ленивая инициализация контейнера (создается только при первом вызове)
 *              - Поддержка 4 типов уведомлений: error, success, warning, info
 *              - Автоматическое скрытие с настраиваемой длительностью
 *              - Пауза таймера при наведении мыши (UX-паттерн "pause on hover")
 *              - Доступность (a11y) через ARIA-атрибуты для скринридеров
 *              - CSS-анимация появления/исчезновения через классы
 *
 * @requires jQuery
 */

const $ = jQuery;

/**
 * Кэшированная ссылка на контейнер всех toast-уведомлений.
 * Хранится в замыкании модуля, чтобы не пересоздавать контейнер при каждом вызове showToast().
 * @private
 * @type {jQuery|null}
 */
let $container = null;

/**
 * Получение или создание контейнера для toast-уведомлений.
 *
 * ПАТТЕРН: Ленивая инициализация (Lazy Initialization).
 * Контейнер создается только при первом вызове showToast(), а не при загрузке скрипта.
 * Это оптимизирует производительность: если уведомления никогда не показываются,
 * лишний DOM-элемент не создается.
 *
 * Также проверяется, что ранее созданный контейнер всё ещё существует в DOM.
 * Если он был удален (например, сторонним скриптом), создается новый.
 *
 * ARIA-АТРИБУТЫ ДЛЯ ДОСТУПНОСТИ:
 * - aria-live="polite" — скринридеры будут зачитывать новые уведомления,
 *   но не прерывая текущее чтение. "polite" означает "вежливо ждать очереди".
 *   (Альтернатива "assertive" — прерывает немедленно, используется для критических ошибок).
 * - aria-atomic="false" — скринридер будет зачитывать только новые добавленные элементы,
 *   а не весь контейнер целиком при каждом изменении.
 *
 * @private
 * @returns {jQuery} jQuery-объект контейнера для toast-уведомлений.
 */
function getContainer() {
    // Проверяем, что контейнер существует в кэше И присутствует в DOM.
    // Проверка .length нужна, так как $container может быть jQuery-объектом,
    // указывающим на уже удаленный из DOM элемент (тогда .length === 0).
    if ( ! $container || ! $container.length ) {
        // Создаем новый контейнер и добавляем его в конец <body>.
        // Позиционирование контейнера (обычно fixed, right: 20px, bottom: 20px)
        // задается через CSS-класс .fs-toast-container.
        $container = $( '<div class="fs-toast-container" aria-live="polite" aria-atomic="false"></div>' );
        $( 'body' ).append( $container );
    }
    return $container;
}

/**
 * Скрытие и удаление toast-уведомления с CSS-анимацией.
 *
 * АЛГОРИТМ РАБОТЫ:
 * 1. Удаляем класс 'fs-toast--visible', что запускает CSS-transition исчезновения
 *    (обычно opacity 1→0 и transform translateY).
 * 2. Ждем завершения анимации через setTimeout (300мс — должно совпадать с transition-duration в CSS).
 * 3. Полностью удаляем элемент из DOM через .remove(), чтобы освободить память.
 *
 * ВАЖНО: Если не вызвать .remove(), элементы будут накапливаться в DOM,
 * что приведет к утечкам памяти и замедлению работы страницы при большом количестве уведомлений.
 *
 * @private
 * @param {jQuery} $toast - jQuery-объект toast-уведомления, которое нужно скрыть.
 */
function dismiss( $toast ) {
    // Запускаем анимацию исчезновения (CSS-transition)
    $toast.removeClass( 'fs-toast--visible' );

    // Удаляем элемент из DOM после завершения анимации.
    // Таймаут 300мс должен совпадать с transition-duration в CSS для класса .fs-toast.
    setTimeout( () => $toast.remove(), 300 );
}

/**
 * Показывает всплывающее toast-уведомление.
 *
 * ОСОБЕННОСТИ:
 * - Автоматически создает контейнер при первом вызове (ленивая инициализация)
 * - Поддерживает 4 типа уведомлений с разными цветами и длительностью показа по умолчанию:
 *   * error (красный) — 4000мс
 *   * warning (желтый) — 4000мс
 *   * success (зеленый) — 2500мс
 *   * info (синий) — 2500мс
 * - Ошибки и предупреждения показываются дольше, так как требуют больше времени на осмысление
 * - Таймер автоматически приостанавливается при наведении мыши (UX-паттерн "pause on hover")
 * - Пользователь может закрыть уведомление вручную кликом по крестику
 *
 * ПАТТЕРН: Force Reflow для CSS-анимации.
 * Перед добавлением класса 'fs-toast--visible' мы читаем offsetHeight,
 * чтобы браузер увидел промежуточное состояние и применил CSS-transition.
 * Без этого модалка появилась бы мгновенно, без анимации.
 *
 * @param {string} message - Текст уведомления. Будет безопасно вставлен через .text(),
 *                           что защищает от XSS-атак (HTML-теги отобразятся как текст).
 * @param {'error'|'success'|'warning'|'info'} [type='error'] - Тип уведомления.
 *        Определяет цвет и длительность показа по умолчанию.
 * @param {number|null} [duration=null] - Длительность показа в миллисекундах.
 *        Если null, используется значение по умолчанию для данного типа (4000 или 2500мс).
 *        Если 0 или отрицательное число, уведомление не будет автоматически скрыто
 *        (но пользователь всё равно может закрыть его вручную).
 * @returns {jQuery} jQuery-объект созданного toast-уведомления.
 *                   Может быть полезен для дальнейшей кастомизации или отслеживания событий.
 *
 * @example
 * // Простое уведомление об ошибке (покажется 4 секунды)
 * showToast('Не удалось сохранить данные');
 *
 * @example
 * // Уведомление об успехе с кастомной длительностью
 * showToast('Изменения сохранены', 'success', 3000);
 *
 * @example
 * // Постоянное уведомление (не исчезнет само, только по клику на крестик)
 * showToast('Критическая ошибка системы', 'error', 0);
 */
export function showToast( message, type = 'error', duration = null ) {
    // ВЫЧИСЛЕНИЕ ДЛИТЕЛЬНОСТИ ПОКАЗА:
    // Оператор ?? (nullish coalescing) подставляет значение по умолчанию,
    // только если duration равен null или undefined.
    // Это позволяет передать 0 для отключения автозакрытия (в отличие от ||, который среагировал бы на 0).
    // Для error и warning используем 4000мс (дольше), для success и info — 2500мс (быстрее).
    const auto = duration ?? ( type === 'error' || type === 'warning' ? 4000 : 2500 );

    // СОЗДАНИЕ HTML-СТРУКТУРЫ TOAST:
    // Используем шаблонные строки для динамической генерации разметки.
    // Класс 'fs-toast--${type}' определяет цвет уведомления (красный/зеленый/желтый/синий).
    // role="alert" — ARIA-атрибут, сообщающий скринридерам, что это важное уведомление.
    // aria-label="Закрыть" — описание кнопки закрытия для скринридеров,
    // так как визуально там только символ ×, который не читается.
    const $toast = $( `<div class="fs-toast fs-toast--${type}" role="alert">
        <span class="fs-toast__message"></span>
        <button type="button" class="fs-toast__close" aria-label="Закрыть">&times;</button>
    </div>` );

    // БЕЗОПАСНАЯ ВСТАВКА ТЕКСТА:
    // Используем .text() вместо .html(), чтобы защитить от XSS-атак.
    // Если в message есть HTML-теги или JavaScript-код, они будут отображены как обычный текст.
    $toast.find( '.fs-toast__message' ).text( message );

    // ДОБАВЛЕНИЕ В КОНТЕЙНЕР:
    // Получаем (или создаем) контейнер и добавляем в него новое уведомление.
    // Несколько уведомлений могут отображаться одновременно, располагаясь вертикально.
    getContainer().append( $toast );

    // ПАТТЕРН: Force Reflow для CSS-анимации.
    // Чтение offsetHeight заставляет браузер пересчитать layout,
    // создавая необходимую "паузу" между добавлением элемента и добавлением класса 'visible'.
    // Без этого CSS-transition не сработает, и уведомление появилось бы мгновенно.
    // void используется, чтобы явно указать, что возвращаемое значение не используется
    // (защита от предупреждений линтеров типа ESLint).
    void $toast[ 0 ].offsetHeight;

    // Запускаем анимацию появления (CSS-transition opacity 0→1, transform и т.д.)
    $toast.addClass( 'fs-toast--visible' );

    // АВТОМАТИЧЕСКОЕ СКРЫТИЕ ПО ТАЙМЕРУ:
    // Сохраняем ID таймера в переменную, чтобы иметь возможность отменить его позже
    // (при наведении мыши или ручном закрытии).
    let timer = setTimeout( () => dismiss( $toast ), auto );

    // ОБРАБОТЧИК КНОПКИ ЗАКРЫТИЯ:
    // При клике на крестик отменяем таймер и сразу скрываем уведомление.
    // clearTimeout(timer) нужен, чтобы таймер не сработал после того, как мы уже удалили элемент.
    // Это предотвращает возможные ошибки и утечки памяти.
    $toast.on( 'click', '.fs-toast__close', () => {
        clearTimeout( timer );
        dismiss( $toast );
    } );

    // UX-ПАТТЕРН: ПАУЗА ТАЙМЕРА ПРИ НАВЕДЕНИИ МЫШИ (Pause on Hover).
    // Когда пользователь наводит мышь на уведомление, он вероятно хочет его прочитать.
    // Мы приостанавливаем таймер, чтобы уведомление не исчезло прямо перед глазами пользователя.
    // Когда мышь убирается — таймер перезапускается с полной длительностью.
    // Это стандартный паттерн в современных UI-системах (используется в Material Design, Ant Design и т.д.).
    $toast.on( 'mouseenter', () => clearTimeout( timer ) );
    $toast.on( 'mouseleave', () => {
        // Перезапускаем таймер с полной длительностью.
        // ВАЖНО: мы не вычисляем оставшееся время, а просто запускаем новый таймер на полную длительность.
        // Это упрощает реализацию и обычно соответствует ожиданиям пользователей.
        timer = setTimeout( () => dismiss( $toast ), auto );
    } );

    // Возвращаем jQuery-объект уведомления, чтобы вызывающий код мог:
    // - Добавить дополнительные обработчики событий
    // - Кастомизировать уведомление после создания
    // - Отследить его в тестах
    return $toast;
}