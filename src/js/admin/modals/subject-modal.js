/**
 * @module SubjectModal
 * @description UI-компонент модального окна создания нового учебного предмета.
 *              Отвечает за:
 *              - Кэширование DOM-элементов для оптимизации производительности
 *              - Управление жизненным циклом модалки (открытие, закрытие, сброс)
 *              - Интеграцию с системой валидации формы (initFormValidation)
 *              - Обработку специфичных ошибок (дублирование ключа) через HTML5 Constraint Validation API
 *              - Сбор данных формы и уведомление внешних менеджеров через паттерн Pub/Sub
 *
 *              В отличие от модалок редактирования, эта модалка работает только в режиме создания.
 *              При каждом открытии форма полностью сбрасывается к исходному состоянию.
 *
 * @requires jQuery
 * @requires openModal, closeModal, bindEsc, unbindEsc - базовые утилиты управления модальными окнами
 * @requires initFormValidation - утилита инициализации комплексной валидации формы
 */

import { openModal, closeModal, bindEsc, unbindEsc } from '../modules/modal-base.js';
import { initFormValidation } from '../../common/validation-manager.js';

const $ = jQuery;

/**
 * UI-компонент модального окна создания учебного предмета.
 * Работает в связке с SubjectModalManager, который обрабатывает AJAX-запросы.
 */
export const SubjectModal = {
    /** @type {jQuery|null} Ссылка на основной контейнер модального окна */
    $modal: null,

    /** @type {Function[]} Массив колбэков, вызываемых при успешной валидации и отправке формы */
    _saveCallbacks: [],

    /** @type {boolean} Флаг для предотвращения повторной инициализации */
    _initialized: false,

    // Кэшированные jQuery-объекты полей формы.
    // Поиск элементов через $('#id') — относительно дорогая операция,
    // поэтому сохраняем ссылки один раз при инициализации.

    /** @type {jQuery} Поле ввода названия предмета */
    $nameInput: null,
    /** @type {jQuery} Поле ввода уникального ключа предмета (slug) */
    $keyInput: null,
    /** @type {jQuery} Поле ввода количества заданий */
    $tasksCountInput: null,
    /** @type {jQuery} Скрытое поле с nonce-токеном безопасности */
    $nonceInput: null,

    /** @type {jQuery} Кнопка отправки формы */
    $saveBtn: null,
    /** @type {jQuery} Форма модального окна */
    $form: null,

    /**
     * Функция комплексной валидации формы.
     * Возвращается утилитой initFormValidation и объединяет все правила валидации
     * (required, pattern, кастомные проверки и т.д.) в единый вызов.
     * @private
     * @type {Function}
     */
    _validateAll: null,

    /**
     * Инициализация компонента.
     * Выполняет проверку существования модалки, кэширует элементы и навешивает события.
     */
    init() {
        // Защита от повторной инициализации (паттерн Singleton).
        // Если скрипт подключен несколько раз или init() вызывается повторно,
        // это предотвратит дублирование обработчиков событий.
        if (this._initialized) return;

        this.$modal = $('#fs-subject-modal');
        if (!this.$modal.length) return; // Если модалки нет в DOM, прекращаем инициализацию

        this._initialized = true;
        this._cacheElements();
        this._bindEvents();
    },

    /**
     * Кэширование DOM-элементов.
     * Поиск элементов через jQuery — относительно дорогая операция.
     * Сохраняя ссылки один раз при инициализации, мы ускоряем последующую работу с формой.
     * @private
     */
    _cacheElements() {
        this.$nameInput       = $('#subj_name');
        this.$keyInput        = $('#subj_key');
        this.$tasksCountInput = $('#subj_tasks_count');
        this.$nonceInput      = this.$modal.find('input[name="security"]');
        this.$saveBtn         = this.$modal.find('button[type="submit"]');
        this.$form            = this.$modal.find('form');

        // ИНИЦИАЛИЗАЦИЯ ВАЛИДАЦИИ ФОРМЫ:
        // initFormValidation — внешняя утилита, которая настраивает комплексную валидацию
        // для всех полей формы (required, pattern, type, кастомные правила).
        // Она возвращает функцию-валидатор, которую мы вызываем при отправке формы.
        // Это позволяет централизованно управлять правилами валидации в одном месте
        // (в validation-manager.js), а не раскидывать их по всему коду модалки.
        this._validateAll = initFormValidation(this.$form[0]);
    },

    /**
     * Привязка обработчиков событий.
     * Используется неймспейсинг событий (`.fs`, `.add-subject`), чтобы при необходимости
     * можно было безопасно удалить только эти обработчики через .off('.fs'),
     * не затрагивая другие.
     * @private
     */
    _bindEvents() {
        // ДЕЛЕГИРОВАНИЕ СОБЫТИЙ ДЛЯ КНОПКИ ОТКРЫТИЯ МОДАЛКИ:
        // Обработчик навешан на document, но срабатывает только для элементов .js-add-subject.
        // Это необходимо, так как кнопка может быть добавлена в DOM динамически
        // (например, после AJAX-обновления списка предметов).
        // Неймспейс 'click.add-subject' позволяет точечно удалить этот обработчик.
        $(document).on('click.add-subject', '.js-add-subject', (e) => {
            e.preventDefault();
            this.open();
        });

        // Закрытие модалки при клике на фон, крестик, кнопку отмены или другие элементы закрытия.
        // Все эти элементы имеют разные классы, но выполняют одну функцию.
        // Здесь НЕ используем делегирование, так как эти элементы всегда присутствуют в DOM
        // с момента инициализации модалки.
        this.$modal.on('click', '.fs-lms-modal-backdrop, .fs-lms-modal-close, .fs-lms-modal-cancel, .js-modal-close, .fs-close', (e) => {
            e.preventDefault();
            this.close();
        });

        // Перехват отправки формы с валидацией и уведомлением подписчиков
        this.$form.on('submit.fs', (e) => {
            e.preventDefault(); // Предотвращаем стандартную перезагрузку страницы браузером

            if (!this._validate()) return; // Если валидация не прошла, прерываем выполнение

            const formData = this._collectFormData();
            // Уведомляем всех подписчиков (например, SubjectModalManager) о готовности данных
            this._saveCallbacks.forEach(cb => cb(formData));
        });

        // UX: Очистка состояния ошибки валидации ключа, как только пользователь начинает его изменять.
        // Это дает мгновенную обратную связь: как только пользователь начал исправлять поле,
        // ошибка исчезает, и он может отправить форму снова.
        // Без этого обработчика ошибка "ключ уже существует" оставалась бы висеть,
        // даже если пользователь ввел корректное значение.
        this.$keyInput.on('input.fs', () => {
            this.$keyInput[0].setCustomValidity('');
        });
    },

    /**
     * Регистрация колбэка, который будет вызван при попытке сохранения формы.
     * Реализует паттерн Pub/Sub (Издатель-Подписчик).
     * Модальное окно не знает о существовании SubjectModalManager — оно просто хранит
     * массив функций и вызывает их при отправке формы. Это делает компонент переиспользуемым.
     * @param {Function} callback - Функция, принимающая объект с данными формы.
     */
    onSave(callback) {
        if (typeof callback === 'function') {
            this._saveCallbacks.push(callback);
        }
    },

    /**
     * Открытие модального окна в режиме создания предмета.
     * Сбрасывает форму к исходному состоянию и устанавливает фокус на первое поле.
     */
    open() {
        // Сбрасываем форму к исходному состоянию перед каждым открытием.
        // Это критически важно, так как модалка может переоткрываться многократно,
        // и без сброса в ней остались бы данные от предыдущего создания предмета.
        this._resetForm();

        // Базовая логика открытия модалки (обычно добавляет класс 'active' с CSS-анимацией)
        openModal(this.$modal);

        // Привязываем клавишу ESC с уникальным неймспейсом 'subject'.
        // Неймспейсинг важен, чтобы закрытие этой модалки не влияло на другие.
        bindEsc('subject', () => this.close());

        // ПАТТЕРН: Двойной requestAnimationFrame для управления фокусом.
        // Браузеру требуется время на отрисовку модалки (CSS transition).
        // Если вызвать .focus() сразу, фокус может потеряться или вызвать "дерганье" интерфейса.
        // Двойной RAF гарантирует, что фокус будет установлен строго после завершения
        // перерисовки кадра, обеспечивая плавный UX.
        requestAnimationFrame(() => {
            requestAnimationFrame(() => {
                this.$nameInput.trigger('focus');
            });
        });
    },

    /**
     * Закрытие модального окна и очистка состояния.
     */
    close() {
        closeModal(this.$modal);
        // Обязательно отвязываем обработчик ESC, чтобы он не перехватывал нажатия клавиш,
        // когда модальное окно уже закрыто.
        unbindEsc('subject');
        this._resetForm();
    },

    /**
     * Управление состоянием кнопки сохранения (блокировка и изменение текста).
     * Вызывается менеджером при отправке AJAX-запроса для предотвращения двойного клика.
     * @param {boolean} loading - Флаг состояния загрузки.
     */
    setSaveState(loading) {
        this.$saveBtn
            .prop('disabled', loading)
            .text(loading ? 'Сохранение...' : 'Создать предмет и CPT');
    },

    /**
     * Программная установка ошибки валидации для поля ключа.
     * Вызывается менеджером, когда сервер возвращает ошибку "duplicate_key".
     * Использует HTML5 Constraint Validation API для отображения ошибки.
     *
     * @param {string} message - Текст ошибки для отображения.
     */
    setKeyError(message) {
        // setCustomValidity устанавливает сообщение об ошибке для встроенного в браузер механизма валидации.
        // Пустая строка '' означает, что поле валидно.
        this.$keyInput[0].setCustomValidity(message);

        // reportValidity() принудительно показывает браузерное всплывающее сообщение об ошибке
        // и подсвечивает поле красным. Это стандартное поведение HTML5 форм.
        this.$keyInput[0].reportValidity();
    },

    /**
     * Валидация формы перед отправкой.
     * Делегирует проверку внешней функции _validateAll, инициализированной в _cacheElements().
     * @private
     * @returns {boolean} true, если форма валидна; false, если есть ошибки.
     */
    _validate() {
        return this._validateAll();
    },

    /**
     * Сброс формы к исходному состоянию.
     * Очищает значения полей и сбрасывает состояния пользовательской валидации.
     * @private
     */
    _resetForm() {
        // reset() — встроенный метод HTMLFormElement, который сбрасывает все поля к начальным значениям.
        // Проверка this.$form[0] нужна на случай, если форма не найдена в DOM.
        if (this.$form[0]) this.$form[0].reset();

        // Сброс кастомной ошибки валидации ключа.
        // Без этого ошибка "ключ уже существует" от предыдущей попытки сохранения
        // осталась бы висеть и блокировала отправку формы.
        this.$keyInput[0].setCustomValidity('');
    },

    /**
     * Сбор данных из полей формы в единый объект.
     * @private
     * @returns {Object} Объект с подготовленными данными для отправки на сервер.
     */
    _collectFormData() {
        return {
            // .trim() удаляет случайные пробелы в начале и конце строки,
            // предотвращая ошибки "пустых" значений и проблемы с регистрацией ключей.
            name:        this.$nameInput.val().trim(),
            key:         this.$keyInput.val().trim(),
            tasks_count: this.$tasksCountInput.val(),
            // Nonce-токен безопасности, считанный из скрытого поля формы.
            // Он был сгенерирован на сервере при рендере страницы и используется
            // для защиты от CSRF-атак.
            security:    this.$nonceInput.val(),
        };
    },
};