/**
 * @module StudentPersonModal
 * @description UI-компонент модального окна для просмотра и редактирования данных студента.
 *              Поддерживает несколько режимов работы:
 *              1. Просмотр маскированных персональных данных (PII)
 *              2. Раскрытие полных данных (с логированием на сервере)
 *              3. Редактирование данных с валидацией
 *              4. Регенерация пароля связанного пользователя WordPress
 *              5. Отображение дополнительных зачислений студента (несколько предметов/групп)
 *
 *              Отвечает за:
 *              - Управление жизненным циклом модалки (открытие, закрытие, сброс)
 *              - Хранение идентификаторов (personId, wpUserId) в data-атрибутах
 *              - Заполнение полей данными из объекта (метод fill)
 *              - Обновление полей раскрытыми данными (метод fillRevealed)
 *              - Переключение между режимами просмотра и редактирования
 *              - Динамическое создание кнопок управления (Сохранить/Отмена)
 *              - Добавление строк дополнительных зачислений через клонирование шаблона
 *              - Генерацию кастомных событий для взаимодействия с менеджером
 *
 *              В отличие от ParentPersonModal, этот компонент поддерживает отображение
 *              нескольких зачислений студента (если он учится на нескольких предметах).
 *
 * @requires jQuery
 * @requires openModal, closeModal, bindEsc, unbindEsc - базовые утилиты управления модальными окнами
 */

import { openModal, closeModal, bindEsc, unbindEsc } from '../../../modules/modal-base.js';

const $ = jQuery;

/**
 * UI-компонент модального окна управления данными студента.
 * Работает в связке с StudentPersonModalManager, который обрабатывает AJAX-запросы.
 */
export const StudentPersonModal = {
    /** @type {jQuery|null} Ссылка на основной контейнер модального окна */
    $el: null,

    /** @type {boolean} Флаг для предотвращения повторной инициализации */
    _initialized: false,

    /**
     * Инициализация компонента.
     * Выполняет проверку существования модалки и навешивает события.
     */
    init() {
        // Защита от повторной инициализации (паттерн Singleton).
        // Если скрипт подключен несколько раз или init() вызывается повторно,
        // это предотвратит дублирование обработчиков событий.
        if ( this._initialized ) return;

        this.$el = $( '#fs-student-person-modal' );
        if ( ! this.$el.length ) return; // Если модалки нет в DOM, прекращаем инициализацию

        this._initialized = true;
        this._bindUiEvents();
    },

    /**
     * Привязка обработчиков событий пользовательского интерфейса.
     * @private
     */
    _bindUiEvents() {
        // Закрытие модалки при клике на фон, крестик или кнопку закрытия.
        // Все эти элементы имеют разные классы, но выполняют одну функцию.
        this.$el.on( 'click', '.fs-lms-modal-backdrop, .js-modal-close, .js-pmm-close', ( e ) => {
            e.preventDefault();
            this.close();
        } );
    },

    /**
     * Открытие модального окна.
     * Перед открытием должен быть вызван reset() для очистки состояния,
     * затем setPersonId/setWpUserId для установки идентификаторов,
     * и fill() для заполнения данными.
     */
    open() {
        // Привязываем клавишу ESC с уникальным неймспейсом 'spm'.
        // Неймспейсинг важен, чтобы закрытие этой модалки не влияло на другие.
        bindEsc( 'spm', () => this.close() );

        // Открываем модалку через базовую утилиту (обычно добавляет класс 'active' с CSS-анимацией)
        openModal( this.$el );
    },

    /**
     * Закрытие модального окна с полным сбросом состояния.
     * После закрытия вызывается reset() для очистки всех данных.
     */
    close() {
        unbindEsc( 'spm' );
        // closeModal принимает колбэк, который вызывается после завершения анимации закрытия.
        // Это гарантирует, что сброс состояния произойдет после визуального исчезновения модалки.
        closeModal( this.$el, () => this.reset() );
    },

    /**
     * Полный сброс модального окна к исходному состоянию.
     * Очищает все поля, тексты, кнопки, data-атрибуты и дополнительные зачисления.
     * Должен вызываться перед каждым новым открытием модалки.
     */
    reset() {
        // ОЧИСТКА ДОПОЛНИТЕЛЬНЫХ ЗАЧИСЛЕНИЙ:
        // Удаляем все динамически добавленные строки зачислений.
        // Это критически важно, так как при следующем открытии модалки
        // для другого студента не должны отображаться зачисления предыдущего.
        this.$el.find( '.js-spm-extra-enrollment-area' ).empty();

        // Очищаем все поля ввода с классом .fs-person-field
        this.$el.find( '.fs-person-field' ).val( '' );

        // Очищаем заголовок модалки
        this.$el.find( '.fs-lms-modal-title' ).text( '' );

        // Восстанавливаем кнопку "Показать данные" в исходное состояние
        this.$el.find( '.js-reveal-all' ).text( 'Показать данные' ).prop( 'disabled', false );

        // Удаляем кнопку генерации пароля, если она была создана
        this.$el.find( '.js-spm-regen-pwd' ).remove();

        // Выходим из режима редактирования (если были в нем)
        this.setEditing( false );

        // Удаляем идентификаторы из data-атрибутов
        this.$el.removeData( 'personId' ).removeData( 'wpUserId' );

        // Удаляем CSS-класс, указывающий на раскрытые PII-данные
        this.$el.removeClass( 'is-pii-revealed' );
    },

    /**
     * Добавление строки дополнительного зачисления в модалку.
     * Используется для отображения нескольких зачислений студента (если он учится на нескольких предметах).
     *
     * ПАТТЕРН: Клонирование шаблона (Template Cloning).
     * Вместо генерации HTML через шаблонные строки, мы клонируем скрытый шаблон из DOM,
     * заполняем его данными и добавляем в контейнер. Это более надежно и поддерживает
     * сложную структуру HTML с валидацией, стилями и событиями.
     *
     * @param {Object} data - Объект с данными зачисления.
     * @param {string} [data.contract_no] - Номер договора.
     * @param {string} [data.subject] - Название предмета.
     * @param {string} [data.group] - Название группы.
     * @param {string} [data.schedule] - Расписание занятий.
     */
    addEnrollmentRow( data ) {
        // КЛОНИРОВАНИЕ ШАБЛОНА:
        // Находим скрытый шаблон в DOM и клонируем его.
        // .clone() создает глубокую копию элемента со всеми дочерними элементами и атрибутами.
        // Шаблон находится внутри .js-spm-extra-enrollment-template, чтобы его можно было
        // легко найти и чтобы он не отображался на странице (обычно скрыт через CSS).
        const $tpl = this.$el.find( '.js-spm-extra-enrollment-template .js-spm-extra-enrollment-block' ).clone();

        // ЗАПОЛНЕНИЕ КЛОНИРОВАННОГО ШАБЛОНА:
        // Итерируем по всем ключам объекта data и заполняем соответствующие поля.
        // Поля ищутся по data-атрибуту data-enr-field="ключ".
        Object.entries( data ).forEach( ( [ key, val ] ) => {
            // Проверка val != null (нестрогое равенство) пропускает null и undefined,
            // но разрешает 0, false и пустую строку.
            // String(val) гарантирует, что значение будет строкой (защита от типов).
            $tpl.find( `[data-enr-field="${ key }"]` ).val( val != null ? String( val ) : '' );
        } );

        // ДОБАВЛЕНИЕ В КОНТЕЙНЕР:
        // Добавляем заполненный клон в область для дополнительных зачислений.
        // Теперь он станет видимым (если шаблон был скрыт, а клон — нет).
        this.$el.find( '.js-spm-extra-enrollment-area' ).append( $tpl );
    },

    /**
     * Установка ID персоны (студента) в data-атрибут.
     * @param {string|number} id - ID персоны.
     */
    setPersonId( id ) {
        this.$el.data( 'personId', id );
    },

    /**
     * Установка ID пользователя WordPress в data-атрибут.
     * @param {string|number} id - ID пользователя WordPress.
     */
    setWpUserId( id ) {
        this.$el.data( 'wpUserId', id );
    },

    /**
     * Получение ID персоны из data-атрибута.
     * @returns {number} ID персоны или 0, если не установлен.
     */
    getPersonId() {
        // parseInt с основанием 10 для явного указания десятичной системы.
        // Оператор || 0 обеспечивает fallback на 0, если значение null/undefined/NaN.
        return parseInt( this.$el.data( 'personId' ), 10 ) || 0;
    },

    /**
     * Получение ID пользователя WordPress из data-атрибута.
     * @returns {number} ID пользователя WordPress или 0, если не установлен.
     */
    getWpUserId() {
        return parseInt( this.$el.data( 'wpUserId' ), 10 ) || 0;
    },

    /**
     * Заполнение полей модалки базовыми данными.
     * Используется для отображения маскированных PII-данных и основной информации.
     *
     * @param {Object} data - Объект с данными для заполнения.
     * @param {string} [data.display_name] - Отображаемое имя для заголовка модалки.
     * @param {string} [data.last_name] - Фамилия.
     * @param {string} [data.phone] - Телефон (может быть маскированным).
     * @param {...*} [data.*] - Другие поля, соответствующие data-field атрибутам в HTML.
     */
    fill( data ) {
        // Специальная обработка заголовка: если передан display_name, устанавливаем его
        if ( 'display_name' in data ) {
            this.$el.find( '.fs-lms-modal-title' ).text( data.display_name || '' );
        }

        // Итерация по всем ключам объекта данных.
        // Object.entries() возвращает массив пар [ключ, значение], что позволяет
        // использовать forEach для итерации по объекту.
        Object.entries( data ).forEach( ( [ key, val ] ) => {
            // Ищем поле по data-атрибуту data-field="ключ" и устанавливаем значение.
            // Проверка val != null (нестрогое равенство) пропускает null и undefined,
            // но разрешает 0, false и пустую строку.
            // String(val) гарантирует, что значение будет строкой (защита от типов).
            this.$el.find( `[data-field="${ key }"]` ).val( val != null ? String( val ) : '' );
        } );
    },

    /**
     * Обновление полей раскрытыми (немаскированными) PII-данными.
     * Вызывается после успешного AJAX-запроса на раскрытие данных.
     * Меняет состояние кнопки "Показать данные" и добавляет CSS-класс.
     *
     * @param {Object} data - Объект с раскрытыми данными (телефон, документы, пароль и т.д.).
     */
    fillRevealed( data ) {
        // Итерация по всем ключам объекта данных
        Object.entries( data ).forEach( ( [ key, val ] ) => {
            // Обновляем поле только если значение truthy (не пустое).
            // Это предотвращает перезапись существующих значений пустыми строками.
            if ( val ) this.$el.find( `[data-field="${ key }"]` ).val( String( val ) );
        } );

        // Меняем текст кнопки и блокируем её, чтобы предотвратить повторное раскрытие
        this.$el.find( '.js-reveal-all' ).text( 'Данные раскрыты' ).prop( 'disabled', true );

        // Добавляем CSS-класс, который может использоваться для стилизации раскрытых данных
        // (например, изменение цвета фона или иконки)
        this.$el.addClass( 'is-pii-revealed' );
    },

    /**
     * Переключение между режимами просмотра и редактирования.
     * В режиме редактирования поля становятся доступными для изменения,
     * появляются кнопки "Сохранить" и "Отмена", скрывается кнопка "Редактировать".
     *
     * @param {boolean} editing - true для входа в режим редактирования, false для выхода.
     */
    setEditing( editing ) {
        // Переключаем атрибут readonly для всех полей, кроме тех, что имеют data-no-edit.
        // Атрибут data-no-edit используется для полей, которые нельзя редактировать
        // (например, ID, дата создания и т.д.).
        this.$el.find( '.fs-person-field:not([data-no-edit])' ).prop( 'readonly', ! editing );

        // Скрываем кнопку "Редактировать" в режиме редактирования
        this.$el.find( '.js-pmm-edit' ).prop( 'hidden', editing );

        // Переключаем CSS-класс режима редактирования на контейнере модалки
        this.$el.toggleClass( 'is-editing', editing );

        // Переключаем CSS-класс редактирования на отдельных полях
        // (может использоваться для визуального выделения редактируемых полей)
        this.$el.find( '.fs-pfield--editable' ).toggleClass( 'fs-pfield--editing', editing );

        if ( editing ) {
            // ВХОД В РЕЖИМ РЕДАКТИРОВАНИЯ:
            // Динамически создаем кнопки "Сохранить" и "Отмена" в футере модалки.
            // Это позволяет не держать их в HTML постоянно, а создавать только когда нужно.
            this.$el.find( '.fs-lms-modal-footer' ).append(
                '<button type="button" class="button button-primary js-pmm-save">Сохранить</button>' +
                '<button type="button" class="button js-pmm-cancel">Отмена</button>'
            );
        } else {
            // ВЫХОД ИЗ РЕЖИМА РЕДАКТИРОВАНИЯ:
            // Удаляем кнопки "Сохранить" и "Отмена", если они были созданы
            this.$el.find( '.js-pmm-save, .js-pmm-cancel' ).remove();
        }
    },

    /**
     * Сбор данных из всех редактируемых полей формы.
     * Используется при сохранении изменений.
     *
     * @returns {Object} Объект с данными формы, где ключи — значения data-field, значения — содержимое полей.
     */
    getEditData() {
        const data = {};

        // Проходим по всем полям с атрибутом data-field
        this.$el.find( '.fs-person-field[data-field]' ).each( ( _, el ) => {
            // Извлекаем ключ из data-атрибута
            const key = el.dataset.field;

            // Добавляем в объект только если ключ есть и значение не пустое (после trim).
            // Это предотвращает отправку пустых строк на сервер.
            if ( key && el.value.trim() ) data[ key ] = el.value.trim();
        } );

        return data;
    },

    /**
     * Отображение кнопки генерации нового пароля.
     * Вызывается, если сервер не смог вернуть существующий пароль (например, он не установлен).
     * Создает кнопку динамически и привязывает к ней обработчик, который генерирует кастомное событие.
     *
     * @param {string|number} wpUserId - ID пользователя WordPress для генерации пароля.
     */
    showRegenerateButton( wpUserId ) {
        // Динамически создаем кнопку генерации пароля
        const $btn = $( '<button type="button" class="button js-spm-regen-pwd">Сгенерировать новый пароль</button>' );

        // Добавляем кнопку в футер модалки
        this.$el.find( '.fs-lms-modal-footer' ).append( $btn );

        // Привязываем обработчик клика, который генерирует кастомное событие.
        // Это реализация паттерна Pub/Sub: модалка не знает о менеджере,
        // она просто "сообщает" миру о событии через $(document).trigger().
        // Менеджер (StudentPersonModalManager) подписан на это событие и обрабатывает его.
        // Имя события 'fs-lms:spm-regenerate-password' отличается от родительского 
        // 'fs-lms:regenerate-password', чтобы избежать конфликтов.
        $btn.on( 'click', () => {
            $( document ).trigger( 'fs-lms:spm-regenerate-password', { wpUserId, $btn } );
        } );
    },
};