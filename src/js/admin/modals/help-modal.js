/**
 * @module HelpModal
 * @description UI-компонент модального окна справочных инструкций для OAuth-провайдеров.
 *              Использует паттерн "один контейнер — много контентных блоков":
 *              в HTML заранее отрисованы все блоки инструкций (для Google, VK, GitHub и т.д.),
 *              но показывается только тот, который соответствует выбранному провайдеру.
 *
 *              Это эффективнее, чем подгружать контент через AJAX при каждом открытии,
 *              так как инструкции — это статический HTML, который не меняется динамически.
 *
 *              Отвечает за:
 *              - Переключение видимых блоков инструкций по data-атрибуту провайдера
 *              - Делегирование событий для кнопок открытия (поддержка динамически добавляемых кнопок)
 *              - Привязку клавиши ESC с уникальным неймспейсом
 *              - Безопасное закрытие модалки при клике на фон или крестик
 *
 * @requires jQuery
 * @requires openModal, closeModal, bindEsc, unbindEsc - базовые утилиты управления модальными окнами
 */

import { openModal, closeModal, bindEsc, unbindEsc } from '../modules/modal-base.js';

const $ = jQuery;

/**
 * Утилита для управления модальными окнами инструкций провайдеров.
 * @namespace HelpModal
 */
export const HelpModal = {
    /** @type {jQuery} Ссылка на основной контейнер модального окна */
    $modal: null,

    /** @type {boolean} Флаг для предотвращения повторной инициализации */
    _initialized: false,

    /**
     * Инициализация компонента.
     * Выполняет проверку существования модалки и навешивает события.
     * В отличие от других модалок, не кэширует элементы формы,
     * так как здесь нет полей ввода — только переключение готовых HTML-блоков.
     */
    init() {
        // Защита от повторной инициализации (паттерн Singleton).
        // Если скрипт подключен несколько раз или init() вызывается повторно,
        // это предотвратит дублирование обработчиков событий.
        if (this._initialized) return;

        this.$modal = $('#fs-lms-help-modal');
        if (!this.$modal.length) return; // Если модалки нет в DOM, прекращаем инициализацию

        this._initialized = true;
        this._bindEvents();
    },

    /**
     * Переключает контент под конкретного провайдера и открывает модалку.
     *
     * ПАТТЕРН: "Один контейнер — много контентных блоков".
     * В HTML-разметке модалки заранее находятся несколько блоков .fs-lms-help-content,
     * каждый с атрибутом data-provider="google|vk|github" и т.д.
     * При открытии мы скрываем все блоки и показываем только нужный.
     * Это быстрее, чем AJAX-загрузка, и позволяет работать офлайн.
     *
     * @param {string} provider - Идентификатор провайдера ('google', 'vk', 'github' и т.д.).
     *                           Должен совпадать со значением data-provider в HTML.
     */
    open(provider) {
        // Guard clause: если провайдер не передан, ничего не делаем.
        // Это защита от вызова open() без параметров или с пустой строкой.
        if (!provider) return;

        // СКРЫТИЕ ВСЕХ БЛОКОВ КОНТЕНТА:
        // Сначала скрываем все блоки инструкций через класс 'hidden' (обычно display: none).
        // Это гарантирует, что при повторном открытии модалки с другим провайдером 
        // не будет наложения контента (когда видны инструкции и для Google, и для VK одновременно).
        this.$modal.find('.fs-lms-help-content').addClass('hidden');

        // ПОИСК НУЖНОГО БЛОКА:
        // Ищем блок с соответствующим data-атрибутом.
        // Селектор `.fs-lms-help-content[data-provider="${provider}"]` — это комбинация 
        // класса и атрибута, что делает поиск точным и быстрым.
        const $targetContent = this.$modal.find(`.fs-lms-help-content[data-provider="${provider}"]`);

        // ЗАЩИТА ОТСУТСТВИЯ КОНТЕНТА:
        // Если для данного провайдера нет блока в HTML (например, опечатались в имени),
        // мы НЕ открываем пустую модалку. Это предотвращает появление "сломанного" интерфейса.
        if ($targetContent.length) {
            // Показываем только нужный блок
            $targetContent.removeClass('hidden');

            // Открываем модалку через базовую утилиту (обычно добавляет класс 'active' с CSS-анимацией)
            openModal(this.$modal);

            // Привязываем клавишу ESC с уникальным неймспейсом 'help_modal'.
            // Неймспейсинг важен: если на странице открыто несколько модалок одновременно 
            // (что маловероятно, но возможно), каждая будет иметь свой обработчик ESC,
            // и закрытие одной не повлияет на другие.
            bindEsc('help_modal', () => { this.close(); });
        }
    },

    /**
     * Закрывает модальное окно и отвязывает глобальные обработчики.
     */
    close() {
        closeModal(this.$modal);
        // Обязательно отвязываем обработчик ESC.
        // Если забыть это сделать, нажатие ESC на странице будет продолжать перехватываться 
        // логикой модалки, даже когда она визуально закрыта, что приведет к непредсказуемым ошибкам.
        unbindEsc('help_modal');
    },

    /**
     * Внутренние обработчики кликов для закрытия и кнопок-триггеров.
     * @private
     */
    _bindEvents() {
        // ДЕЛЕГИРОВАНИЕ СОБЫТИЙ ДЛЯ КНОПОК ОТКРЫТИЯ:
        // Обработчик навешан на document, но срабатывает только для элементов .js-open-help-modal.
        // Это критически важно, так как кнопки открытия справки могут быть добавлены в DOM динамически
        // (например, при рендере списка провайдеров через JS или после AJAX-обновления страницы).
        // Если бы мы использовали $('.js-open-help-modal').on('click', ...), 
        // то на новые кнопки событие не навесилось бы.
        $(document).on('click', '.js-open-help-modal', (e) => {
            e.preventDefault(); // Предотвращаем стандартное поведение (например, переход по ссылке #)

            // Считываем идентификатор провайдера из data-атрибута кнопки.
            // jQuery .data() автоматически преобразует kebab-case (data-provider-name) в camelCase,
            // но здесь атрибут простой (data-provider), поэтому обращаемся напрямую.
            const provider = $(e.currentTarget).data('provider');
            this.open(provider);
        });

        // ОБРАБОТЧИКИ ЗАКРЫТИЯ МОДАЛКИ:
        // Закрываем модалку при клике на крестик (.fs-lms-modal-close) или на затемненный фон (.fs-lms-modal-backdrop).
        // Это стандартный UX-паттерн: пользователь ожидает, что клик вне контента закроет окно.
        // Здесь НЕ используем делегирование, так как эти элементы всегда присутствуют в DOM 
        // с момента инициализации модалки.
        this.$modal.find('.fs-lms-modal-close, .fs-lms-modal-backdrop').on('click', () => {
            this.close();
        });
    }
};