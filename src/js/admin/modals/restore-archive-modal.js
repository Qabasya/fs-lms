/**
 * @module RestoreArchiveModal
 * @description UI-компонент модального окна выбора способа восстановления записи из архива.
 *              Реализует промисифицированный API с отложенным разрешением (Deferred-like pattern):
 *              модалка возвращает Promise, который разрешается только после того, как пользователь
 *              сделает выбор или закроет окно. Это позволяет использовать её в async/await конструкциях.
 *
 *              Отвечает за:
 *              - Отображение двух вариантов восстановления: с данными родителя или без них
 *              - Динамическое отключение опции "с родителем", если в системе нет данных родителя
 *              - Возврат результата выбора через Promise ({ withParent: boolean })
 *              - Lazy-инициализацию обработчиков событий с защитой от дублирования
 *              - Корректную очистку состояния при любом способе закрытия (подтверждение, отмена, ESC)
 *
 * @requires jQuery
 * @requires openModal, closeModal, bindEsc, unbindEsc - базовые утилиты управления модальными окнами
 */

import { openModal, closeModal, bindEsc, unbindEsc } from '../modules/modal-base.js';

const $ = jQuery;

/**
 * UI-компонент модального окна выбора способа восстановления из архива.
 * Работает в связке с ArchiveViewModalManager, который вызывает choose()
 * и обрабатывает результат через .then()/.catch().
 */
export const RestoreArchiveModal = {
    /** @type {jQuery|null} Ссылка на основной контейнер модального окна */
    $modal:        null,

    /**
     * Флаг, указывающий, что обработчики событий уже привязаны.
     * Используется вместо _initialized, так как этот модуль использует lazy-setup:
     * обработчики навешиваются не в init(), а при первом вызове choose().
     * @private
     * @type {boolean}
     */
    _eventsBound:  false,

    /**
     * Ссылка на функцию resolve текущего Promise.
     * Хранится как свойство объекта, чтобы быть доступной в обработчиках событий,
     * которые навешиваются один раз при _setup(), а не при каждом вызове choose().
     * @private
     * @type {Function|null}
     */
    _resolve:      null,

    /**
     * Ссылка на функцию reject текущего Promise.
     * Аналогично _resolve, хранится для доступа из обработчиков событий.
     * @private
     * @type {Function|null}
     */
    _reject:       null,

    /**
     * Ленивая инициализация (Lazy Setup).
     * Выполняет поиск модалки в DOM и привязку обработчиков событий.
     * Вызывается автоматически при первом вызове choose() или init().
     *
     * ПАТТЕРН: Защита от дублирования обработчиков через _eventsBound.
     * В отличие от других модалок, где обработчики навешиваются в init() один раз,
     * здесь используется отдельный флаг _eventsBound. Это позволяет вызывать init()
     * в любой момент, а реальная привязка событий произойдет только при первом
     * открытии модалки (когда она гарантированно есть в DOM).
     *
     * @private
     * @returns {boolean} true, если модалка найдена и готова к работе; false, если нет.
     */
    _setup() {
        // Если $modal еще не кэширован или кэширован, но элемент исчез из DOM,
        // пытаемся найти его заново. Это полезно, если модалка была добавлена 
        // в DOM динамически после вызова init().
        if ( ! this.$modal || ! this.$modal.length ) {
            this.$modal = $( '#fs-restore-archive-modal' );
        }

        // Если модалки всё равно нет в DOM, возвращаем false.
        // Вызывающий код (choose()) обработает это и вернет rejected Promise.
        if ( ! this.$modal.length ) { return false; }

        // Привязываем обработчики событий ТОЛЬКО ОДИН РАЗ.
        // Без этой проверки каждый вызов choose() навешивал бы новые обработчики 
        // поверх старых, что привело бы к множественным срабатываниям и утечкам памяти.
        if ( ! this._eventsBound ) {
            this._eventsBound = true;

            // ОБРАБОТЧИК КНОПКИ ПОДТВЕРЖДЕНИЯ:
            // При клике на "Восстановить" считываем выбранную radio-кнопку 
            // и разрешаем Promise объектом { withParent: boolean }.
            this.$modal.on( 'click', '#ram-confirm-btn', () => {
                // Считываем значение выбранной radio-кнопки.
                // value="1" означает "с данными родителя", value="0" — "без данных".
                // Приводим к boolean через сравнение === '1'.
                const withParent = this.$modal.find( 'input[name="ram-mode"]:checked' ).val() === '1';

                // СОХРАНЕНИЕ ССЫЛКИ НА RESOLVE:
                // Перед вызовом _close() сохраняем ссылку на resolve в локальную переменную.
                // Это критически важно, так как _close() обнуляет this._resolve (см. ниже).
                // Если бы мы вызвали this._resolve() после _close(), получили бы ошибку 
                // "this._resolve is not a function", так как свойство уже стало null.
                const resolve = this._resolve;

                // Закрываем модалку и очищаем состояние
                this._close();

                // РАЗРЕШЕНИЕ PROMISE С ОПЦИОНАЛЬНОЙ ЦЕПОЧКОЙ:
                // Оператор ?.() вызывает функцию только если она не null/undefined.
                // Это защита от edge case, когда обработчик срабатывает, 
                // но Promise уже был разрешен/отклонен другим способом.
                resolve?.( { withParent } );
            } );

            // ОБРАБОТЧИКИ ОТМЕНЫ/ЗАКРЫТИЯ:
            // При клике на фон, кнопку отмены или крестик — отменяем выбор.
            // Все эти действия приводят к вызову _cancel(), который отклоняет Promise.
            this.$modal.on( 'click', '.fs-lms-modal-backdrop, .fs-lms-modal-cancel, .js-modal-close', () => {
                this._cancel();
            } );
        }

        return true;
    },

    /**
     * Публичная инициализация компонента.
     * Может быть вызвана при загрузке страницы, но реальная привязка событий
     * произойдет только при первом вызове choose() (lazy setup).
     */
    init() {
        this._setup();
    },

    /**
     * Открытие модального окна для выбора способа восстановления.
     * Возвращает Promise, который разрешается объектом { withParent: boolean }
     * при подтверждении выбора, или отклоняется строкой 'cancel' при отмене.
     *
     * ПАТТЕРН: Отложенное разрешение Promise (Deferred-like).
     * Вместо того чтобы разрешать Promise сразу (как в AlertModal),
     * мы сохраняем ссылки на resolve/reject в свойствах объекта (_resolve/_reject).
     * Это позволяет обработчикам событий, навешанным один раз при _setup(),
     * разрешать/отклонять "текущий" Promise при каждом новом вызове choose().
     *
     * @param {boolean} [hasParent=true] - Флаг, указывающий, есть ли в системе данные родителя
     *                                    для восстанавливаемой записи. Если false, опция
     *                                    "с данными родителя" будет отключена.
     * @returns {Promise<{withParent: boolean}>} Промис с результатом выбора пользователя.
     */
    choose( hasParent = true ) {
        // Пытаемся выполнить lazy setup. Если модалка не найдена в DOM,
        // возвращаем rejected Promise с понятной ошибкой.
        // Это graceful degradation: вызывающий код может обработать ошибку через .catch().
        if ( ! this._setup() ) {
            return Promise.reject( new Error( '#fs-restore-archive-modal not found in DOM' ) );
        }

        // Создаем НОВЫЙ Promise при каждом вызове choose().
        // Это позволяет использовать модалку многократно: каждый вызов создает 
        // свой независимый Promise, который будет разрешен/отклонен независимо от других.
        return new Promise( ( resolve, reject ) => {
            // СОХРАНЕНИЕ ССЫЛОК НА RESOLVE/REJECT:
            // Это ключевой момент паттерна. Мы сохраняем функции resolve и reject 
            // в свойствах объекта, чтобы обработчики событий (навешанные один раз при _setup())
            // могли вызвать их позже, когда пользователь сделает выбор.
            // При следующем вызове choose() эти ссылки будут перезаписаны новыми функциями,
            // что обеспечит корректную работу с "текущим" Promise.
            this._resolve = resolve;
            this._reject  = reject;

            // ПОЛУЧЕНИЕ ССЫЛОК НА ЭЛЕМЕНТЫ УПРАВЛЕНИЯ:
            // Находим radio-кнопку, её label и текстовое описание для динамического изменения.
            const $input = this.$modal.find( '#ram-mode-with-parent' );
            const $label = this.$modal.find( '#ram-with-parent-label' );
            const $text  = this.$modal.find( '#ram-with-parent-text' );

            // УСЛОВНОЕ ОТКЛЮЧЕНИЕ ОПЦИИ:
            // Если в системе нет данных родителя для этой записи (hasParent === false),
            // мы не можем предложить пользователю восстановление "с родителем".
            // Вместо того чтобы скрывать эту опцию, мы отключаем её и показываем причину.
            // Это улучшает UX: пользователь видит все возможные варианты и понимает, 
            // почему один из них недоступен, а не гадает, куда делась опция.
            if ( ! hasParent ) {
                // Блокируем radio-кнопку
                $input.prop( 'disabled', true );

                // Добавляем CSS-класс для визуального обозначения недоступности 
                // (обычно делает текст серым и добавляет курсор "not-allowed")
                $label.addClass( 'fs-radio-label--disabled' );

                // Объясняем причину недоступности
                $text.text( 'С данными родителя — нет данных в системе' );

                // Автоматически выбираем единственную доступную опцию: "без данных родителя"
                this.$modal.find( 'input[name="ram-mode"][value="0"]' ).prop( 'checked', true );
            } else {
                // Если данные родителя есть, делаем опцию доступной
                $input.prop( 'disabled', false );
                $label.removeClass( 'fs-radio-label--disabled' );
                $text.text( 'С данными родителя — заявка сразу готова к проверке' );
            }

            // Привязываем клавишу ESC с уникальным неймспейсом 'restore_archive'.
            // При нажатии ESC вызываем _cancel(), который отклонит Promise.
            bindEsc( 'restore_archive', () => this._cancel() );

            // Открываем модалку через базовую утилиту
            openModal( this.$modal );
        } );
    },

    /**
     * Отмена выбора пользователя.
     * Вызывается при клике на фон, кнопку отмены, крестик или нажатии ESC.
     * Закрывает модалку и отклоняет текущий Promise со строкой 'cancel'.
     * @private
     */
    _cancel() {
        this._close();
        // Отклоняем Promise со строкой 'cancel'.
        // Вызывающий код (ArchiveViewModalManager) проверяет reason === 'cancel' 
        // в .catch() и игнорирует этот случай, так как отмена пользователем 
        // не является ошибкой, требующей показа уведомления.
        // Оператор ?.() защищает от вызова null, если _reject уже обнулен.
        this._reject?.( 'cancel' );
    },

    /**
     * Внутренний метод закрытия модального окна с полной очисткой состояния.
     * Отвязывает обработчик ESC, закрывает UI и обнуляет ссылки на resolve/reject.
     *
     * ВАЖНО: Этот метод НЕ разрешает и НЕ отклоняет Promise.
     * Он только подготавливает модалку к следующему вызову choose().
     * Разрешение/отклонение происходит в обработчиках событий (#ram-confirm-btn, _cancel).
     * @private
     */
    _close() {
        // Обязательно отвязываем обработчик ESC, чтобы он не перехватывал нажатия 
        // клавиш, когда модальное окно уже закрыто.
        unbindEsc( 'restore_archive' );

        // Закрываем модалку через базовую утилиту
        closeModal( this.$modal );

        // ОЧИСТКА ССЫЛОК НА RESOLVE/REJECT:
        // Это критически важный шаг. Если не обнулить эти свойства, 
        // при следующем вызове choose() может произойти утечка памяти 
        // (старые функции останутся в памяти) или неожиданное поведение 
        // (если обработчик сработает после _close(), но до следующего choose()).
        // Обнуление гарантирует, что каждый Promise живет только в рамках одного вызова choose().
        this._resolve = null;
        this._reject  = null;
    },
};